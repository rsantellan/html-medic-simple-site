				    <h1>El frío: enemigo de los hipertensos</h1>	
					  <p>De acuerdo a estudios realizados por el London School of Hygiene &amp; Tropical Medicine del Reino Unido, la baja de 1ºC de la temperatura media diaria se asocia a un incremento en el riesgo de los infartos. Se estima que cada vez que la temperatura baja un 1ºC, se eleva en un 2% la probabilidad de sufrir un ataque al corazón.</p>
					  <p>El frío provoca vasoconstricción (estrechamiento de las arterias) para evitar que se pierda calor por la piel y por eso sube la tensión arterial. Además en los meses de bajas temperaturas, disminuye la práctica de actividad física, las dietas son más calóricas y las enfermedades respiratorias aumentan, generando inflamaciones que afectan el árbol vascular.</p>
					  <h2>¿Cómo cuidarnos?</h2>
					  <p>					    Lo primero que recomendamos es no fumar, quienes fuman tienen el doble de riesgo de sufrir un infarto en comparación con quienes no lo hacen.</p>
					  <p>También aconsejamos mantener una alimentación saludable, evitando el consumo de grasas saturadas y priorizando el consumo de frutas y verduras.</p>
					  <p>Realizar actividad física en forma frecuente es beneficioso, por lo menos tres veces a la semana. </p>
					  <p>Controlar el peso es importante ya que el exceso de kilos produce una serie de cambios en el organismo que predisponen a tener enfermedades cardiovasculares.</p>
					  <p>Otro factor a tener en cuenta para el cuidado es alternar el trabajo con la recreación, el cansancio acumulado y las jornadas laborales mayores a 10 horas diarias predisponen a infartos y otros problemas vasculares.</p>
					  <p>Según estudios realizados por la University College de Londres quienes trabajan más de 55 horas a la semana tienen 1.3 veces más  riesgo de morir por un infarto u otra enfermedad coronaria.</p>
