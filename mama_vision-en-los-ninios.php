						<h1>Cómo se debe atender la visión de los niños</h1>
						<ul class="notas_mama">
							<li>Revista Tu bebé </li>
							<li>Dr. Oswaldo Borras</li>
						</ul>
						<p>La visión de un niños es algo que se manifiesta a través de pequeños indicadores a los que, tanto padres como educadores, deben estar atentos para observar y evaluar cualquier anomalía.</p>
						<p>Antes de comenzar a hablar sobre los controles y los problemas que pueden llegar a surgir, hagamos un pantallazo breve sobre el desarrollo visual de cada niño, según la edad que tenga. </p>
						<p><strong>En el primero año de vida</strong></br>En este periodo el bebé se interesa por los objetos de mucho contraste. Los de colores fuertes son los que le llaman más la atención y las cosas que estén a mucha distancia no le atraen.</p>
						<p><strong>De uno a dos años</strong></br>Empiezan la exploración de otros objetos que estén un poco más lejos. Es lo que va a estimular a gatear, a caminar y a corre. Comienzan a coordinar ojo-mano, lo que el bebé ve lo podrá tocar. Articulará juguetes, y se dará cuenta de las formas y tamaño de las cosas.</p>
						<p><strong>De tres a cinco años</strong>A esta edad el desarrollo visual es creciente. El niño usará su visión para leer y escribir, siguiendo visualmente de izquierda a derecha.</p>
						<p><strong>¿Cuáles son las patologías de visión más comunes en los niños?</strong></br>Las patologías más frecuentes son las relacionadas con los vicios de refracción, lo que popularmente se conoce como “corto de vista”, y es a lo que se debe prestar atención sobre todo cuando hay una diferencia entre un ojo y otro; por ejemplo cuando un ojo ve bien y el otro tiene un vicio de refracción que le produce una menor visión, eso es importante destacarlo porque ese ojo al no recibir la imagen bien enfocada no está desarrollando bien su función visual y eso puede llegar a generar un problema después que puede ser irreversible en algunos casos.</p>
						<p><strong>¿Y cómo se puede llegar a detectar?</strong></br>La revisión es fundamental ya que, retomando el ejemplo anterior, si un niño ve bien con un ojo pero mal con el otro, pueden pasar años antes de que lo llegue a manifestar, en ese sentido la responsabilidad es del profesional y de los padres de hacer un protocolo de seguimiento.</p>
						<p>Los niños deberían ser controlados al nacer para descartar determinado tipo de patologías como ser cataratas congénitas, trastornos que alteren la trasparecía de las estructuras que permiten que la imagen llegue a la retina.</p>
						<p>Después, si todo está bien, se debe realizar otro control al año para apartar algún tipo de anomalías visuales o desviaciones oculares, aunque estas también puede descartarse antes.</p>
						<p>Si en estos dos controles iniciales no surge nada en particular, se deben mantener controles periódicos buscando cualquier tipo de alteraciones.</p>
						<p><strong>A medida que el niño va creciendo la vista se va desarrollando con él, ¿esto también se controla?</strong></br>Por supuesto, hay determinadas funciones visuales de acuerdo a la edad y es fundamental el acompañarlas con controles periódicos, incluso en los bebés, hay pequeños elementos que pueden indiciar alteraciones en este desarrollo.</p>
						<p><strong>¿Hay otros síntomas que se asocien con que este desarrollo no se está dando de forma correcta, como pueden ser las jaquecas? </strong></br>Si, muchos asocian a los dolores de cabeza con esto y es un tema de consulta frecuente, pero habitualmente la causa ocular de estos dolores no es lo más normal, pero de todas maneras hay que descartarlo, hay un tipo de alteración, de vicio de refracción, que puede provocarlas y es la que se da en los niños que presentan hipermetropía, que vendría a ser lo opuesto a miopía, o sea, el chico tiene una gran capacidad de enfoque, puede ver bien, pero ese esfuerzo involuntario produce cefaleas. Por lo que en la consulta parece que está todo bien, porque el niño enfoca correctamente, se realiza un refractometría computada y da normal, y en esos casos lo que se hace es paralizar ese músculo de enfoque con unas gotitas, se repite el examen y ahí aparece una hipermetropía. </p>
						<p><strong>¿Desde que edad los niños pueden empezar a usar lentes para corregir esas alteraciones?</strong></br>Desde que sea necesario, por ejemplo, en casos extremos, un niño operado de cataratas congénitas a los 3 o 4 meses en los que no se colocan lentes intraoculares ya que se recomienda ponerlas al año, usará lentes de contacto hasta que se lo pueda volver a operar.</p>
						<p>O sea que la corrección tiene que ser cuando es correcta hacerla, no hay edad determinada. Lo que sucede a veces con los bebés es que tiene una gran capacidad de enfoque por lo que pueden tener algún vicio de refracción, pero logran enfocar y eso está bien, se debe controlar que eso no genere otras alteraciones como cefaleas, estrabismo o desviación ocular.</p>
						<p>Cuando son miopías no muy importantes, el miope ve bien y no es necesario mandarle lentes.</p>
						<p>En resumen, no hay una edad para comenzar a corregir la visión, depende del caso, si corresponde que un bebé use lentes se colocarán.</p>
