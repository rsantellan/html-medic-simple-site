				    <h1>Obesidad: Uno de los mayores problemas de salud de nuestros tiempos.</h1>	
					  <p>La obesidad constituye un exceso de grasa corporal que determina la aparición de numerosas repercusiones a nivel de distintos sistemas, y con esto, un importante aumento de la mortalidad.</p>
					  <p>Responde a factores genéticos, metabólicos, endocrinológicos y ambientales, destacando un particular protagonismo  este último factor. La obesidad exógena, o sea por sobrealimentación, es la más representativa por lejos.</p>
					  <p>Hoy en día, la obesidad, es uno de los mayores problemas de salud, tanto para países desarrollados como en vías de desarrollo, siendo declarada actualmente por la OMS como una epidemia mundial.</p>
					  <p>La Encuesta Nacional de Sobrepeso y Obesidad II (ENSO II) realizada en 2009 revela que el 54% de la población adulta del Uruguay alcanza o excede el IMC de 25 kg/m2 y el 20% presenta un IMC (peso (kg)/ altura2 (m) superior a 30 kg/m2.</p>
					  <p>Eso significa que uno de cada dos uruguayos padece sobrepeso y uno de cada cinco uruguayos es obeso.</p>
