					  <p>Esto tiene para el niño severas repercusiones a nivel clínico y en su desarrollo psico-emocional.</p>
					  <p>La obesidad confiere un aumento del riesgo de morbimortalidad, no solamente de origen cardiovascular sino también de otras causas, como cáncer o diabetes y sus complicaciones.</p>
