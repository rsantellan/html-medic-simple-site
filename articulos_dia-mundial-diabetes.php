				    <h1>Día Mundial de la Diabetes<br>
				      14 de Noviembre</h1>	
					  <p>La diabetes mellitus es una patología que se produce cuando el cuerpo procesa mal el azúcar y se genera un exceso de la misma en la sangre. Esto se produce por una insuficiente secreción o acción de la insulina, una hormona producida por el páncreas y cuya función, entre otras, es permitir que la glucosa – presente en algunos alimentos que ingerimos – sea aprovechada como energía por nuestro organismo.</p>
					  <p><strong>Existen 4 tipos de diabetes:</strong></p>
					  <ul>
					    <li><span>&#8226;</span><strong>Diabetes Tipo 1:</strong> Se diagnostica con mayor frecuencia en niños, adolescentes o adultos jóvenes. El cuerpo no produce o produce poca insulina y se necesitan inyecciones diarias de esta hormona.</li>
					    <li><span>&#8226;</span><strong>Diabetes Tipo 2:</strong> Corresponde a la mayoría de los casos de diabetes. Generalmente se presenta en la edad adulta, aunque ahora se está diagnosticando en adolescentes y adultos jóvenes debido a las altas tasas de obesidad.  Muchas personas no saben que la padecen. No requiere de insulina para su tratamiento, sino utilizar fármacos que bajan la glicemia, hacer dieta y ejercicio.</li>
					    <li><span>&#8226;</span><strong>Diabetes gestacional:</strong> Es el azúcar alto en la sangre que se presenta en cualquier momento durante el embarazo en una mujer que no tiene diabetes.</li>
					    <li><span>&#8226;</span><strong>Otros tipos específicos de diabetes:</strong> Están asociados a algunos defectos genéticos, a endocrinopatías como el hipertiroidismo, o inducida por fármacos o algunas infecciones por virus.</li>
				      </ul>
					  <p>Centrándonos en los niños, está estudiado que existe una alta relación entre la obesidad y la diabetes infantil. La OMS entiende que la obesidad y el sobrepeso en niños es uno de los principales problemas de este siglo.  El promedio de niños obesos de entre 0 y 5 años en Uruguay es de 10.5% situándose por encima de la media de América Latina que es de 7.1%.</p>
					  <p>Si nos comparamos con la región, nuestro país tiene la peor incidencia de sobrepeso y obesidad junto con Chile (9.4%). En Montevideo la prevalencia es mayor que en el interior. Los niños que están por encima del peso recomendado, tienen más probabilidad de desarrollar patologías cuando son adultos como cardiopatías, ser resistentes a la insulina, que se puede traducir en diabetes, artrosis o tener más predisposición a padecer cáncer de colon, mama o endometrio.</p>
					  <p>Se recomienda aumentar el consumo de frutas y verduras evitando alimentos complementarios ricos en grasas, azúcar y sal.  Comer con sal puede generar hipertensión en niños. El consumo de azúcar tampoco es bueno y Uruguay tiene un alto consumo: 36% de los niños menores de 2 años consumen habitualmente golosinas y alfajores y 72% de 2 a 4 años.</p>
					  <p>Las organizaciones Mundial y Panamericana de la Salud piden reducir a menos del 10% de calorías totales la ingesta diaria de azúcares libres.  Los mismos son los monosacáridos (glucosa y fructosa) y los disacáridos (sacarosa o azúcar de mesa) que se agregan a los alimentos y las bebidas por el fabricante, el cocinero o el consumidor, así como azúcares que están naturalmente presentes en miel, jarabes, jugos de fruta y concentrados de fruta.</p>
					  <p>El azúcar no es un nutriente esencial y hay evidencia sólida que muestra que en realidad puede ser perjudicial al contribuir al sobrepeso, la obesidad y la caries dental.</p>
