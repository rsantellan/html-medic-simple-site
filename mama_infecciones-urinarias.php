						<p>Es una infección bacteriana de las vías urinarias las cuales abarcan la vejiga, los riñones, los uréteres (conductos que llevan la orina desde cada riñón hasta la vejiga) y la uretra (el conducto que saca la orina desde la vejiga hacia fuera). </p>
						<p>Puede ocurrir cuando las bacterias encuentran el camino hacia la vejiga o los riñones. Estas bacterias normalmente se alojan en la piel alrededor del ano o algunas veces alrededor de la vagina.</p>
						<p>Los niños pequeños con infecciones urinarias pueden solo tener fiebre, inapetencia, vómitos o no tener síntomas en absoluto, la mayoría solo comprometen la vejiga. Si la infección se disemina a los riñones, se denomina pielonefritis y suele ser más seria.</p>
						<p><strong>¿Qué son concretamente las infecciones urinarias?</strong></br>Tal y como lo indica su nombre, son infecciones del aparato urinario, que está compuesto por los riñones, los uréteres que son unos conductos que van hasta la vejiga, que es el receptáculo de la orina y la uretra que es otro conducto que comunica la vejiga con el exterior; la infección se puede presentar en cualquier parte del aparato, en la vejiga, se la denomina baja y si compromete los riñones, alta.</p>
						<p><strong>¿Cómo se comporta en las edades tempranas?</strong></br>En el niño se diferencia su comportamiento de acuerdo a la edad; si es un lactante, estas infecciones son una entidad severa y cuanto más pequeño el niño, mayor la gravedad, a parte las manifestaciones de la enfermedad son mucho más sutiles ya que no puede comunicarse, un chico más grande puede decir: “Me arde al hacer pichi”, por ejemplo, mientras el bebé simplemente puede hacer fiebre sin causa aparente o no aumenta de peso o pierde apetito, síntomas comunes a varias infecciones, y en las edades tempranas hay que descubrirlas rápidamente.</p>
						<p><strong>¿Cómo se detecta una infección urinaria?</strong></br> Si frente a un cuadro febril, el pediatra ve que el niño se encuentra un foco ostensible que lo cause, como puede ser un tema de garganta u oído entre otros, lo primero que va a hacer es mandar un examen y un cultivo de orina; también puede pasar que al llevarlo a controlar se vea que el pequeño perdió peso o no aumentó lo que debiera, aunque no tenga otros síntomas, igualmente se debe sospechar y realizar los estudios correspondientes; porque en el niño por lo general son infecciones altas y bajas a la vez, siempre afectan al riñón.</p>
						<p><strong>¿Qué causa esta infección?</strong></br>En el niño pequeño, por lo general son causas congénitas que hacen que tenga alguna malformación y la más frecuente es causada por lo que se llama el reflujo vesicoureteral, o sea, cuando se orina la vejiga se contrae y la orina normalmente sale hacia fuera y no refluye hacia los riñones, cuando existe algún problema de los uréteres la orina vuelve hacia arriba y va generando problemas en distintos grados.</p>
						<p><strong>¿Se cura con el paso del tiempo?</strong></br>Eso va a depender de los grados, si es un reflujo pequeño al niño hay que tratarlo durante la infección y cuando está sano para que no se produzca nuevamente, pero cuando el reflujo alcanza el grado 4 o 5 que son los más altos, o sea, ya se han deformado los uréteres, el niño debe ser sometido a cirugía y se deben reimplantar esos conductos para que no refluya la orina; será el médico tratante quien determine el grado y el tratamiento en cada caso.</p>
						<p><strong>¿Cuándo los niños ya son más grandes también puede ocurrir la infección de igual manera?</strong></br>Si, puede darse tanto en niñas como en varones y el tratamiento siempre va a ser con antibióticos, aunque sea una infección urinaria baja, o sea una cistitis nada más, se lo trata con antibióticos. Por esta infección que es más común en las niñas, es que los pediatras insistimos en que la higiene luego de la orina debe ser de adelante hacia atrás; si no hay una malformación es simplemente maximizar las medidas de higiene y tratarla en el momento que tiene la infección que no genera mayores consecuencias, la importancia es detectar que no existan esas malformaciones que mencionaba anteriormente sobre todo en las edades más tempranas.  </p>
