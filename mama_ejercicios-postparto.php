					  <h1>Ejercicios postparto: qué, cómo y cuándo realizarlos</h1>
                      <p>Una vez que ya estamos en casa más seguras con el nuevo integrante y empezando de a poco con nuestra rutina es tiempo de pensar en volver a hacer ejercicios para ir recuperando la figura.</p>
                      <p><strong>Ahora, ¿qué ejercicios podemos realizar y cuándo conviene hacerlos?</strong></p>
                      <p>Hasta los seis meses del postparto, se recomienda realizar actividad cardiovascular de intensidad moderada inicialmente, para luego ir aumentándola. Debe ser de bajo impacto e incluir ejercicios localizados enfocados en recuperar la musculatura abdominal y la estabilidad abdominopélvica, por ejemplo, se puede hacer yoga, natación y elíptica.</p>
                      <p>Como norma general, la actividad física se puede retomar cuatro semanas después del parto normal y seis semanas luego de una cesárea. Asimismo, los ejercicios de piso pélvico se pueden efectuar de forma precoz desde el postparto inmediato, sobre todo si se aprendieron durante el embarazo.</p>
                      <p>Se puede realizar actividad física entre dos y cuatro veces a la semana, dependiendo del nivel de entrenamiento previo de la mujer. Lo ideal es complementar ejercicios aeróbicos de forma progresiva -como elíptica o bicicleta- con algunos localizados de abdomen, brazos, glúteos, espalda y de piso pélvico. Los abdominales clásicos tipo crunch están contraindicados, al igual que toda actividad deportiva que aumente mucho la presión intraabdominal como el jumping.</p>
                      <p><strong>Entrenamiento muscular de piso pélvico</strong></p>
                      <p>Existe una serie de ejercicios específicos para hacer un entrenamiento de la fuerza y resistencia de la musculatura pelviana. Anteriormente, se conocían como ejercicios de Kegel, pero en la actualidad se les llama entrenamiento muscular de piso pélvico, ya que incorpora, además de los ejercicios localizados perineales, una visión más global del sistema abdominopélvico.</p>
                      <p>Incluye abdomen profundo y se enfoca en la buena postura al activar el periné y en que la respiración acompañe adecuadamente. Su objetivo es mejorar la funcionalidad del piso pélvico, considerando que dentro de sus funciones principales destacan la de soporte de los órganos de esa zona. </p>
                      <p>Se recomienda que todas las mujeres realicen estos ejercicios, independiente de la vía de parto, ya que el embarazo por sí solo debilita esta musculatura. Para alcanzar un fortalecimiento óptimo se debe realizar un entrenamiento diario durante tres meses. Luego, se recomienda mantener rutinas por lo menos tres a cuatro veces por semana.</p>
                      <p>Para una buena ejecución de estos ejercicios, se debe adoptar una buena postura, esto es, estar acostada, con caderas y piernas flectadas y pies apoyados. Luego, hay que contraer el piso pélvico como si estuvieras reteniendo tus necesidades, sin cortar la respiración ni contraer los glúteos, piernas o abdomen. También se puede progresar a otras posturas agregando gravedad, ya sea sentada o de pie.</p>
                      <p>Las mujeres que hayan presentando alguna disfunción después del parto, como incontinencia de orina o fecal y desgarros perineales, deben ser asesoradas por su médico tratante.</p>
