						<h1>Recomendaciones para viajeros: ébola</h1>	
						<p>La Organización Mundial de la Salud (OMS) ha declarado el brote de ébola en África occidental como una emergencia de salud pública de importancia internacional y recomendó medidas excepcionales para detener su transmisión.</p>
						<p>Una de ellas es que los países donde hay contagio del ébola efectúen exámenes a la salida de aeropuertos, puertos marítimos y en cruces fronterizos de quienes presenten síntomas febriles que puedan asociarse a esta enfermedad que no tiene tratamiento específico ni vacuna.</p>
						<h2>¿Qué es el ébola?</h2>
						<p>Este virus es causante de una fiebre hemorrágica viral muy grave en humanos y otros primates. Los brotes de esta enfermedad tienen una tasa de letalidad que puede llegar al 90% y se producen principalmente en aldeas remotas de África central y occidental, cerca de la selva tropical, ya que el reservorio del virus sería un tipo de murciélago presente en este continente.</p>
						<p>El virus se transmite inicialmente a humanos por contacto con animales infectados, por lo general al cazarlos para comer. Posteriormente, otras personas se pueden contagiar mediante contacto directo con sangre o secreciones de alguien enfermo, o mediante objetos (como agujas) contaminados por estos fluidos.</p>
						<p>El ébola no se transmite por el aire, tampoco a través de agua o alimentos ni desde una persona infectada sin síntomas. La transmisión requiere contacto estrecho con un enfermo.</p>
						<p>Las condiciones de vida de las personas en los países afectados, la precaria atención de salud y factores culturales explican la diseminación explosiva de la enfermedad que ha ocurrido actualmente en el oeste de África.</p>
						<p>El período de incubación del virus en humanos es de dos a 21 días, es decir, una persona puede contagiarse y no experimentar molestias hasta tres semanas después. Los principales síntomas son fiebre alta de inicio brusco, gran decaimiento, dolor de cabeza y garganta y dos días después de esto, vómitos y diarrea. También produce sangramientos, insuficiencia renal y falla multisistémica en tres a cinco días.</p>
						<p>Se aconseja evitar los viajes no indispensables a Guinea, Liberia y Sierra Leona, donde están experimentando el brote. En Nigeria también han ocurrido casos y existe la posibilidad de que se expanda a otros países vecinos.</p>
						<h2>Recomendaciones de la OMS</h2>
						<ul>
							<li><span>&#8226;</span>Los viajeros deben evitar todo contacto con pacientes infectados.</li>
							<li><span>&#8226;</span>El personal de salud que viaje a las zonas afectadas debe acatar rigurosamente las orientaciones sobre el control de la infección recomendadas por la OMS.</li>
							<li><span>&#8226;</span>Las personas que hayan estado en zonas con casos recientemente notificados deben conocer los síntomas de la infección y solicitar atención médica al primer signo de la enfermedad.</li>
							<li><span>&#8226;</span>Se aconseja a los médicos clínicos que atiendan a viajeros que hayan regresado de zonas afectadas y que presenten síntomas compatibles que consideren la posibilidad de la enfermedad por el virus del ébola.</li>
						</ul>
