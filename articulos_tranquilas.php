					  <p>En una entrevista realizada en el blog <a href="https://mamasrealesblog.com/" target="blank">Mamás Reales</a>, la Lic. en Psicología Cristina Pons responde sobre el puerperio para saber un poco más de esta etapa.</p>
					  <p class="preguntas">¿Qué características tiene el posparto?</p>
					  <p>El posparto es una etapa de mucha vulnerabilidad para la mujer, los cambios hormonales generan diferentes emociones, puede que hasta desconocidas. Muchas veces está instaurada la idea de que la mujer va a sentirse plenamente feliz y a veces la mujer se encuentra sintiendo además de alegría, angustia. Incluso puede tener momentos de llanto y sentirse entreverada.</p>
					  <p class="preguntas">¿Qué sugerencias podés dar a los acompañantes de esa mujer que acaba de parir?</p>
					  <p>Escucharlas, habilitar a que la mujer pueda decir lo que tenga ganas, porque a veces las preguntas que hacemos están direccionadas: “¿Estás contenta? ¿Estás emocionada? ¿Esto es divino ¿no?”. Es más importante preguntarles simplemente cómo se sienten y transmitirles la tranquilidad de que esto va a pasar, sobre todo en ese primer mes de adaptación y entrevero.</p>
             		  <p class="preguntas">¿Y qué hay que hacer puntualmente cuando la mujer cae en depresión posparto?</p>
             		  <p>Hay que diferenciar la depresión del <i>baby blues</i> que a veces se entrevera cuando no estamos en la temática. El <i>baby blues</i> es una adaptación a uno de los cambios más importantes, pero en general se da hasta el primer mes. En la depresión, en general, uno ve que la mujer llora demasiado, a veces no puede hacerse cargo de ella misma ni del bebé. En ese caso es muy importante la consulta con una psicóloga perinatal, porque esto tiene solución.</p>
              		  <p class="preguntas">Por último, ¿qué recomendaciones le podés dar a esa mujer que llega a su casa con un nuevo bebé en sus brazos?</p>
              		  <p>En primer lugar, que entienda que es una etapa difícil de la vida, pero cuando se prepara para esta realidad, el después es mucho mejor. Seguramente sea un primer mes de adaptación en donde va a necesitar ayuda, y está bueno que la acepte y la reciba de brazos abiertos. Una ayuda que te dé una mano con el bebé, que prepare la comida, que te acompañe en el día a día, porque a veces la mujer queda sola en los primeros meses. La persona que va de visita a conocer al bebé no tiene que esperar a que la atiendan sino tener una actitud más activa, ¡ayudar! Y si puede, habilitar a que surjan los sentimientos que tengan que surgir sin hacer juicios de valor.</p>
