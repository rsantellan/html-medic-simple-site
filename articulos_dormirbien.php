						<h1>Dormir bien: </br>el primer paso para una vida saludable</h1>	
						<p>Un descanso adecuado te brinda la energía necesaria para realizar todas tus actividades durante el día, por eso es importante que tu sueño sea reparador. 
 						Si bien no existe un número ideal sobre cuántas horas necesita dormir un individuo, la siguiente tabla es una guía estimativa a tener en cuenta.</p>		
						<ul>
							<li>Recién nacidos(0-2 meses) - 12-18 horas</li>
							<li>Bebés (3-11 meses) - 14-15 horas</li>
							<li>Niños pequeños (1-3 años) - 12-14 horas</li>
							<li>Pre escolares (3-5 años) - 11-13 horas</li>
							<li>Niños en edad escolar (5-10 años) - 10-11 horas</li>
							<li>Adolecentes (10-17 años) - 8.5-9.5 horas</li>
							<li>Adultos - 7-9 horas</li>
						</ul>
						<h2>Consecuencias de un sueño insuficiente</h2>
						<p>Las consecuencias de la falta de sueño son mucho más importantes de lo que se estima.
						Diferentes investigaciones han concluido que aquellas personas que no consiguen un sueño reparador tienen un riesgo más elevado de desarrollar enfermedades crónicas.</p>		
						<p>Considerar al sueño como una prioridad más que un lujo es el paso inicial para prevenir un número de condiciones médicas relacionadas, como ser:</p>
						<ul>
							<li><span>&#8226;</span>Obesidad: diversos estudios han relacionado el sueño insuficiente con la ganancia de peso.</li>
							<li><span>&#8226;</span>Diabetes: la falta de sueño de forma crónica se ha asociado al desarrollo de diabetes tipo II, debido a su influencia en el metabolismo de la glucosa.</li>
							<li><span>&#8226;</span>Enfermedades cardiovasculares e hipertensión: estudios han demostrado que una inadecuada noche de sueño en personas que tienen hipertensión puede causar elevación de la presión arterial al día siguiente.</li>
							<li><span>&#8226;</span>Alteraciones en el humor: una noche sin dormir ocasiona irritabilidad al día siguiente y es concebible que la insuficiencia crónica del sueño puede conducir a alteraciones crónicas del humor.</li>
							<li><span>&#8226;</span>Consumo de alcohol: el consumo de alcohol es más prevalente entre aquellos que tienen un sueño inadecuado.</li>
							<li><span>&#8226;</span>Expectativa de vida: se ha vinculado la reducción de la expectativa de vida y por consiguiente un aumento en la mortalidad entre aquellas personas que duermen menos horas.</li>
						</ul>
						<p>Consejos para mejorar la calidad del sueño:</p>
						<ul>
							<li>Tratar de acostarse y levantarse a la misma hora cada día</li>
							<li>Asegurarse que el dormitorio sea un espacio oscuro, tranquilo y templado (ni muy caliente ni muy frío)</li>
							<li>Confirmar que su cama sea confortable y utilizarla solo para dormir, no para realizar otras actividades como leer, mirar TV o escuchar música. Remover el televisor, computadora, así como cualquier otro dispositivo ruidoso o luminoso de la habitación.</li>
							<li>La actividad física moderada puede ayudar a promover el sueño, sin embargo evite realizarla en las horas cercanas a acostarse.</li>
							<li>Evite comidas copiosas previo a la hora de dormir como bebidas estimulantes o infusiones (café, mate).</li>
						</ul>
						<p>Fuente: Centers for Disease Control and Prevention. Harvard Medical School</p>
