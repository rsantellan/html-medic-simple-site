				    <h1>Entendiendo a los niños 
				      con trastornos de aprendizaje</h1>	
					  <p><strong>Basado en la exposición del Dr. Guillermo Agosta – Neurólogo Pediatra – Hospital Italiano de Buenos Aires.</strong></p>
					  <p>Los trastornos específicos del desarrollo de aprendizaje escolar constituyen un conjunto de problemas que interfieren significativamente en el rendimiento en la escuela, dificultando el adecuado progreso del niño y la consecución de las metas marcadas en los distintos planes educativos.</p>
					  <p>El trastorno de aprendizaje es algo distinto al espectro autista o a la discapacidad intelectual. Un niño con trastorno tiene un nivel intelectual normal, pero le va mal en el colegio, puede existir aisladamente o coexistir con otros trastornos teniendo o no relación.</p>
					  <p><strong>Tiene como eje fundamental una serie de síntomas agrupados entorno a tres áreas concretas:</strong></p>
					  <ul>
					    <li><span>&#8226;</span>Inatención<br>
					    </li>
                        <li><span>&#8226;</span>Impulsividad<br>
					    </li>
                        <li><span>&#8226;</span>Hiperactividad</li>
					  </ul>
					  <p>Existen dos tipos de atención: la selectiva y la sostenida. La primera hace referencia a la habilidad para centrarse en la información relevante de la tarea que se pretende efectuar e ignorar otros estímulos irrelevantes próximos. La segunda es el mantenimiento de la atención durante todo el tiempo.<br>
				      Los niños con inatención son aquellos que evaden tareas que requieran un gran esfuerzo mental. Se caracterizan por ser desordenados, hipoactivos, tienen menos velocidad de procesamiento, se aburren, pierden cosas, tienen dificultad para organizar tareas o concluirlas a tiempo.</p>
					  <p>La impulsividad es el síntoma más persistente a medida que los niños con trastorno de desarrollo crecen y el que mayor problema puede producirle, en especial en la relación con sus compañeros.</p>
					  <p>La hiperactividad no solo se caracteriza por su elevada energía sino también por una falta de autocontrol en la realización de actividades potencialmente peligrosas para el niño o molestas para los demás.</p>
					  <p>Actualmente hay diversas hipótesis de los factores que pueden ser las posibles causas del T.D.A.H, es decir que un caso particular puede ser causado por un factor distinto al que ha producido otro, o dentro de un mismo caso clínico haber diferentes factores desencadenantes en diferente grado.</p>
					  <p>Dentro de los mismos pueden encontrarse los ambientales y adquiridos, como por ejemplo que las mujeres que cometen excesos o abusos de alcohol en el embarazo pueden aumentar el riesgo de niños con hiperactividad, impulsividad, desatención y anomalías físicas.</p>
					  <p>También están los factores genéticos que son los de mayor relevancia en la explicación del trastorno o los factores sociales y familiares.<br>
				      </p>
<p><strong>Algunos mitos sobre la T.D.A.H son:</strong></p>
<ul>
  <li><span>&#8226;</span>Afecta solo a niños</li>
   <li><span>&#8226;</span>Desaparece en la adolescencia</li>
  
  <li><span>&#8226;</span>Es un trastorno de la sociedad moderna</li>
  
  <li><span>&#8226;</span>Los niños son todos inquietos y distraídos</li>
  
  <li><span>&#8226;</span>No tiene T.D.A.H. porque presta atención a lo que le interesa</li>
  <li><span>&#8226;</span>No tiene T.D.A.H. porque el padre o madre eran iguales de niños</li>
  <li><span>&#8226;</span>No tiene T.D.A.H. porque es inteligente</li>
  <li><span>&#8226;</span>Es un trastorno que solo afecta el aprendizaje</li>
  <li>El tratamiento incluye orientación a los padres, rehabilitación neurocognitiva y abordaje farmacológico. Se los ayuda fijando horarios y rutinas, poniendo reglas claras, cortas y sencillas, premiando los logros y dividiendo las tareas.</li>
  
	                  </ul>
