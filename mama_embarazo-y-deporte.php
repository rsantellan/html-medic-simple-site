						<h1>Embarazo y deporte</h1>	
						<p>El embarazo es una etapa inolvidable en la vida de una mujer. Si tenemos en cuenta que la práctica habitual de deporte es siempre saludable, es bueno prestar atención a cómo afecta en el embarazo y viceversa. Para ello hablamos con el <strong>Prof. Dr. José Enrique Pons</strong> de MP, Medicina Personalizada.</p>
						<p><strong>¿Se puede practicar deporte durante el embarazo?</strong></br>Sí, se puede, aunque deben respetarse algunas precauciones para que la actividad física resulte beneficiosa y no signifique riesgos.</p>
						<p><strong>Acorde a la etapa del embarazo, ¿cuáles son los ejercicios más aconsejables? ¿Por qué?</strong></br>En términos generales, puede dividirse el embarazo en tres grandes etapas:</p>
						<p><strong>1)</strong> Durante la primera etapa es prudente no realizar actividad física exigente. Se pueden realizar ejercicio aeróbicos de bajo impacto, específicos para embarazadas. La natación y los ejercicios aeróbicos acuáticos son adecuados, pero evitando que el agua este mas caliente que la temperatura de la piel. El ciclismo es apropiado, pero evitando realizarlo en sitios de transito complicado, o subiendo cuestas. Las caminatas son excelentes.</p>
						<p><strong>2)</strong> La segunda etapa suele ser la mas “tranquila”. En general en ese momento ya se sabe que todo se desarrolla  sin grandes inconvenientes, la mujer se siente bien y por lo tanto es posible realizar ejercicios sistemáticamente. </p>
						<p><strong>3)</strong> La última etapa suele ser mas molesta, el vientre puede pesar bastante, los movimientos pueden estar entorpecidos por modificación del centro de gravedad del cuerpo, lo cual hace que lo que hasta ese momento se realizaba con destreza, resulte menos controlable. Un buen ejemplo puede ser lo difícil que es mantener el equilibrio en una bicicleta, si se debe realizar un giro más o menos brusco.</p>
						<p><strong>¿Qué tipo de actividad deportiva debe evitarse? ¿Por qué?</strong></br>Es necesario evitar el ejercicio competitivo, una embarazada no debe procurar “establecer records”, o superarse a si misma.  Su organismo está sujeto a una exigencia nueva, tanto física (el ya mencionado cambio del centro de gravedad, el aumento de peso) como funcional (cambios circulatorios y respiratorio).</p>
						<p>Por ejemplo, el Tenis “tranquilo” se puede practicar, evitando los saltos y los estiramientos, en especial en el tercer trimestre, cuando los ligamentos y articulaciones comienzan a ablandarse en preparación para el parto.</p>
						<p>Deben evitarse asimismo las actividades que impliquen contacto físico, posibilidad de golpes sobre el abdomen, o riesgo de caídas, entre ellas de cola, ya que generan “rebotes” sobre el abdomen.</p>
						<p><strong>¿Cuáles son los principales beneficios de realizar actividad física durante el embarazo?</strong></br>La actividad física durante el embarazo tiene idénticos beneficios a los de cualquier otra etapa de la vida, en especial, beneficia la evolución del proceso del parto, haciéndolo más fácil y rápido, y favorece la recuperación post-parto.</p>
						<p><strong>¿A qué señales debe estar atenta una embarazada mientras realiza actividad física?</strong></br>Una buena medida a tener en cuenta es prestar atención a “lo que dice el organismo”, vale decir, si se siente cansancio hay que detenerse sin esperar a que el mismo sea intenso, si aparecen molestias en el bajo vientre es necesario suspender la actividad física y consultar antes de reiniciarla.</p>
						<p><strong>Si la embarazada no realizaba actividad deportiva alguna, ¿es conveniente comenzar durante el embarazo?</strong></br>Sí, es conveniente, ya que los beneficios se pueden obtener aun cuando previamente se llevara una vida sedentaria. Sin embargo, se debe tener prudencia. </p>
						<p>Cuando no se ha desarrollado actividad física previa, será necesario comenzarla en forma lenta y progresiva. </p>
