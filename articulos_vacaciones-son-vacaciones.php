				    <h1>Vacaciones son vacaciones</h1>	
					  <p>Estamos en meses donde muchos de nosotros aprovechamos para pedirnos unos días de licencia y así recargar energía para el año que ya ha comenzado.</p>
					  <p>¿Cómo vivir nuestras vacaciones? Eso depende de nosotros, ya que somos quienes decidiremos cómo sacarle el mejor provecho. </p>
					  <p>Muchas veces, frente a preguntas como a dónde  o con quién nos vamos, o qué vamos a hacer, comenzamos a sentir ansiedad y estrés por creernos obligados a dar una respuesta.</p>
					  <p>Las vacaciones sirven cuando significan un descanso mental, el cual puede comenzar en el momento de elegir el destino y programar nuestros días. Estar de vacaciones es renovarnos emocional, corporal y mentalmente.</p>
					  <p>Lo ideal es que el tiempo de desconexión del trabajo y la rutina sea de tres semanas o, al menos, dos. Eso es lo que demora el cuerpo en olvidar los horarios de la rutina y adaptarse al ritmo biológico propio. </p>
					  <p>Como se trata de un tiempo para cambiar de aire y salir de la rutina, la recomendación es dejar de lado las llamadas y los correos electrónicos relacionados con el trabajo. <br>
					    <br>
					    Estar de vacaciones es sentirse libre. Debemos recordar que lo importante es con quién compartimos las vacaciones y cómo queremos vivirlas, no tanto dónde iremos o qué haremos. Parte de esa libertad se logra no dejando pendientes.</p>
					  <p>Es un buen momento para hacer cosas distintas, aprovechar los días para permitirnos disfrutar un atardecer, una buena siesta, dormir hasta tarde, hacer ejercicio, cocinar algo que nos gusta y compartirlo.</p>
					  <p>Puede ser útil escribir un plan de acción de lo que no queremos dejar de hacer en nuestras vacaciones (leer libros, escuchar música, bailar, desconectar dispositivos móviles, andar en bicicleta).</p>
					  <p>Descansar es necesario para evitar síntomas de estrés como dolor de cabeza, tensión o dolor muscular, dolor de pecho, fatiga, problemas de sueño, ansiedad, inquietud, falta de motivación, irritabilidad o enojo, tristeza, comer en exceso o muy poco, explosiones de ira entre otros.</p>
					  <p>¡A disfrutar de las vacaciones y poner la mente en blanco para comenzar a escribir un nuevo año!</p>
