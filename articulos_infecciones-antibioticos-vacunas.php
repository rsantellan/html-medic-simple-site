				    <h1>Infecciones,
				      antibióticos
				      y vacunas<br>
				      ¿Cómo? ¿Cuándo? ¿Por qué?				    </h1>	
					  <p><strong>Prof. Adj. Dr. Álvaro Galiana - Pediatra, Microbiólogo, Infectólogo</strong></p>
					  <p>Muchas son las enfermedades que motivan la consulta al pediatra. Algunos ejemplos son la faringitis estreptocócica, influenza o gripe, virosis respiratorias en general, infección urinaria, infección genital, diarreas, neumonía, tos convulsa, infecciones de piel, etc.</p>
					  <p>De estas enfermedades existen aquellas que pueden ser tratadas con antibióticos, otras que no sirven los antibióticos y que se pueden prevenir con vacunas y las que no existen ni antibióticos ni posibilidad de prevención por vacunas.</p>
					  <p>Dentro del primer grupo (tratables con antibióticos) tenemos la faringitis estreptocóccica, la infección urinaria, la diarrea algunas veces, la tos convulsa, la neumonía si es bacteriana, ciertas infecciones de la piel y la meningitis bacteriana.</p>
					  <p>Podemos prevenir con vacunas la Influenza (gripe), la infección genital por HPV, el rotavirus, algunas cepas de neumococo productor de neumonía o meningitis, la tos convulsa y algunas variantes de meningitis bacteriana.</p>
					  <p>Entre las consultas más frecuentes a los pediatras o a los servicios de Emergencia están las infecciones respiratorias altas. Suceden con más frecuencia en los meses fríos del año y requieren un tiempo de resolución entre 3 y 10 días. Nos referimos al resfriado común, que puede manifestarse con un poco de fiebre, y cuyo tratamiento está orientado a mejorar los síntomas si estos son molestos, como la fiebre o la congestión nasal.</p>
					  <p>Las complicaciones de las virosis respiratoria alta son poco frecuentes, e incluyen la otitis media, una crisis asmática, sinusitis, o más excepcionalmente la neumonía.</p>
					  <p>Frente a estas complicaciones y viendo que los días pasan y nuestros niños no se curan aparece la pregunta de todo padre ¿con antibiótico no pasará más rápido?</p>
					  <p>No todas estas enfermedades son tratadas del mismo modo y no todas requieren la administración de antibiótico. </p>
					  <p>Debemos consultar al pediatra tratante, realizar los estudios que nos sugieran y confiar en su decisión en cuanto al tratamiento a seguir.</p>
					  <p>El uso de antibiótico, cuando no son necesarios, generan una serie de problemas que pueden traer consecuencias potencialmente graves para el niño que los recibe.</p>
					  <p>Nuestras mucosas (mucosa oral, urinaria, genital, digestiva) normalmente están colonizadas por gérmenes denominados saprofitas que forman lo que llamamos la &quot;flora normal&quot;. Esta flora tiene funciones importantes para evitar infecciones, al evitar la proliferación de gérmenes más virulentos.</p>
					  <p>Usar antibióticos puede alterar la flora normal, permitiendo la proliferación de bacterias potencialmente más peligrosas, alterando el ecosistema y favoreciendo la aparición de otras enfermedades, por ejemplo una diarrea. </p>
					  <p>También pueden favorecer el desarrollo de bacterias resistentes a los mismos, generando la posibilidad que se administren medicamentos no efectivos en enfermedades bacterianas, lo que puede agravar la situación.<br>
					    <br>
					    Los antibióticos no son medicamentos que puedan ser usados para tratar síntomas, como si fueran antifebriles. Tienen una indicación precisa en relación al proceso infeccioso que se está tratando, y de eso dependerá la dosis y los días de tratamiento que se indiquen. Es importante que sean administrados de acuerdo a las indicaciones específicas.</p>
					  <p>En cuanto a las vacunas, muchas veces hay padres que tienen miedo que sean administradas sin un fundamento adecuado: que se inyectan agentes infecciosos, que provocan enfermedades, que no sirven para nada o que van a estar peor después de vacunarse.</p>
					  <p>Las vacunas son, sin duda, &quot;medicamentos especiales&quot; ya que se administran a grandes poblaciones de individuos sanos, en general niños, con el objetivo de evitar la aparición de determinadas enfermedades.</p>
					  <p>Antes de salir al mercado son sometidos a un número muy importante de pruebas y evaluaciones. Una vez que son aprobadas y se inicia su administración en forma general se administran bajo la vigilancia de aparición de los denominados ESAVI (Eventos Adversos Supuestamente Atribuidos a la Vacunación e Inmunización). De acuerdo a su magnitud, estos pueden ser leves o graves.</p>
					  <p>Dentro de los efectos adversos leves está el dolor en el sitio de la inyección, calor, rubor, fiebre, irritabilidad, síntomas inespecíficos, diarrea, cefalea, mialgias. Estos efectos se consideran aceptables cuando ocurren en las primeras 48 a 72 horas posteriores a la administración de la vacuna.</p>
					  <p>En Uruguay tenemos un esquema de vacunación muy completo el cual previene muchas internaciones y muchas enfermedades graves. </p>
