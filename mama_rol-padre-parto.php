						<h1>El rol del padre en el parto</h1>
						<ul class="notas_mama">
							<li>Revista Tu Bebé</li>
							<li>Dr. Gualberto Arribeltz</li>
						</ul>						
						<p>La figura paterna es tan importante como la de la madre en el momento único de la llegada de un hijo. Desde el momento en que se enteran que van a ser padres, es preciso que como esposo y papá te involucres de lleno en la experiencia del embarazo y, más adelante, el parto. El padre no está preparado ni biológica ni psicológicamente para este proceso, sin embargo, la mejor receta es el diálogo entre la pareja. La mujer atraviesa diversos cambios emocionales y físicos producidos por el embarazo, que su pareja no ha experimentado. Por eso es bien importante y necesario que la mujer involucre al papá y le explique cómo se siente, hablar sobre la nueva responsabilidad que tienen en sus manos, lo que les preocupa y que ambos participen de la experiencia. Es necesario que papá se involucre con el bebé aun antes de nacer, esto va a impulsarlo a defender el embarazo, a ayudar y suplir las necesidades de mamá. De ahora en adelante será una relación entre tres personas, en la que cada uno es importante y mediante la cual el bebé se siente amado y querido desde el vientre.</p>
						<p>El padre puede realizar algunas actividades para involucrarse en el embarazo:</p>
						<ul>
							<li><span>&#8226;</span>Acompañar a la pareja al ginecólogo, para compartir el progreso del bebé y entender los cambios físicos que están ocurriendo en la mujer</li>
							<li><span>&#8226;</span>Leer libros o, lo que es mejor, leerlos entre ambos y para orientarse respecto a temas sobre la educación y el cuidado del bebé.</li>
							<li><span>&#8226;</span>Compartir las labores del hogar; recuerda que eras su mayor apoyo.</li>
							<li><span>&#8226;</span>Tomarse fotos donde estén juntos para que cuando crezca perciba que tanto su padre como su madre estaban ansiosos y contentos esperando su llegada.</li>
							<li><span>&#8226;</span>Participar en los preparativos para la llegada del bebé, como arreglarle el cuarto juntos.</li>
							<li><span>&#8226;</span>Matricularse en clases preparatorias para el parto, para que sepan cómo responder al momento del parto y disfrutarlo al máximo.</li>
						</ul>
						<p>En el parto, el objetivo del padre no es mirar el parto, sino acompañar y ayudar a la madre. La mujer en parto necesita un acompañante que entienda lo que está sucediendo, que se integre en el proceso animándola, complaciéndola, practicando diversas técnicas de alivio (relajación, visualización, masaje, cambiar de posición, técnicas para aliviar la tensión en la espalda, entre otros). Esto se logra cuando el acompañante de parto está preparado, tiene el conocimiento necesario para enfrentar el parto con seguridad, fuerza y sin miedo. Todo un trabajo que no permita preocuparse, sino por lo contrario ocuparse en realizarlo de la forma más consciente y activa posible. Es un esfuerzo conjunto.</p>
						<p>Para conocer más del rol del padre en el embarazo y en el parto, conversamos con el ginecólogo Gualberto Arribeltz.</p>
						<p><strong>Siempre se le da más relevancia a la madre, pero el papel del padre también es muy importante en este momento, ¿no?</strong></br>Por supuesto, partiendo de la base que el embarazo es producto de la interacción de ambos, es muy importante que el padre esté involucrado en las etapas del mismo, así como en el parto, el puerperio y la lactancia, entre otras, es necesario que esté involucrado en la relación, eso hará que la madre esté siempre acompañada y afianzará a la pareja para la llegada del bebé.</p>
						<p>Que el padre asita a los controles del embarazo es fundamental porque de esa manera podrá entender todo lo que está viviendo la madre y ayudarla en distintas cosas, como ser en mantener una dieta, controlar las comidas para que mantenga un peso adecuado, ayudarla a que no fume o no tome alcohol, adoptar estas costumbres como regla de la casa.</p>
						<p>También es importante que vaya a clases de parto en las que va a comprender el proceso que vivirán en el momento del parto tanto la madre como él.</p>
						<p><strong>¿El vínculo familiar que se forja en esta etapa también es importante?</strong></br>Claro, aunque el bebé esté en la panza de la madre, el hombre ya lo va conociendo también, lo ve en las ecografías, le hace el seguimiento, ayuda en la casa para su desarrollo.</p>
						<p>Es importante que sea un proceso de tres, a medida que se acerque la fecha de nacimiento, la madre estará más ansiosa, preocupada, y él que conoce a su hijo y cómo viene el proceso podrá calmarla y ayudarla a llegar lo más tranquila posible al momento de estar los tres; un padre que no se ha interiorizado en el desarrollo del embarazo lo más seguro es que sienta más nervios al momento del parto.</p>
						<p><strong>¿Cuánto tiempo hacer que los padres entran en la sala de parto?</strong></br>Es algo de los últimos 10 años aproximadamente, antes era alguno que pedía excepcionalmente y entraba, pero en la actualidad hay una ley que autoriza a que la madre esté acompañada por alguien que ella elija, y habitualmente elige al padre.</p>
						<p>La presencia del padre en la sala es muy importante porque ayuda a calmarla, se dedica a ella, puede hablarle, acariciarla, darle la mano, hacerle masajes en la zona baja de la espalda que alivian las contracciones, puede llamar al médico, él mismo empieza a conocer por el monitor cuando viene una contracción.</p>
						<p>Algo fundamental durante el parto es la complicidad de la pareja, se da una comunicación y un entendimiento entre los dos que es importante. Un vinculo muy fuerte que uno mucho a las parejas, refuerza el núcleo familiar y está comprobado que lo hace más duradero.</p>
						<p><strong>¿El padre puede colaborar durante el parto en sí?</strong></br>Primero que nada ayuda a pujar a la mujer, que es algo fundamental, él puede hacer que el puje dure el tiempo adecuado, le recuerda cómo debe respirar, porque lo han aprendido juntos y de esa manera la ayuda, la acompaña.</p>
						<p>También es muy importante que él corte el cordón umbilical cuando nace el hijo, que es algo que perfectamente puede hacer y crea un gran vínculo con el bebé, que luego se refuerza ya que, generalmente, es el primero en agarrarlo y lo sostiene mientras la madre termina con la expulsión de la placenta, entre otras cosas.</p>
						<p>Puede sacar fotos, retratar los primeros minutos del núcleo familiar, es un momento muy hermoso para todos los presentes pero en especial para los tres involucrados.</p>
						<p>Incluso puede pasar que se sienta mal, simplemente pide para salir, se recompone y vuelve a entrar sin que pase nada.</p>
						<p>En resumen, el rol del padre, bien llevado, de forma activa, es fundamental para el desarrollo del embarazo, colabora en el parto y afianza los vínculos familiares.</p>
						<p>La mayoría de los padres cuentan que es una de las experiencias más impactantes y disfrutables de su vida.  </p>
