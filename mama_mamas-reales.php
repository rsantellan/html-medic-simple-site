					  <h1>Nuestras mamás también son reales</h1>
					  <p>Dentro de nuestro ciclo de charlas Mamá MP, varias veces de la mano de la Lic. en Psicología Cristina Pons tocamos el tema de las emociones en el embarazo y en el puerperio. Entre risas, Cristina nos hace entender que ese ideal que se tiene de la mujer sonriente, siempre feliz con su hijo, maquillada, peinada de peluquería, con su figura recuperada de manera inmediata, no es nada más que eso: un ideal.</p>
					  <p>En esta etapa las emociones están a flor de piel y las hormonas se encargan de hacer que las mamis pasen de la carcajada al llanto desconociéndose ellas mismas. ¡Nada parece estar tan bajo control y bienvenido que así sea!</p>
					  <p>Desde hace unas semanas nos sumamos al blog Mamás Reales donde Carolina Anastasiadis y Federica Cash, periodistas y amigas, comparten lo que es ser mamás. Los invitamos a conocer el blog y disfrutar su contenido. </p>
					  <p>Mientras, les hacemos un adelanto para que vayan entrando en el tema y se sientan identificadas (también vale para los padres) con Carolina.</p>
					  <h2>La hora de las brujas | Carolina Anastasiadis</h2>
					  <p>En la vida de las mamás existe la hora del caos y comienza más o menos a las seis y media de la tarde (con un margen de media hora, que depende del horario de salida de trabajo y, por supuesto, del horario en que esa niñera-empleada-abuela-suegra que cuida al niño SE VA).</p>
					  <p>Hay una hora en los días de las mamás en que se produce la mutación y esa joven (por supuesto que madres treintañeras entramos en esta categorización), que desde las 9 de la mañana era una ejecutiva, hacía llamadas, tenía reuniones, compraba o vendía y era una persona &quot;independiente&quot; y profesional, se pone las pantuflas. En esa hora crítica, su mundo deja de tener exterior para replegarse a su versión más mínima, cuyo tamaño depende del metraje de su casa. Es esa hora en que las mamás tenemos que dejar de hacer, para estar.</p>
					  <p>Una sobrina segunda, de beba, lloraba todos los días entre las 19 y las 19.30 hrs. Sin falta, de lunes a lunes. Sufría de cólicos, pero a esa hora del día se transformaban en retorcijones insoportables para su cuerpito de 5 kilos. Quien estuviera en la casa con ella a esa hora, sabía que esa angelita que hasta las 18.59 era pura risa y amor, un minuto después se transformaba en satán. Pero pasaba. También sabía eso.</p>
					  <p>Cuando tu hijo anda afiebrado, a las 19 hrs. sube su temperatura a 39º. Es matemático. Cuasi científico. Hay algo que sucede a esa hora que ni la ciencia, ni la metafísica han podido explicar, pero pasa. El mundo de afuera se acelera y las mamás adentro, enmarcadas por la vereda, &quot;atajamos los penales&quot;.</p>
					  <p>Antes de ser mamá, escuché a la editora de una revista renombrada del medio afirmar con vehemencia que los primeros cinco años de su hija, después de las seis de la tarde, ella no pisaba la vereda. O sea,… NO PISABA LA VEREDA. Literalmente. La hora del supermercado, el pago de cuentas o la escapada para comprar el regalito pendiente había dejado de ser la tardecita, para ocupar media hora de su tiempo de almuerzo. Me pareció ridícula su afirmación hasta que… llegó Alfonsina. Y entonces caí en que… es cierto, en la vida de las mamás existe LA HORA DE LAS BRUJAS.</p>
					  <p>Ayer tuve que salir 18.30 de casa a un evento de un cliente. Por supuesto que desde que soy madre, trato de tirar todo para la mañana, mediodía y primeras horas de la tarde, pero, como el mundo no depende del horario de nosotras (hasta que hagamos nuestra revolución), hay cuestiones que quedan fuera de mi rango de alcance. Como la hora de lanzamiento de un producto que no es solo para madres. Y fui.</p>
					  <p>19.35 tenía un whatsapp de mi marido con tres palabras: &quot;x favor vení&quot;. No me dijo más nada, pero lo entendí. Era claro. Las brujas se habían apoderado de casa. Alfonsina de casi tres años había vuelto fundida del club, no se quería bañar, mucho menos comer, y a la vez estaba como una zombie energizada. Por otro lado, estaba Francisca, la beba de 6 meses. Divertida con su hermana mayor pero con sueño. Y aún no sabe que las ganas de dormir se aplacan cuando una cierra los ojitos. Entonces lloraba. Dentro y fuera de casa –porque aunque durante el día es la beba más silenciosa y armoniosa del planeta, cuando tiene sueño, levanta los techos-. Ella sabe que puede hacerlo; de 19 a 19.30. Y esta vez, quien estaba en casa de pantuflas era el papá.<br>
					    Volví 20.15 hs. La beba estaba casi dormida en brazos del padre. La grande en el sillón, por dormirse. La casa tranquila. Las brujas se habían ido, pero habían pasado y pude ver su rastro en los ojos cansados de mi beba; en el abatimiento de la mayor que ahora sí había agotado sus últimas pilas; en la cara del papá, eternamente agradecido por mi llegada. Aliviado, porque es alivio el sentimiento que dejan las brujas a su paso.</p>
					  <p>Ahora que soy mamá, sé que hay una hora en la que el viento sopla, las paredes tiemblan, y los rayos hacen sus últimas descargas. Es una hora difícil en la que, aunque la gente no entienda, el mundo es mi casa. Y tengo que estar.</p>
