				    <h1>Obesidad infantil</h1>	
					  <p><strong>Lic. en Nutrición Patricia Piñeyro</strong></p>
					  <p>En los últimos años se habla cada vez más de obesidad infantil. En Uruguay ha aumentado el número de niños que la padece y es el país de América Latina con mayor prevalencia. La misma se define como una acumulación anormal o excesiva de grasa que produce un riesgo para la salud. Entre los riesgos se encuentran enfermedades cardiovasculares, Diabetes y algunos tipos de cáncer, entre otros.</p>
					  <p>Día a día nos enfrentamos a excesiva publicidad y oferta de productos con alto aporte de azúcares refinados y grasas como ser golosinas, alfajores, galletitas dulces, refrescos y jugos azucarados, embutidos, snacks y algunos alimentos congelados. Estos han logrado ser de preferencia para los niños y terminan siendo los principales causantes del sobrepeso y obesidad, sumado al sedentarismo que viven cada vez más.</p>
					  <p>A su vez, diariamente vemos cómo los adultos quieren complacer los gustos de los niños y terminan optando por este tipo de productos.  Entonces, ¿cómo se hace para lograr un equilibrio entre los gustos de los niños y lo que les hace bien?</p>
					  <p>En el cuidado del niño es importante prevenir estas enfermedades enseñándoles a realizar una buena alimentación en la cual se incluyan todos los grupos de alimentos que son necesarios para el adecuado crecimiento. Entre estos no puede faltar el aporte de frutas, verduras, carnes y lácteos de manera diaria y agua como bebida fundamental para la hidratación. La incorporación de estos alimentos en la vida diaria es lo que les generará hábitos saludables, determinando positivamente la calidad de vida. </p>
					  <p>La buena alimentación comienza en casa, por lo que se recomienda ofrecer alimentos caseros y hacerlos partícipes en la elaboración de los mismos. Educarlos en que puedan hacer una correcta elección de los alimentos.</p>
					  <p>Negarle a un niño alimentos de su preferencia no significa que no los queremos, sino por el contrario, cuanto más los queremos, más los cuidamos. Lo ideal sería que comience a preferir alimentos saludables y mantener una actividad física acorde a su edad.</p>
