						<h1>Lactancia materna</h1>
						<p>Naturalmente la mayoría de las mujeres sienten el deseo de amamantar a sus hijos. El acto de amamantar resulta placentero para las madres, estimulando de esa manera el apego con su hijo. Esta relación que se establece es un vinculo muy estrecho y facilita a la madre la identificación con su hijo. </p>
						<p>El contacto físico con la madre piel a piel, recibiendo calor, amor y alimento será esencial para su desarrollo. </p>
						<p>La leche de la madre contiene toda el agua, calorías y nutrientes para cubrir las necesidades del niños en sus primeros meses de vida. En el niño alimentado con leche materna son menos frecuentes las infecciones respiratorias y la diarrea, evitando además la aparición de alergias alimentarias. </p>
						<p><strong>Le damos las siguientes recomendaciones para lograr una lactancia exitosa:</strong></p>
						<ul>
							<li><strong>Es conveniente que el niño sea puesto al pecho en la primera hora después del parto </strong> para aprovechar la reactividad del bebé al tener adrenalina circulante y favorecer el apego precoz.</li>
							<li><strong>El niño debe succionar introduciendo totalmente en la boca el pezón y parte de la areola.</strong>Para una buena succión es fundamental que el niño no ingiera otros líquidos (té de yuyos, etc.) que además de innecesarios, disminuyen su aptito y con ello el éxito de la lactancia. </li>
							<li><strong>La posición para amamantar debe ser la más cómoda para la madre y el niño</strong>, particularmente en las primeras mamadas la posición acostada o sentada dependerá de la voluntad de la madre, pero es importante que el bebé pueda mirar a la madre mientras esta lo alimenta. </li>
							<li><strong>Es aconsejable que sea alimentado cada vez que lo requiera y durante el tiempo que lo desee.</strong> En lo primeros días el bebé necesita mamadas más frecuentes y poco a poco irá adquiriendo un ritmo propio. Se debe ofrecer los dos pechos en cada mamada comenzando por el que fue ofrecido último en la lactancia anterior. La succión frecuente estimula la producción de leche. Si el bebé queda satisfecho no importa que sólo tome de un pecho. <strong>Cada binomio madre-hijo tiene su propia dinámica de amamantamiento</strong>. La producción de leche continúa tan efectiva en la noche como durante el día. Puede ocurrir que en los primeros días de amamantamiento se produzca dolor o incluso grietas en los pezones. Para su tratamiento o uso de técnicas correctas puede consultar a nuestra especialista en lactancia telefónicamente o en la Policlínica de Lactancia coordinando a través de nuestro Dpto. de Coordinación (2711 1000 op. 2).</li>
							<li><strong>Es conveniente que luego del amamantamiento se coloque al niño con la cabeza sobre su hombro para favorecer la salida de aire del estómago (provecho).</strong> En oportunidades el eructo se acompaña de expulsión de una cantidad variable de la leche ingerida (regurgitación), lo cual no debe preocupar si el niño se alimenta adecuadamente. En algunas madres la “bajada” de la leche a los pechos demora y el recién nacido puede quedar parcialmente insatisfecho. Sin embrago, ésta es una situación pasajera cuya mejor solución es mantener la alimentación natural exclusiva, ya que la succión del niño constituye el mejor estímulo. Esta situación es más frecuente en el nacimiento por cesárea. Si por cualquier causa es necesario complementar con otro alimento diferente de la leche materna, no deje de consultar a su pediatra.</li>
