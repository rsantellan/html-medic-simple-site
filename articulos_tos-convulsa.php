						<h1>Tos Convulsa</h1>	
						<p>La tos convulsa es una enfermedad infecciosa respiratoria, aguda, altamente contagiosa.</p>
						<p>Puede afectar a todos los grupos etarios; sin embargo existe un claro predominio en la edad pediátrica. En los menores de 1 año se observan los cuadros clínicos más graves, debido fundamentalmente a complicaciones como apnea, convulsiones y lesión cerebral (1 en 500 casos) y neumonía (1 de cada 16 niños).</p>
					  <p>El reservorio es exclusivamente humano. La distribución es mundial, sin predominio geográfico ni racial. El mecanismo de transmisión es respiratorio, a través de la inhalación de gotitas de secreciones respiratorias expelidas por la tos del enfermo o de un portador asintomático. El período de incubación es de 10 días, con un rango de 7 a 21 días.</p>
				      <h2>Se considera caso sospechoso de tos convulsa a toda persona que presente:<br>
				        <br>
				      </h2>
						<ul>
						  
                          <li><span>&#8226;</span>Tos persistente de al menos 14 días de duración.<br>
                          </li>
                          <li><span>&#8226;</span><strong></strong>Tos de cualquier duración que se presente como tos paroxística o acompañando de 1 o más de los siguientes síntomas o signos: estridor o reprise inspiratorio, vómito luego de la tos, apnea, cianosis, convulsiones, antecedente de contacto con caso sospechoso, sin otra causa que lo explique.<br>
					      </li>
                          <li><br>
                            <h2>Ante un caso sospechoso se recomienda:<br>
                              <br>
                            </h2>
                            <ul>
                              <li><span>&#8226;</span>Aislamiento respiratorio<br>
                              </li>
                              <li><span>&#8226;</span><strong></strong>Tratamiento sintomático<br>
                              </li>
                              <li><span>&#8226;</span><strong></strong>Tratamiento antibiótico<br>
                                <br>
                              </li>
                              <li>
                                <h2>Las medidas preventivas son:</h2><br>
                                <strong>Higiénicas </strong><br>
                                  <br>
                                
                                <ul>
                                  <li><span>&#8226;</span>Uso de pañuelos descartables que cubran la boca y nariz al toser o estornudar, luego descartarlo en la basura.<br>
                                  </li>
                                  <li><span>&#8226;</span><strong></strong>Higiene de manos con agua y jabón o con alcohol-gel<br>
                                  </li>
                                  <li><span>&#8226;</span><strong></strong>Mantener a los lactantes, especialmente a los menores de 4 meses, alejados tanto como sea posible, de personas con tos o síntomas respiratorios</li>
                                  <li><span>&#8226;</span><strong></strong>Aplicar medidas estándar y de transmisión por gotitas en los servicios de salud</li>
                                  <li><strong>Vacunación</strong><br>
                                    <br>
                                    Frente al aumento de casos de tos convulsa, el MSP exhorta a cumplir con las medidas de prevención.<br>
                                    <br>
                                    <ul>
                                      <li><span>&#8226;</span>Controlar que la población infantil se vacune de acuerdo al cronograma establecido en el Certificado Nacional de Vacunaciones en las fechas recomendadas: 2, 4, 6 y 15 meses vacuna pentavalente; 12 años vacuna dpaT<br>
                                      </li>
                                      <li><span>&#8226;</span><strong></strong>Desde marzo del corriente año, es obligatoria la vacunación contra la tos convulsa para las embarazadas a partir de las 28 semanas de edad gestacional<br>
                                      </li>
                                      <li><span>&#8226;</span><strong></strong>En puérperas que no han sido vacunadas y en casos prematuros nacidos antes de las 28 semanas de gestación y hasta los 6 meses de edad, cuya madre no haya sido vacunada durante el embarazo se recomienda promover la vacunación con una dosis de dpaT antes del alta de la maternidad al padre y madre</li>
