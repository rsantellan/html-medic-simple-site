						<p>Ha llegado la primavera. Después de idas y venidas (o mejor dicho de subidas y bajadas) en la columna de mercurio de los termómetros, parece que se ha estabilizado el calor. Además, los días son más largos por la magia del cambio de horas. Las actividades al aire libre nos tientan y sacar a nuestros pequeños a tomar sol y aire parece casi una obligación.</p>
						<p>Pero la primavera tiene, además de lo bueno y lo romántico, fama de ser el mes de las alergias, de las crisis de asma, de las conjuntivitis. </br>Algo cierto hay de todo esto. Es un viejo concepto, presente en el imaginario colectivo, de que el frío enferma. Esto no es así. Si así fuera, los esquimales vivirían enfermos, cosa que no sucede. Lo que enferma, o mejor dicho favorece las enfermedades respiratorias, es el clima variable, los cambios de temperatura en el mismo día, las pasadas de calor a fresco, o a frío, sin que el cuerpo pueda ajustarse a esos cambio.</br>La primavera y el otoño son los periodos del año en que esto se da en su máxima expresión. (También es cierto que los cambios climáticos han transformado a nuestro año en una primavera-otoño casi continua, sin estaciones definidas, pero eso es otro tema).</p>
						<p>Me gustaría dar mi opinión sobre las alergias. En realidad, la alergia es un mecanismo normal en el cuerpo y es la forma, o una de las formas más importantes, que tiene el organismo para reconocer agentes nocivos y defenderse de ellos. De hecho, las vacunas se basan en ese mecanismo. Creo una pequeña enfermedad controlada (usando gérmenes muertos o atenuados), para que el organismo los reconozca y la próxima vez que se enfrente a ellos, los pueda combatir. Esto es claro. De igual manera, las infecciones respiratorias, tan frecuentes en la infancia, pasan por un proceso similar: enfermo, creo anticuerpos y me defiendo la próxima vez. Las variaciones que los virus tienen mutando, o sea cambiando su apariencia, hacen que la eficacia de ese mecanismo no sea 100% eficiente. Cada año nos resfriamos. Pero es algo de notoriedad y de simple observación que los seres humanos van enfermando cada vez menos, a medida que pasan los años.</p>
						<p>Entonces, ¿qué es la alergia enfermedad? ¿Qué es eso que nos hace consultar a especialistas, sufrir y padecer?</p>
						<p>Pues es una respuesta exagerada del mecanismo normal o fisiológico. Lo explico de manera simple: si detecto contrabando por uno de las fronteras del país, mando refuerzo de funcionarios, policía, militares, etc. y decomiso todo lo que sea ilegal, incluso mando preso a los culpables.</br>Es lo que hace el organismo: cuando detecta un antígeno, o sea un enemigo, manda más sangre a la zona, más leucocitos, inflama los ganglios de la zona, genera más mucosidad para “atrapar” a esos enemigos, o sea: genera una respuesta local y regional.</p>
						<p>Pero, ¿qué pasaría, en el caso de la aduana, si el funcionario a cargo empezara, exageradamente, a poner en marcha todo ese proceso militar y civil frente, por ejemplo, a un Garoto o a un par de barras de chocolate? La respuesta sería la misma que frente a un enemigo, pero desproporcionada frente a lo que la causó.</p>
						<p>De la misma manera, si el organismo reconoce como enemigo a algo que no lo es (un alérgeno y no un antígeno), va a responder de manera máxima y desproporcionada.</p>
						<p>Que el niño sea o no alérgico, es como tener los ojos azules o negros. Lo que podemos hacer es ayudarlo a moderar ese mal carácter de sus mecanismos de defensa, y por lo tanto, que pase mejor.</p>
						<p>También es cierto que con el tiempo ese mágico bálsamo que cura o atenúa tantas cosas, el cuerpo va aprendiendo y va moldeando sus respuestas.</p>
						<p>El periodo de máxima expresión de estas “patologías” es, por lo tanto, la infancia, periodo donde estamos continuamente en contacto con agentes, real o posiblemente, agresivos.  </p>
						<p>Y aquí volvemos a la primavera. La polinización, la pelusa de los árboles, las floraciones, la mayor estancia al aire libre, el polvo, los cambios de temperatura, hacen que los resfríos, las conjuntivitis, los broncoespasmos, sean cosa de todos los días y aquellos que tengan una predisposición mayor a estos mecanismos exagerados de respuesta, los tengan más manifiestos ahora. </p>
						<p>Que esto sea así, no exime de consultar con el pediatra ni de tomar las medidas que sean necesarias. Pero saber que en el fondo es un mecanismo fisiológico, exagerado sí, pero fisiológico, nos asustará menos y nos evitará tomar antibióticos innecesarios.</p>
