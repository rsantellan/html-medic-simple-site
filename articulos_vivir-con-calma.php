				    <h1>Vivir con calma</h1>	
					  <p>La calma hace referencia a un estado mental en el que predomina la paz, la mente está quieta y serena. Este estado no depende solo de las circunstancias, sino que también se ve asociado a los niveles de reactividad y modelos familiares y sociales de respuesta al entorno de cada individuo.</p>
<p>No es condición necesaria para sentirse en calma una vida de descanso y de relax, si bien estas circunstancias facilitan, es posible entrenar la mente para enfrentar la vida con un foco que ordene, serene, temple, pause y neutralice las reacciones.</p>
					  <p>Vivir con calma enriquece la salud mental, especialmente en nuestros tiempos donde estamos rodeados de presiones, exigencias y apuros.</p>
					  <p>					    Este estado favorece a la biología y a la salud en general pues se relaciona con la liberación de hormonas que aumentan la inmunidad y disminuyen la enfermedad.</p>
					  <p>Los padres ayudan mucho a los niños en el aprendizaje de mantener la calma, son su principal referente y al observar que en diversas situaciones se actúa de un modo sereno y tranquilo colabora en la formación de ellos.</p>
					  <p>El mundo mental y afectivo es dinámico, no se puede permanecer en un solo estado, el movimiento afectivo es sano.</p>
					  <p>Existen situaciones que disminuyen o alteran la calma porque exigen de la persona un menor tiempo de reacción, ya sea por la urgencia que revisten o porque la vida se encuentra en riesgo, sin embargo buscar una respuesta con un poco de calma también puede hacernos más efectivos.</p>
					  <p>Aquellas circunstancias de abuso, violencia, atropello e injusticia son factores que interrumpen la calma.</p>
					  <p>					    Hay personas que son hiperreactivas que frente a pequeños estímulos pierden la calma y su capacidad de juicio disminuye francamente.</p>
					  <p>Las prácticas de meditación; el relacionamiento con gente que nos ayuda a aprender y emular la calma; buscar informarnos por medios que no provoquen &quot;alarmismo&quot;; disfrutar la vida al aire libre; practicar deporte; buscar alguna disciplina que nos relaje como ser la música, la literatura o la pintura; permitirnos no hacer nada y tener tiempo libre para nuestra recreación, pueden ser prácticas que nos ayuden a tener una vida más calma.</p>
