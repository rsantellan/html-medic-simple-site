						<p>Las adenoides, también conocidas como vegetaciones, son un órgano linfoide, parecido a las amígdalas, que está situado en la parte alta de la faringe, tras las fosas nasales, por encima de la úvula o campanilla. Cumplen una función defensiva del organismo frente a las infecciones.</br>Están presentes en todas las personas al nacer. En los primeros años de la vida aumentan de tamaño, estimuladas por las infecciones respiratorias de la infancia, pero a partir de los 6 años de edad tienden a disminuir con el desarrollo del niño. Al llegar a la adolescencia se han atrofiado.</p>
						<p>Cuando sufren una infección, la fiebre suele estar presente y reaccionan inflándose y produciendo moco abundante y espeso. Se adquiere por contagio a través de la tos y los estornudos entre personas que se encuentran próximas y suelen curarse sin secuelas en pocos días.</br>Con las adenoides se segrega mucho moco y es muy frecuente que las niñas y niños se lo traguen. La inflamación y el moco pueden producir sinusitis y otitis.</p>
						<p><strong>¿Qué son las vegetaciones?</strong></br>La mayoría de las infecciones de los niños ingresan al organismo de forma orofaríngea, y la naturaleza prevé un círculo de defensa en esta entrada que son las amígdalas, las más comunes son las dos que vemos en la garganta, pero hay otras atrás de la nariz, atrás de la lengua y otras más hacia abajo; que forman un mecanismo de defensa que contienen, en primera instancia, los microbios que ingresan por esas vías.</p>
						<p>Estas “aduanas” de defensa, por llamarlas de alguna manera, se ponen en alerta, se inflaman cada vez que hay una infección, como en los niños chicos de producen frecuentemente ese tejido se hipertrofia porque aumenta con una infección y no le da el tiempo para retroceder antes de la próximo agresión.</p>
						<p>El ciclo normal de las amígdalas es cuando el bebé nace comienzan a desarrollarse y van creciendo en tamaño a medida que crece el niño hasta que a mitad de la edad escolar comienzan a declinar y  cuando termina la edad escolar ya tienen su tamaño definitivo como las de un adulto, de la misma manera crecen las que están atrás de la nariz que son las vegetaciones. </p>
						<p><strong>¿Cuándo las vegetaciones pasan a ser un problema?</strong></br>Cuando se da una infección, o sea están colonizadas por un microbio por lo que el niño hace fiebre y no se termina de curar, o cuando el volumen de estas amígdalas dificulta, obstruye la respiración normal.</p>
						<p><strong>¿Cómo identificamos que están más grandes de lo debido?  </strong></br>Lo más normal es que el niño ronca de noche, babea la almohada, obstrucción nasal, paladar ojival por lo que no llegan a desarrollar bien y airear su macizo facial.</p>
						<p><strong>¿A qué edad se puede comenzar a detectar?</strong></br>En el entorno de los 2 años, antes de eso no se plantea que pueda haber un problema.</p>
						<p><strong>¿Cómo se determina la operación?</strong></br>A partir de los 2 años que comienzan a manifestarse los síntomas, no hay una edad puntual para realizar la operación, si el niño muestra problemas para respirar correctamente, ronca de noche, babea la almohada, que es lo clásico, tiene otitis y cuadros de garganta frecuentes, por lo que se tejido paso de ser una defensa a un problema. De todas maneras siempre se intenta un tratamiento médico, los otorrinos son expertos y además hacen tratamientos preventivos o terapéutico médico. Si fracasan se pasa a la operación.</p>
						<p>Las amígdalas se tiende a no sacarlas, las vegetaciones si no ayudan a las defensas sí se extraen, pero en el caso de las amígdalas depende se otra cosa.</p>
						<p><strong>La operación es de bajo riesgo, ¿no?</strong></br>Si, bajo riesgo, por lo que no hay que generar una preocupación frente a esto y se debe tener en cuenta que la calidad de vida para el niño mejora muchísimo.</p>
						<p><strong>¿Cuál es el especialista a consultar?</strong></br>En primer lugar es el pediatra, quien le hace el seguimiento al niño y orienta a la familia en estos casos, luego el otorrino que es el especialista, indicará el tratamiento a seguir y eventualmente será quien determine la operación.</p>
