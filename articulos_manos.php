						<h1>La importancia de lavarse las manos</h1>	
						<p>El lavado de manos, como ya es sabido, es la acción eficaz para eliminar microorganismos de fácil transmisión y contagio. </br>Teniendo en cuenta que en el último tiempo se han producido cambios irreversibles como el desarrollo de patógenos multirresistentes, el lavado de manos pasa a ser fundamental no sólo para limpiarlas sino también para evitar resfríos, contagio de virus respiratorios, intestinales (incluyendo la hepatitis A).</p>
						<p>El lavado de manos es un hábito que se forma tanto en la casa como en el colegio y el ejemplo es la mejor herramienta para inculcarlo. </br>Los momentos para practicarlo son antes y después de comer y antes y después de ir al baño.</p>
						<h2>Lavado de manos con agua y jabón</h2>
						<ul>
							<li><span>&#8226;</span>Abrir la llave y humedecer las manos.</li>
							<li><span>&#8226;</span>Aplicar jabón.</li>
							<li><span>&#8226;</span>Frotar las manos.</li>
							<li><span>&#8226;</span>Cubrir completamente las manos con jabón. Lavarlas por 15 segundos por todas sus caras.</li>
							<li><span>&#8226;</span>Enjuagarlas con abundante agua.</li>
							<li><span>&#8226;</span>Secar las manos.</li>
						</ul>						
						<h2>Lavado con alcohol en gel</h2>
						<ul>
							<li><span>&#8226;</span>Aplicar abundante cantidad de solución alcohólica sobre sus manos.</li>
							<li><span>&#8226;</span>Distribuir la solución alcohólica frotando enérgicamente por toda la superficie de las manos.</li>
							<li><span>&#8226;</span>Extender la solución alcohólica entre los dedos y la palma de la mano.</li>
							<li><span>&#8226;</span>Friccionar las yemas de los dedos.</li>
							<li><span>&#8226;</span>Frotar ambos pulgares.</li>
							<li><span>&#8226;</span>Finalizar el lavado de manos refregando las superficies de las manos hasta que se encuentren secas.</li>
