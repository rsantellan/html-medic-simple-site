						<h1>Problemas en la fertilidad de la pareja</h1>
						<ul class="notas_mama">
							<li>Revista Tu Bebé</li>
							<li>Dr. Enrique Pons</li>
						</ul>						
						<p>Muchas veces las parejas buscan tener un hijo y cuando pasa el tiempo y no se logra comienzan a preguntarse a cerca de la fertilidad de alguno de ellos.</p>
						<p>La infertilidad puede ser causada por varios factores, no solo por la mujer sino que también por los hombres en 50% de los casos que se registran en la actualidad. Igualmente, gracias al avance de la ciencia y la tecnología hoy es posible combatirla. A continuación explicamos en líneas generales cada uno de esos factores y su tratamiento.   </p>
						<p><strong>Factor ovulatorio</strong></br>La causa de infertilidad se produce en la mujer por problemas de ovulación, como la ausencia de ovulación u ovulación irregular.</p>
						<p><strong>Factor tubario</strong></br>Las alteraciones de las trompas de Falopio, como obstrucciones o lesiones que puedan estar asociados, provocan que el óvulo no pueda llegar al útero, impidiendo de esta forma la concepción.</p>
						<p><strong>Factor uterino</strong></br>Puede corresponder a malformaciones congénitas del útero (tabiques, útero bicorne) o bien miomas (tumores benignos uterinos que deforman la cavidad del útero). En todos los casos el tratamiento es quirúrgico</p>
						<p><strong>Factor cervical</strong></br>Puede resultar de un cierre insuficiente del cervix (incompetencia ístmico-cervical) o bien a la presencia de un moco que impide el ascenso de los espermatozoides ya sea por factores infecciosos o inmunológicos.</p>
						<p><strong>Factor peritoneal</strong></br>Está relacionado con adherencias que involucran los órganos pélvicos ya sea por cirugías previas o endometriosis (focos de endometrio fuera del útero). En todos los casos el tratamiento es quirúrgico.</p>
						<p><strong>Factor masculino</strong></br>Los factores de infertilidad masculina pueden estar asociados a infecciones, varicocele, obstrucciones y baja producción espermática.</p>
						<p><strong>Infertilidad sin causa aparente</strong></br>Por último, de 5% a 10% de los casos, no se descubre ningún factor, pero aun así no se logra un embarazo espontáneamente. Se lo llama infertilidad sin causa aparente, en estos casos se deben aplicar técnicas de fertilización asistida.</p>
						<p>Para conocer un poco más a cerca de este tema, estuvimos conversando con el Dr. Enrique Pons.</p>
						<p><strong>Tiene que ser difícil ese momento en el que la pareja no logra concebir el hijo, ¿no?</strong></br>Si, genera un sentimiento de frustración el no saber qué está pasando que requiere ayuda.</p>
						<p><strong>¿Con qué frecuencia sucede que las parejas tengan esta dificultad?</strong></br>No es muy frecuente pero tampoco es algo raro, lo que hay que tener en cuenta es que cuando llega una persona y te dice: “Tengo dificultades para quedar embarazada” no se le puede responder: “Es raro”; esa paciente o esa pareja requieren el máximo de compromiso y de ayuda por parte de los profesionales a lo que acuden.</p>
						<p><strong>¿Cuáles son las patologías más frecuentes cuando se dan situaciones como estas?</strong></br>Hay grandes causas que pueden dificultar un embarazo, sobre todo en la mujer ya que hay múltiples lugares en los que pueden presentar problemas, en el cuello o en el interior del útero, en la cavidad de las trompas de Falopio, en el funcionamiento de los ovarios o en el resto del organismo. También en el varón se pueden dar distintos problemas.</p>
						<p><strong>¿Y cuando no se pueden detectar es que comienzan los análisis?</strong></br>Si, se realizan a los dos integrantes de la pareja, ya que son los dos quienes quieren tener un hijo y no pueden, por lo tanto tienen que respaldarse mutuamente, esta es una de las cosas más importantes.</br>Si bien la mujer tiene más posibilidad de tener algún problemas, deben estudiarse los dos, hasta por un tema de ahorrar tiempos, si esperamos a terminar los análisis de uno para comenzar con el otro se pierde tiempo y se genera más nerviosismo en la pareja.</p>
						<p><strong>¿Pueden haber también factores psicológicos en la infertilidad de la pareja?</strong></br>Por supuesto, el caso más claro es cuando después de mucho intentar quedar embarazada, no lo logran y deciden salir de ese ambiente, toman vacaciones y vuelven con un embarazo, esto es algo que se ve frecuentemente.</p>
						<p><strong>¿Actualmente las parejas tienden a esperar más tiempo que antes para concebir, esto puede influir?</strong></br>Si, en algunos casos si, hay funciones que van haciéndose menos precisas a medida que pasan los años, lo que tampoco significa que haya que salir a buscar un embarazo inmediatamente por la edad que se pueda tener, sino irse controlando y saber que todo está bien.</br>Los profesionales podemos llegar a aconsejar a la mujer al detectar que pueden haber algún tipo de enfermedades que a la larga pueden repercutir en la fertilidad, en ese caso el médico debe informarles lo que sucede y decirles que quizás no puedan empezar a pensar en esa posibilidad.</p>
