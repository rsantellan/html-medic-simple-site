						<h2>De 1 a 3 años</h2>
						<p>Los controles se realizarán de forma semestral.</p>
						<p>Recibe leche de vaca y ya está integrado a las comidas de la familia, respetando una dieta equilibrada y variada.</p>
						<p>Continúa creciendo pero a un ritmo más lento. En forma progresiva caminará solo, subirá escaleras gateando y luego utilizará la baranda. Puede agacharse y levantarse del suelo sin ayuda y toma juguetes del suelo. Será capaz de saltar, correr, trepar, lanzar una pelota y pedalear un triciclo. Tiene que ser vigilado por su seguridad pero no debe impedirse que ejercite todas las destrezas que está adquiriendo. Una actitud de este tipo le ayudará a tomar confianza en sí mismo y en los demás.</p>
						<p><strong>En esta etapa adquiere un vocabulario cada vez más rico. </strong></br>Progresivamente utilizará verbos y construirá frases cortas. A los 3 años compone frases largas y comprende órdenes.</p>
						<p>Conoce al menos 2 colores y es capaz de contar hasta 4. </p>
						<p>Utiliza la palabra mío y utiliza el no con lógica. Es necesario estimular su capacidad de expresión conversando con él. Es útil y divertido contarle historias de familia, leerle cuantos y mostrarle fotos. </p>				
						<p>Le gusta trasladar objetos de un lado a otro, poner los objetos en su sitio. De esa manera descubre que tienen su lugar que él también tiene el suyo. Es probable que quiera colaborar en las tareas simples de la casa, que irá perfeccionando. Ayuda a vestirse levantando los brazos. </p>
						<p>Los juegos que predominan siguen siendo los de imitación. En esta etapa los padres deben ser tolerantes brindándole seguridad y estimulando sus iniciativas. </p>
						<p><strong>Debe iniciarse el entrenamiento para control esfínterario alrededor de los 2 años.</strong></br>A menudo el mejor indicador para empezar es la percepción por parte del niño de que se ha ensuciado. Es importante señalar que como todas las adquisiciones de maduración y desarrollo, el momento en que se inicia el control esfínterario es muy variado de un niño a otro, e incluso puede haber avances y retrocesos dentro del proceso normal. </p>
						<p>Les atrae la TV aunque no es aconsejable que miren más de 1 hora por día, haciéndoles compañía y comentando lo que se está viendo. </p>
						<h2>De 3 a 5 años</h2>
						<p>Se controlará cada 1 año salvo que su médico indique la necesidad de verlo más seguido.</p>
						<p>La alimentación debe ser variada y fresca, haciendo hincapié en las frutas y verduras frescas. A esta edad ya come lo mismo que los padres.</p>
						<p>El aumento de peso no debe detenerse, aunque la ganancia de peso es menor que antes. </p>
						<p><strong>Es la edad de la socialización. </strong></br>Comienza a vestirse solo y a comer solo. Adquiere destreza con sus manos, es capaz de dibujar. Camina, corre, salta, sube y baja escaleras. </p>
						<p>A los 3 años ya se expresa correctamente y se integra socialmente. Es explorador e inquieto, por lo que se expone a accidentes. Es capaz de pararse en un solo pie, atrapar y lanzar la pelota. Dado su creciente dominio motriz es recomendable estimularlo a que participe en juegos compartidos, pero de baja competitividad. </p>
						<p>Dibuja círculos y cuadrados. En la etapa preescolar disfruta de juegos compartidos, de la música, distingue entre el día y la noche, ubica << arriba >> y << abajo >>, << afuera >> y << adentro >></p>
						<p>Es un gran conversador disfruta con las charlas y con los cuentos. Conoce su sexo y controla los esfínteres durante el día y a menudo en la noche. Con frecuencia tiende a salirse de los límites en un intento de afirmación. </p>
						<p><strong>Una vida regular y equilibrada sobre todo por los ritmos del sueño y la alimentación son importantes.</strong></p>
						<p>Se recomienda estimularlo a que coma solo, sostenga el vaso con la mano y utilice el cuchillo y el tenedor para alimentarse en una actividad supervisada.</p>
