					  <h1>Esos bichitos molestos: Parásitos</h1>	
						<p>El parasitismo intestinal se presenta cuando una especie vive a expensas de una persona, dentro del tracto intestinal, pudiendo provocar daño.</p>
						<p>Es una enfermedad bastante común en la edad preescolar, entre los dos y seis años, porque los niños acostumbran a jugar con tierra, tienen menos hábitos de lavado de manos y suelen llevar a la boca alimentos sin lavar o cosas que encuentran en el suelo.</p>
						<p>La principal forma de adquirir estos parásitos es a través de la contaminación del agua, de frutas y verduras o de tierra con deposiciones humanas infectadas con sus huevos, o directamente a través de un manipulador de alimentos con malos hábitos higiénicos que lo hagan portador de alguna parasitosis.</p>
						<p>Los síntomas son variados, hay niños que no muestran indicios de la enfermedad mientras que otros sufren cólicos abdominales recurrentes, disminución o ausencia de apetito, detención del progreso en el aumento de peso o peso insuficiente y una serie de trastornos digestivos.</p>
						<p>Los oxiuros son de los parásitos más comunes. Los huevos se diseminan directamente de una persona a otra o se propagan por contacto con ropa de cama, alimentos u otros elementos contaminados con huevos.</p>
						<p>Generalmente los niños se infectan al tocar los huevos de los oxiuros y llevarse luego los dedos a la boca. Los huevos son ingeridos y finalmente eclosionan en el intestino delgado. Los oxiuros maduran en el colon.</p>
						<p>Las hembras de estos gusanos se trasladan al área anal del niño, especialmente en la noche cuando depositan sus huevos, momento en el cual se pueden observar. Pueden causar una intensa picazón en la zona.</p>
						<p>Para diagnosticarlo se realiza, sin previa higiene, una toma de muestra con una espátula adhesiva durante tres mañanas consecutivas, que luego será estudiada por el laboratorio.</p>
						<p>Para controlar los huevos se debe limpiar el baño diariamente, mantener las uñas cortas y limpias, lavar la ropa de cama dos veces por semana y lavar las manos antes de las comidas y luego de usar el sanitario.</p>
						<p>En el caso de la giardia, una vez que los quistes llegan al estómago, salen los trofozoítos que pasan al intestino delgado, donde se pegan a las paredes provocando diarreas y cólicos intestinales. Para detectarlos, se realiza un examen parasitológico, que consiste en juntar muestras de deposiciones día por medio y analizarlas posteriormente en el microscopio.</p>
					  <p>La lombriz solitaria es un gusano aplanado que puede llegar a medir de 10 a 12 metros y se adquiere de comer achuras o carne insuficientemente cocida.</p>
					  <p>Tratar estas infecciones a tiempo permite evitar los molestos síntomas y prevenir consecuencias peores, como la desnutrición o intensos daños abdominales.</p>
						<h2>Las medidas preventivas son:</h2>
                        <ul>
                          <li><span>&#8226;</span>Buen aseo de manos</li>
                          <li><span>&#8226;</span>Evitar comerse las uñas</li>
                          <li><span>&#8226;</span>Consumir alimentos bien lavados y cocidos</li>
                          <li><span>&#8226;</span>Beber siempre agua potable</li>
                          <li><span>&#8226;</span>Alimentarse adecuadamente y en forma balanceada</li>
                          <li><span>&#8226;</span>Mantener la vivienda, los pisos, paredes y los alrededores limpios y secos </li>
                          <li><span>&#8226;</span>Evitar el contacto de las manos y los pies con el barro, tierra o arena de aquellos sitios donde se sabe o se sospecha que hay contaminación fecal </li>
                          <li><span>&#8226;</span>Evitar ingerir alimentos en ventas callejeras y lugares con deficientes condiciones higiénicas </li>
