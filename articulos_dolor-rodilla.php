				    <h1>¿Qué es dolor anterior de rodilla?</br><span>Dr. Gastón Gioscia | Deportólogo</span></h1>	
				    <p>El dolor anterior de la rodilla o dolor patelofemoral (DPF) es uno de los motivos más frecuentes por los que los adolescentes y los adultos jóvenes consultan al Médico. Debido a las causas multifactoriales del DPF, con factores locales, proximales y distales a la rodilla potencialmente implicadas, su tratamiento es particularmente difícil y desafiante.</p>
              		<p>El DPF es el término preferentemente usado para describir el dolor en y alrededor de la rótula. El síntoma principal experimentado por los pacientes con DPF es dolor retropatellar (atrás de la rótula) o peripatellar (alrededor de la rótula). El dolor aparece o se agrava con actividades que cargan la articulación de la rodilla, como subir y bajar las escaleras, ponerse en cuclillas, usar el embrague del automóvil (si la rodilla izquierda está dolorida), usar zapatos de taco alto y sentarse con flexión prolongada de la rodilla. Habitualmente el dolor comienza progresivamente, sin haber existido un antecedente traumático claro. Es más frecuente en la mujer (2,2 veces más) que en el hombre. Los síntomas en ambas rodillas son comunes y pueden pasar de una rodilla a otra con el tiempo.</p>
              		<p>dentificar el área dolorosa y replicar los síntomas. La ubicación del dolor es crucial, porque es capaz de indicar la estructura lesionada, lo cual es realmente útil para diagnosticar y planificar el tratamiento. El déficit de fuerza de cuádriceps (músculo ubicado adelante en el muslo) parece ser un predictor del DPF. Por lo tanto, es muy importante evaluar la fuerza de este grupo muscular. Además existe actualmente una fuerte evidencia de que los pacientes con DPF tienen menos fuerza en los separadores y extensores de cadera. Los pies con un apoyo pronador (es decir con la pisada hacia adentro) también son un factor de riesgo que es importante estudiar. Es necesario también evaluar la flexibilidad de los músculos del muslo, pierna y cadera dado que el DPF se asocia frecuentemente con acortamiento de estas estructuras. </p>
              		<p>Un programa de fisioterapia con ejercicios es eficaz para reducir los síntomas del DPF. Estos deben consistir en un entrenamiento de fuerza de los músculos de la cadera como el glúteo medio (implicado en la separación del muslo) y los cuádriceps. Además, corregir las deficiencias de flexibilidad que puedan existir en los miembros inferiores. Durante la rehabilitación se le indicará al paciente las actividades en las cuales hay mayor presión de contacto en la articulación de la rótula y el fémur, y por lo tanto a evitar, por lo menos durante la persistencia del dolor. También se le enseñarán las actividades con menor presión de contacto, las cuales se aplicaran durante el proceso de rehabilitación. Es importante enseñar al paciente cómo sentarse y ponerse de pie sin usar las manos para levantarse y mantener la cadera, rodillas y pies alineados, sin que la articulación de la rodilla se desplace hacia el medio. Los ejercicios de rehabilitación necesitan ser incorporados fácilmente en la vida del paciente.</p>
              		<p>En conclusión, el DPF es una causa frecuente de consulta por dolor de rodilla. Es importante llegar rápidamente al diagnóstico para poder instaurar un tratamiento adecuado, que en la mayoría de las veces, consiste en fisioterapia y ejercicios de fuerza y flexibilidad. </p>
