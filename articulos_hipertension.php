				    <h1>¿Sabés qué es la hipertensión? ¿Sabés qué es ser hipertenso?</h1>	
					  <p>El 17 de Mayo celebramos el Día Mundial de la Hipertensión Arterial y queremos recordarles qué es, cómo controlarla y qué población tiene mayor riesgo.</p>
					  <p>La hipertensión arterial (HTA) es una enfermedad que se presenta cuando la presión arterial se encuentra alta, o sea, por encima de los valores normales. La presión arterial es una medición de la fuerza ejercida contra las paredes de las arterias a medida que el corazón bombea sangre a través del cuerpo.</p>
					  <p>Se habla de hipertensión arterial (HTA) cuando la presión sistólica (alta) es igual  o superior a 140 mm de mercurio (Hg) y/o la presión diastólica (baja) es igual o superior a 90 mm Hg.</p>
					  <p>Cuánto más alta es la presión arterial, mayor es el riesgo de daño al corazón y a los vasos sanguíneos de órganos principales como el cerebro y los riñones. Si no se controla, la hipertensión puede provocar un infarto del miocardio, un ensanchamiento del corazón y, a la larga, una insuficiencia cardíaca entre otras consecuencias.</p>
					  <p>Se trata de una enfermedad esencialmente asintomática por lo que se la conoce como &quot;el asesino silencioso&quot;.</p>
					  <p>En algunos casos puede manifestarse por dolor de cabeza, zumbido de oídos, trastornos de la visión, mareos o palpitaciones.</p>
					  <p>Todos los adultos deberían controlar su presión arterial y, si está elevada, deben consultar a su médico de cabecera.</p>
					  <p>Si se es hipertenso se debe cuidar el peso; controlar periódicamente la presión arterial; consumo de sal y los alimentos como quesos, fiambres, pan, galletas, comida comprada, bizcochos, sopas en cubos e instantáneas; consultar periódicamente al médico; incluir más de 5 porciones de frutas y verduras al día y leguminosas en la semana; consumir carnes magras y lácteos descremados.</p>
					  <p>Existen casos donde también se necesita tomar medicamentos con prescripción médica.</p>
					  <p>La población con mayor riesgo a desarrollar hipertensión arterial es aquella que tiene antecedentes familiares, adultos mayores, sedentarios, consumidores excesivos de sal, personas con sobrepeso y obesidad y bebedores excesivos de alcohol.</p>
					  <p>Invitamos a todos nuestros socios a prevenir controlándose la presión arterial.</p>
