				    <h1>¿Golosinas otra vez?</h1>	
					  <p>En los cumples infantiles generalmente hay snacks, frankfurters, hamburguesas, golosinas en la mesa, en la piñata y en las sorpresitas...claro que si los chicos consumen estos alimentos esporádicamente, no corren ningún riesgo. El problema es que cuando las generaciones son muy grandes, ¡pueden llegar a tener tres cumples por semana o más!</p>
					  <p>Sumado a esto, en muchos casos, las meriendas que llevan los chicos al cole suelen ser snacks y alimentos poco saludables. Es importante reflexionar y preguntarse sobre qué están comiendo los niños en la semana. ¿Son este tipo de comidas excepciones o por su frecuencia se vuelven rutina?</p>
					  <p><strong>¡Un cumple infantil puede tener otras opciones!</strong><br>
					    Puede parecer más fácil comprar solamente los clásicos de cumpleaños, pero si le ponemos pienso y un ratito de tiempo, podemos servir una mesa diferente e igualmente atractiva para los más chicos.<br>
				      En vez de snacks podemos hacer pop casero y servirlo en un bol de color o en conos de papel. Una divertida propuesta puede ser armar un mini kiosko con alimentos saludables y que lo &quot;atiendan&quot; los hermanos mayores, les encanta! Frutos secos como pistachos, castañas, maní (no frito) sin sal, semillas de girasol (pipas) en paquetitos chicos pueden ser algunos de los alimentos del kiosko. </p>
					  <p><strong>Como &quot;plato principal&quot;</strong><br>
					    Los sandwiches admiten variantes de ingredientes: huevo, atún, carne. También podemos utilizar diferentes tipos de panes como pebete, mini pita, wraps.<br>
					    Las empanaditas de copetín hechas al horno les encantan, rellenas de carne por ejemplo.<br>
				      Las mini pizzas también son una excelente idea, se pueden tener pre hechas y simplemente meterlas en el horno. Algunas opciones para acompañar las mini pizzas pueden ser croquetitas o papas noisette.</p>
					  <p><strong>¡A soplar las velitas!</strong><br>
					    A la hora de soplar las velitas no puede faltar la torta, decorada como pide el homenajeado/a.<br>
					    Para acompañar la torta podemos armar mini brochettes de frutas, bols con cereales, salchichón de chocolate casero, merenguitos, mini muffins de frutas, waffles para que agreguen dulce de leche o chocolate, galletitas caseras o gomitas de gelatina sin azúcar. <br>
					    Si es verano, se pueden ofrecer helados hechos con jugo natural o con licuado de frutas.<br>
				      Es preferible hacer todo en porciones pequeñas y que los chicos se sirvan más de una vez a tener que tirar comida.</p>
					  <p>Algunas lindas ideas para las sorpresitas pueden ser libretas, lápices, drypens, libros, agendas, de esta forma podés evitar las golosinas y reservarlas únicamente para la piñata.</p>
					  <p><strong>Alguien tiene que empezar alguna vez, ¿y si sos vos?</strong></p>
