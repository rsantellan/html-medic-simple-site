						<p>Las angustias que el niño siente durante el día se manifiestan de noche en forma de pesadillas o miedos nocturnos. Las pesadillas son sueños que producen miedo y, por mas que queramos, no podemos evitar que las tengan, pero sí podemos brindarles nuestro apoyo y vigilar la información que reciben durante el día.</p>	
						<p>Las pesadillas aparecen alrededor de los dos años, pero son más habituales entre los tres y los seis.  Son sueños largos que ocurren generalmente en el último tercio de la noche. Mientras hay niños que las tienen muy seguido, algunos nunca tienen pesadillas y otros las sufren durante una etapa y luego desaparecen. Normalmente, tienden a disminuir con el paso del tiempo.</p>
						<p>Pueden presentarse como consecuencia de un cambio radical en su pequeño mundo, algo que los hace sentir desconcertados como un nuevo bebé en la familia, una mudanza, la separación de los padres, el cambio de colegio, situaciones que les causan inseguridad o alguna circunstancia nueva en su vida que pueda causarle estrés, así lo manifieste de día o no.</p>
						<p>Los niños mayores también pueden tener pensamientos de miedo o pesadillas por la noche después de haber oído un cuento que les asustó o haber visto en la tele algo violento que les hubiera perturbado. Monstruos, bichos y otras cosas raras se les aparecen en las noches. </br>El mejor remedio par las pesadillas y los miedos nocturnos es acudir de inmediato a consolar al niño que se sentirá arropado, se calmará y se dormirá en seguida. Cuanto más demoremos en acudir, se sentirá más asustado al encontrarse solo en su cama, a oscuras, escuchando su propio llanto y tardará en volver a dormirse. </br>Si el niño llora pero sigue dormido, cálmalo hasta que deje de llorar y se vuelva a dormir pero sin despertarlo. Generalmente, después de una pesadilla, el niño se despierta consciente y es capaz de contar lo que ha soñado.</p>
						<p><strong>¿A qué edad comienzan los niños a tener pesadillas?</strong></br>Bastante temprano, los sueños es una actividad cerebral que se tiene desde siempre probablemente, solo que uno no tiene conciencia de tenerlo hasta ser más grande. Dentro de lo que es la actividad cerebral nocturna que se expresa a modo de sueños, a veces aparecen pesadillas que en algunos niños son muy pocas y en otros muy frecuentes. </br>Los niños imaginativos, fantasiosos e intensos, suelen tener pesadillas más frecuente a que los normales, quienes suelen recordarla, a veces se despierta asustado y es capaz de contar lo que soñó lo que lo diferencia de los terrores nocturnos.</p>
						<p><strong>¿De qué tratan estos terrores?</strong></br>Los padecen los niños que se despiertan gritando, llorando, con una respiración agitada, pueden estar de ojos abiertos pero no reconocer a los padres; este estado dura unos minutos y luego pasa, el niño no recuerda nada. Es un estado que asusta mayormente a los adultos pero no representa mayor significado.</p>
						<p><strong>¿Qué repercusiones puede tener la pesadilla?</strong></br>Como decíamos al principio, estos sueños lo despierta y lo asusta, pero no debería generar mayor preocupación salvo que se empiece a generar un mal hábito en el niño como ser: “Tuve la pesadilla, llamé, mis papás vinieron y me llevaron a su cama” que el niño se dé cuenta de ese proceso puede ser el principio de un circulo reiterativo simplemente para dormir con los padres u obtener alguna otra cosa.</p>
						<p><strong>¿Cómo se debe manejar la angustia del niño cuando a mitad de la noche viene a contar que tuvo una pesadilla?</strong></br>Hay que calmarlo, devolverle la paz, tratar de entender con él que fue lo que soñó o lo que vio en sueños y buscar la mejor manera para explicarle lo que puede haber sucedido y sacarle ese temor momentáneo, para que pueda volver a su habitación y dormir tranquilo. No es recomendable que se quede a dormir con los padres, ese debe ser el último recurso.</p>
						<p><strong>¿Con qué frecuencia se pueden dar estos sueños?</strong></br>Lo normal es que un niño duerma bien toda la noche y ocasionalmente tenga una pesadilla, si se da todas las noches durante un lapso breve, se la puede asociar a un evento estresante o una preocupación puntual para él, por ejemplo no es raro que cuando empiece guardería tengan pesadillas o hablen dormidos y se los escucha decir cosas de su actividad diurna. Hoy en día los niños están bombardeados en actividades y se los debe proteger hasta de las imágenes que ven en la tele o en un afiche, así como películas de terror o suspenso, le generan una respuesta, que en el día expresa como ansiedad y miedos y en la noche con pesadillas.</p>
