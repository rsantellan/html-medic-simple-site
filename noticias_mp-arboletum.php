				    <h1>Un paseo distinto: Arboretum Lussich</h1>
                        <p>
                            En las vacaciones algunos días más nublados se
prestan para hacer actividades diferentes. El paseo
al Arboretum Lussich resulta atractivo tanto
para grandes como para chicos, debido a las increíbles
vistas que tiene, y los aromas y sonidos
de la naturaleza que nos conectan con nuestro
lado más espiritual.
El Arboretum se encuentra por la calle Antonio
Lussich, por Ruta IB a la altura del km 128, sobre
la Sierra de la Ballena. Su origen se remonta
al año 1896, cuando Antonio Lussich adquirió
terrenos en la zona ubicada al sur de la Laguna
del Sauce. Al año siguiente, emprendió la tarea
de forestar, (tarea que duró 30 años) e introdujo
especies de los cinco continentes.
En 1980 lo que se conoce como el parque quedó
en manos del Estado. Hoy cuenta con 400 especies
exóticas y 70 autóctonas, entre árboles y
arbustos, y es una de las reservas forestales más
importantes reconocidas a nivel mundial.
Es posible recorrer el Arboretum a pie por los
numerosos senderos que posee. En su parte más
alta, a 140 metros de altura, se encuentra una
glorieta donde se puede descansar y disfrutar de
una increíble vista de toda la bahía, del Cerro Pan
de Azúcar y del resto de la sierra.
Para realizar este paseo, recomendamos llevar
agua para mantenerse hidratados y alimentos
prácticos y livianos, utilizar calzado deportivo
cómodo y llevar repelente y protector solar, reaplicando
cuando sea necesario.
El Parque ofrece visitas guiadas y la entrada es gratuita.
