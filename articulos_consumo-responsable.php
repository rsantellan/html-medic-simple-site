				    <h1>Enseñar y vivir el consumo responsable</h1>	
					  <p>Vivimos en un entorno donde el consumo se ha convertido en una de las actividades cotidianas más habituales. Consumir es una necesidad que nos permite a la vez cubrir y satisfacer necesidades de diferentes tipos: alimentación, vestimenta, vivienda, cultura, ocio.</p>
					  <p>¿Consumimos realmente lo que nos hace falta? A partir de esta inquietud es que surge la educación de un consumo más responsable. Es importante educar a nuestros hijos en la correcta utilización del dinero y del consumo.</p>
					  <p>Muchas veces nos encontramos comprando cosas por presiones externas o porque sobreprotegemos a nuestros hijos y cedemos ante sus caprichos.</p>
					  <p>Debemos enseñarles que el verdadero bienestar y la verdadera felicidad se encuentran más allá de la posesión de bienes materiales. </p>
					  <p>Es importante que nuestros hijos sepan lo que cuesta ganar el dinero y cómo administrarlo de manera correcta. Administrar dinero supone enseñarles a distinguir, según criterios rectos y verdaderos, lo que es razonable gastar.</p>
					  <p>Recordemos que debemos dar el ejemplo, si nosotros realizamos gastos innecesarios de la misma forma lo harán ellos.</p>
					  <p>Educar ante el consumo implicará también promover una actitud de austeridad ante la vida ayudando a discernir cuáles son aquellos productos que podremos reducir. Implica ser creativo y buscar alternativas creativas al consumo. Reciclar o construir los objetos puede tener más valor que comprarlos hechos y hasta puede ser más divertido.</p>
					  <p>Vivir y educar el consumo responsable requiere necesariamente el trabajo de la autonomía y el autocontrol. Es un ejercicio del autoconocimiento ayudando a explorar cuáles son las verdaderas necesidades, intereses, deseos de cada uno y también cuáles son las propias debilidades, carencias o dificultades a la hora de controlar las propias actitudes.</p>
