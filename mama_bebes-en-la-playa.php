						<h1>Los bebés en la playa</h1>
						<ul class="notas_mama">
							<li>Revista Tu Bebé</li>
							<li>Martes 24 de Enero de 2011</li>
							<li>Dr. Néstor Macedo</li>
							<li>Dermatólogo</li>
						</ul>
						<p>Nada mejor que estar a la orilla del mar para refrescarse durante el verano. Además de atenuar el calor por la brisa, el mar es relajante y muy estimulante para los niños.  Sin embargo, aunque la playa es buena, no es un destino adecuado para los bebés, sobre todo, si es recién nacido o tiene pocos meses de vida.  Si te gusta pasar todo el día en la playa tomar mucho el sol y bañarte hasta que cae la tarde, y ahora tiene un bebé, la playa no es el lugar más adecuado para llevarle de vacaciones.</p>
						<p>Los bebés menores de 6 meses no pueden ir a la playa porque no se les puede exponer nunca al sol directo.  Los bebés de más de 6 meses tampoco deben ir a la playa, porque aunque estén bajo una sombrilla, el simple reflejo de sol sobre su piel puede provocarles quemaduras de sol, ya que su piel es muy sensible.</p>
						<p>Si el bebé tiene más de 8 meses se puede bajar a la playa, pero dentro de un horario limitado.  Por ejemplo, de 9 a 10 y media de la mañana y por la tarde a partir de las 17 horas.  Así te asegurarás de estar protegiéndolo de los rayos de sol más dañinos y peligrosos, que son los de las horas centrales del día.  A primera hora de mañana y a última de la tarde disfrutarán del frescor de mojar sus pies en el mar, si lo desean, y de sentir el suave tacto de la arena sin ningún peligro para su salud.  Una vez en la playa, no le pierdas de vista por si se lleva algo a la boca.</p>
						<p>Los niños pequeños disfrutan mucho jugando con la arena, pero procura que estén en todo momento bajo una sombrilla.  La arena también refleja las radiaciones. Por esta razón, mantén a tu hijo siempre vestido con una camiseta de algodón y una gorra.  Si tu hijo ha jugado por la mañana en la playa, es conveniente que antes o después de almorzar, el niño descanse y eche una siesta.  Por la tarde, se puede elegir un paseo distinto en lugar de volver a la playa.</p>
						<p>Algunos consejos para quienes optan por la playa para las vacaciones:</p>
						<ul>
							<li><span>&#8226;</span><strong>Crema protectora para bebés y niños.</strong> Elige una crema fotoprotectora especial para la piel de niños y bebé, con un factor de protección superior a 30 y que sea resistente al agua.  Asegúrate de extenderla bien por todo el cuerpo, incluidas la nuca, los pies, manos y orejas, por lo menos media hora antes de ir a la playa.  Una vez allí, no te olvides de renovarla con frecuencia, sobre todo, después del baño.</li>
							<li><span>&#8226;</span><strong>Paseos de verano.</strong> Al salir de paseo con tu hijo, no te olvides de desplegar siempre una sombrilla o un toldo en su sillita para que el bebé vaya a la sombra, y protege su cabeza con un gorrito bien puesto.</li>
							<li><span>&#8226;</span><strong>Tejidos claros y ligeros.</strong> Viste a tu hijo con ropa muy ligera, de algodón, que es transpirable.</li>
							<li><span>&#8226;</span><strong>Agua fresca.</strong> Lleva siempre en la bolsa del bebé un biberón de agua o botellita de agua para tu hijo.  Los niños pequeños se pueden deshidratar fácilmente.  Los más pequeños, los lactantes están hidratados con sólo ofrecerles leche (de pecho o biberón).  Para garantizarles un agua fresquita, una buena idea es llevar un termo o una pequeña nevera portátil.  En ella podrás llevar también algo de fruta y/u otra cosa refrescante.</li>
						</ul>
						<p><strong>¿Los bebés pueden ir a la playa?</strong></br>Si, pero no los más chicos, es muy importante crear conciencia y transmitir conceptos a los chicos para tener una buena foto educación.</br>Los primeros tres o cuatro meses la piel de los bebes es inmadura y vulnerable a los agentes infecciosos, la playa es un foco probable de infecciones virales, bacterianas, debido a la arena fundamentalmente.</p>
						<p>Por otro lado, los dermatólogos no aconsejamos usar cremas fotoprotectoras en los menores de 6 meses, porque la piel en esos primeros meses es como una esponja, absorbe todo lo que se aplica por lo que hay que tener mucho cuidado con las medicaciones tópicas y cremas que se aplican.</p>
						<p><strong>¿Cómo se pueden proteger a los menores de 6 meses?</strong></br>Se debe tratar de mantenerlos a la sombra, en lugares frescos dentro de lo posible y la protección se da con la ropa, mantenerlos vestidos, con remera, gorro sin exponerlos abruptamente al sol.</p>
						<p><strong>¿Y después de los 6 meses?</strong></br>Toda esa conducta y esos hábitos que decimos siempre los médicos, de manejar con prudencia los horarios de exposición al sol, son tremendamente importantes.</br>La vestimenta es un elemento de protección básico, acostumbrarlos a estar siempre con sombrero y remera.  Recomendamos utilizar un factor mayor de 30 y después hay algunos niños que pueden requerir aún más.</p>
						<p><strong>¿Cómo se aplica el protector?</strong></br>El protector solar debe aplicarse media hora antes de exponer al bebé al sol de forma que la piel tenga tiempo de absorber la protección y se debe tener especial cuidado en que todas las zonas que vayan a estar expuestas directamente al sol queden cubiertas.</p>
						<p>Cada dos horas máximo se debe repetir la aplicación, aunque la marca que se elija indique que dura más tiempo.</p>
						<p>Al momento de elegir el fotoprotector, tener en cuenta que sea resistente al agua y a la transpiración del niño, porque más allá de que lo apliquemos reiteradas veces, debe dar la tranquilidad de que no se le va a salir o a correr mientras juegue o se acerque al agua.</p>
