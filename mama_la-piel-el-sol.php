						<h1>La piel y el sol</br>Cuidados básicos - Fotoprotección</h1>
						<p>En los niños y adolescentes es donde se deben profundizar las medidas de fotoprotección y fotoeducación, pues se calcula que un 50–80% de la radiación solar acumulada a lo largo de la vida se recibe en los primeros 18 años.</p>
						<p><strong>Una estrategia adecuada de fotoprotección se debe basar en los siguientes conceptos esenciales: </strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Reducir al mínimo la exposición al sol.</strong> El método más sencillo para disminuir la exposición solar es reducir la actividad al aire libre durante las horas de mayor intensidad de la radiación UVB (10 de la mañana a 4 de la tarde). Las medidas de protección se deben extender a todas las horas del día, pues la radiación UVA es constante a lo largo del mismo. Se ha demostrado que la radiación UVA al igual que la UVB promueve el desarrollo de cáncer de piel y el fotoenvejecimiento. Por lo tanto es imprescindible desterrar el concepto de << sol bueno, sol malo >>. Por otra parte hay que recordar que bastan 10 o 15 minutos de exposición a la radiación para contemplar los efectos beneficiosos del sol (fotosíntesis de vitamina D).</li>
							<li><span>&#8226;</span><strong>Usar indumentaria adecuada.</strong> El uso de ropa y sobreros adecuados que disminuyan el área cutánea expuesta al sol es muy importante. Brindan mejor protección prendas de tejitos naturales, de trama apretada y colores oscuros. No olvidar la fotoprotección ocular con lentes adecuados.  </li>
							<li><span>&#8226;</span><strong>Uso de pantallas o fotoprotectores adecuados.</strong> Es muy importante el uso regular de un fotoprotector o pantalla solar con un factor de protección solar alto (no menor a de 15). Debe renovarse su aplicación a lo largo del día, luego de baños prolongados o sudoración profusa. No olvidar su aplicación los días nublados, pues las nubes impiden el pasaje de rayos infrarrojos, pero no de los ultravioletas. No es recomendable el uso de fotoprotectores en niños menores de 6 meses, por lo que debe extremarse las medidas de protección física. Deben protegerse con ropas adecuadas y mantenerse a la sombra natural.</li>
							<li><span>&#8226;</span><strong>Reacciones alérgicas de fotosensibilidad.</strong> Con la administración de algunos medicamentos pueden producirse reacciones fotoalérgicas. La oportuna consulta al médico puede evitarlos.</li>
							<li><span>&#8226;</span><strong>Tener cuidado con las superficies reflexivas.</strong> La arena, el agua y el concreto reflejan de forma importante los rayos solares. El uso de sombrilla no asegura la protección adecuada.</li>
							<li><span>&#8226;</span><strong>Consultar precozmente al médico en caso de aparición de lesiones pigmentadas o de cambios en las ya existentes.</strong></li>
