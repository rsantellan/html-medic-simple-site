						<h1>Lavado de frutas y verduras</h1>	
						<p>Durante el verano aumenta el consumo de frutas y verduras. Para disfrutarlas y aprovechar su gran poder nutritivo sin contratiempos es importante lavarlas y desinfectarlas adecuadamente.</br>Existen múltiples gérmenes que se trasmiten por esta vía produciendo algunas patologías que se pueden manifestar con un síndrome diarreico, intolerancia digestiva alta e incluso hepatitis.</br>La falta de higiene en la manipulación de alimentos y el riego con aguas contaminadas son los principales factores responsables de estas enfermedades.</br>Por lo general, dicha afección no reviste gravedad y puede ser manejada en el domicilio manteniendo una dieta e higiene adecuadas y una buena hidratación. El control médico se hace necesario cuando aparecen vómitos reiterados, deshidratación, fiebre, sangre, moco o secreciones purulentas en la materia, dolor abdominal intenso así como pigmentación amarillenta de la piel y mucosas. </p>
						<p>Una vez instalado el cuadro se recomienda extremar los cuidados de higiene, una dieta antidiarreica y de protección gástrica, evitando grasas, frituras, lácteos, café, condimentos y bebidas alcohólicas.</br>La rehidratación fraccionada y con líquidos frescos es fundamental en el caso de presentar diarrea o vómitos. Los niños y los adultos mayores se deshidratan más rápido, por lo que hay que prestarles especial atención.</p>
						<h2>Precauciones: </h2>
						<ul>
							<li><span>&#8226;</span>Comprar alimentos regados con agua limpia (leer bien las etiquetas de los productos)</li>
							<li><span>&#8226;</span>Comprar frutas y verduras embolsadas, lavarlas con desinfectantes permitidos y con la dilución correcta.</li>
							<li><span>&#8226;</span>Lavarse las manos con agua y jabón antes y después de manipular alimentos y especial- mente después de ir al baño, cambiar pañales y tocar mascotas.</li>
							<li><span>&#8226;</span>Los principales síntomas de enfermedad por alimentos contaminados son: dolor abdominal, náuseas, vómitos y diarrea. Pueden ir o no acompañados de fiebre.</li>
						</ul>	
						<p>Recuerde consultar a su médico si al cabo de una semana no se ha resuelto el cuadro.</p>
