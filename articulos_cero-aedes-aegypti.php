				    <h1>Cero Aedes Aegypti</h1>	
					  <p>El Aedes Aegypti es el mosquito transmisor del virus del dengue.  Su actividad es generalmente diurna, dentro de las 2 o 3 horas después del alba y las horas de media tarde y crepúsculo.  Sin embargo, cuando el insecto se aloja en el interior de las viviendas está activo de manera uniforme.</p>
					  <p>Todas las personas en las áreas de endemia y que no tienen inmunidad por infecciones previas, están expuestos a hacer la fiebre dengue.</p>
					  <p>Se transmite a partir de un mosquito infectado por alguno de los cuatro virus del dengue. Es necesario que ese mosquito haya picado antes a una persona infectada.</p>
					  <p>Para prevenirlo se deben eliminar lugares donde los mosquitos ponen sus huevos y se reproducen: los recipientes donde se acumula agua. Los huevos del mosquito son colocados en lugares con agua, envases, cubiertas, baldes, floreros, tanques de agua, bebederos de animales, etc. Todos los recipientes deben ser vaciados, tapados o rellenados con arena.</p>
					  <p><strong>Síntomas</strong><br>
					    <br>
				      Los síntomas se desarrollan entre 3 a 14 días después de la picadura.  Generalmente, se inicia con fiebre alta, dolor de cabeza, debilidad general y un intenso dolor muscular, de espalda y articular.</p>
					  <p>Algunas veces se puede dar una erupción cutánea, náuseas y vómitos.</p>
					  <p>En caso de que la fiebre persista, que los síntomas sean muy marcados o que ya se haya tenido dengue se debe consultar al médico de forma inmediata.<br>
					    <br>
					    <strong>Medidas preventivas<br>
					    </strong><br>
					    De vuelta baldes, botellas y demás recipientes, evitando de esta forma la acumulación de agua.</p>
					  <p>Se recomienda el uso de insecticidas de uso doméstico, de la familia de los PIRETROIDES.  Como repelente ambiental utilice preferentemente productos termoevaporables así como espirales.  Coloque estos repelentes en los horarios de actividad del mosquito y, por lo tanto, de mayor riesgo de picadura.</p>
					  <p>Aplique insecticidas sólo cuando haya visto mosquitos adultos en lugares como cortinas, detrás de los muebles, zonas oscuras de la casa o con vegetación.  Evite la presencia de niños y mascotas mientras lo aplica.</p>
