				    <h1>Calor con prevención</h1>	
					  <p>Una de las cosas más lindas que tiene el invierno es llegar a casa y sentir ese &quot;calor de hogar&quot; que nos ayuda a distendernos y empezar a vivir lindos momentos en familia o en compañía de una buena película o un buen libro.</p>
					  <p>Para que este calor sea realmente placentero debemos tomar todas las precauciones y así prevenir una posible intoxicación por monóxido de carbono. Este gas se produce por la combustión incompleta del carbono presente en materiales tales como leña, carbón de leña, gas, kerosene, alcohol, gas oil y nafta. La inhalación e intoxicación se produce porque el monóxido de carbono reemplaza al oxígeno en el torrente sanguíneo, en consecuencia sufren el corazón, el cerebro y el cuerpo.</p>
					  <p>Los síntomas pueden variar de una persona a otra. Tienen mayor riesgo de intoxicación los niños pequeños, los adultos mayores, las personas con enfermedades cardíacas y/o pulmonares y los fumadores.</p>
					  <p>En muchos casos la persona no es consciente de los síntomas. Éstos pueden ser dolor de cabeza, náuseas o vómitos, mareos acompañados de cansancio, letargo o confusión, desmayo o pérdida de conocimiento, alteraciones visuales, convulsiones, estado de coma.<br>
					    Ante los primeros síntomas se debe ventilar el ambiente y recurrir al SEMM (159) informando sobre la sospecha de estar sufriendo intoxicación por monóxido de carbono.</p>
					  <p>Se puede prevenir controlando las instalaciones y el buen funcionamiento de artefactos así como mantener los ambientes bien ventilados. Se deben examinar las salidas al exterior de hornos, calefones, estufas y calderas para asegurarse que están permeables y en buen estado. También es importante comprobar que la llama de estufas y hornallas sea siempre de color azul, si la misma es anaranjada es una mala señal.<br>
