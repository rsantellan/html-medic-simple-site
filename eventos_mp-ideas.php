              <p>El mismo consistió en que niños de 4<sup>to</sup> o 5<sup>to</sup> de primaria pensaran una acción que colaborara en el cuidado de nuestro planeta y que pudiera ser implementada en su colegio.</p>
              <p>Montevideo, resultando ganador el International College (Punta del Este) con una propuesta de reciclar plástico triturándolo a través de una máquina  y obteniendo chips los cuales adquieren formas en moldes que son puestos en horno. De este modo ese plástico que es contaminante pasa a ser nuevamente usado con un nuevo fin como ser platos y vasos para escuelas de la zona.<br/>
                En el mes de octubre, cuando el concurso llegó al final, MP entregó el premio al colegio que consistió en “Un recreo divertido”, un espacio con estaciones de juegos, food trucks, cabina de fotos y como cierre un show musical de Camila Sapin y Pedro Alemany.</p>
