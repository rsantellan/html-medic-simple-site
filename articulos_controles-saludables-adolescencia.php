				    <h1>Controles saludables en la adolescencia</h1>	
					  <p>La adolescencia es un período de intenso desarrollo, no solo físico, sino también emocional e intelectual. Se puede dividir en tres etapas: adolescencia temprana, generalmente entre los 12 y 13 años de edad; adolescencia media, entre los 14 y 16 años de edad y adolescencia tardía entre los 17 y 21 años de edad.</p>
					  <p>La pubertad nos muestra cambios visibles sexualmente como son los senos, el período menstrual, el vello púbico y la barba. Aproximadamente en la adolescencia media, se completa la mayor parte del crecimiento fisiológico de los jóvenes, ya casi tienen su estatura y peso de adultos y tienen la capacidad física de ser padres.</p>
					  <p>En el comienzo de la adolescencia el mundo se percibe como bueno o malo, con razonamientos concretos y rara vez se logra que consideren las consecuencias de su actuar a largo plazo. Finalizando esta etapa muchos jóvenes han llegado a apreciar las sutilezas de las situaciones e ideas y a proyectarse hacia el futuro. Su capacidad de resolver problemas complejos y sentir lo que los demás piensan se ha agudizado considerablemente.<br>
					    Emocionalmente, es una etapa donde se busca la afirmación de la independencia. Empiezan a separarse de &quot;mamá y papá&quot;. La búsqueda de esta autonomía se puede manifestar de distintos modos, por ejemplo se muestran menos afectivos, desean estar más tiempo con sus amigos, tienen un comportamiento polémico, desafían límites. <br>
					    <br>
					    Consideran importante la aceptación de los demás e intentan &quot;encajar&quot; en el grupo y ser aceptados. Empiezan a &quot;probar&quot; cómo se sienten en diferentes apariencias físicas, estilos e identidades.</p>
					  <p>Es relevante frente a tantos cambios realizar un control anual en este grupo etario para acompañar a los jóvenes en los cambios que van experimentando, detectar patologías en el crecimiento, dar las vacunas necesarias, educar en hábitos de vida saludable y aclarar dudas en relación a la sexualidad.</p>
<p><strong>Basado en:</strong> American Academy of Pediatrics <a href="http://HealthyChildren.org" target="_blank">HealthyChildren.org</a></p>
