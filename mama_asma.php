						<h1>Asma, qué es y cómo se trata</h1>
						<ul class="notas_mama">
							<li>Revista Tu Bebé</li>
							<li>Martes 31 de Enero de 2012</li>
							<li>Dra. Catalina Pinchak</li>
							<li>Neumóloga – Pediatra</li>
						</ul>
						<p>El asma es una enfermedad crónica que hace que las vías respiratorias, los tubos que trasladan el aire hacia dentro y fuera de los pulmones, se sensibilicen y se hinchen.</br>Las niños tienen vías respiratorias más pequeñas que los adultos, lo que hace que el asma sea especialmente seria en ellos. Los niños con asma pueden respirar con un silbido, tos, rigidez torácica y dificultad, especialmente temprano en la mañana o en la noche.</br>Es una de las enfermedades crónicas más frecuentes en el ámbito mundial, con una prevalencia entre el 20% y el 30% en las diferentes poblaciones.</p>
						<p><strong>¿Qué es el asma?</strong></br>El asma es un trastorno inflamatorio crónico de las vías aéreas, según los últimos estudios la prevalencia va en aumento en la mayoría de los niños de menor edad. Afortunadamente, puede ser tratado eficientemente en la gran mayoría de los pacientes hasta lograr un control adecuado.</br>Se caracteriza por episodios recurrentes de dificultad para respirar, ruidos respiratorios audibles a distancia, opresión torácica y tos especialmente a la noche.</br>Tiene diferentes desencadenantes: infecciones respiratorias virales, ejercicio, polvo, polen, olores particulares, peluches, estados de ánimo, humo de cigarro, entre otras.</p>
						<p><strong>Cuando el paciente asmático está bien controlado puede:</strong></p>
						<ul>
							<li><span>&#8226;</span>Evitar la presencia de síntomas molestos.</li>
							<li><span>&#8226;</span>Utilizar al mínimo los medicamentos de rescate (son los que se usan para controlar un ataque o crisis de asma).</li>
							<li><span>&#8226;</span>Tener una adecuada tolerancia al ejercicio.</li>
							<li><span>&#8226;</span>Tener una función pulmonar normal o muy cercana a la normalidad.</li>
							<li><span>&#8226;</span>Evitar crisis severas.</li>
						</ul>
						<p><strong>¿A partir de qué edad se pude establecer que el niño es asmático?</strong></br>Por lo general el asma se puede diagnosticar en los primeros años de vida a partir de los síntomas y de la historia del paciente. </br>La tos y la presencia de ruidos respiratorios (sibilancias) episódicos son comunes en este grupo de niños y sus causantes no son necesariamente asmáticos; particularmente los menores de 3 años.</br>Es fundamental realizar un análisis de todos sus síntomas y signos clínicos previos; que factores lo desencadenan, y que medidas físicas, farmacológicas u otras se han tomado para revertir los síntomas. De esa manera se exploran todos los diagnósticos alternativos que puedan explicar los episodios de dificultad respiratoria recurrente. En los niños mayores de 5 años, además se puede tener una herramienta fundamental que es la exploración funcional pulmonar.</p>
						<p><strong>¿Qué tratamiento debe recibir?</strong></br>El objetivo en el cuidado del niño asmático es lograr mantener un adecuado control de las manifestaciones de la enfermedad por periodos prolongados</p>
						<p>Por ello es importante integrar cuatro componentes:</p>
						<ul>
							<li><span>&#8226;</span>Desarrollar una adecuada relación médico-paciente-familia.</li>
							<li><span>&#8226;</span>Identificar y reducir la exposición a los factores de riesgo.</li>
							<li><span>&#8226;</span>Monitoreo constante del control en el paciente asmático.</li>
							<li><span>&#8226;</span>Manejo correcto de las crisis.</li>
						</ul>
						<p>El paciente y su familia deben estar bien informados acerca de los diferentes medicamentos que se utilizan en las crisis, cuando iniciarlo, conocer las dosis adecuadas. El uso de la inhalocámara, la técnica inhalatoria de acurdo a la edad del paciente y el tipo de dispositivo.</br>Debe saber reconocer los síntomas en forma precoz, y la conducta frente a los mismos. Para lograr este objetivo es fundamental tener además de una información verbal, una información escrita con la secuencia de la medicación (broncodilatadora, antiinflamatoria), nombres (,arcas comerciales diferentes que contengan el mismo fármaco), dosis, frecuencia, y qué hacer si el niño no responde al tratamiento.</br>Conocer que existe además la medicación que se denomina “controladora” o “preventiva”, tiene una función muy diferente a la medicación de rescate, y debe ser muy bien explicada por el médico, para poder lograr la adhesión al tratamiento, de esa manera se quiere lograr el objetivo a mediano o largo plazo que es el control del asma.</p>
						<p><strogn>¿Cuáles son las diferencias entre uno y otro?</strong></br>El tratamiento de rescate debe ser utilizado para el alivio de los síntomas según sea necesario. Son fármacos broncodilatadores de acción inmediata, en algunos casos si no mejora se utilizan los antiiflamatorios-corticoides. Se usan por cortos periodos de tiempo.</br>En cambio los medicamentos controladores son los que evitarán la presencia de síntomas o crisis de asma. Se utilizan por períodos prolongados.</p>
						<p><strong>¿Qué deben hacer los padres para evitar que el niño llegue a la crisis?</strong></br>La familia debe involucrarse activamente en el manejo en el manejo del asma para prevenir problemas y permitir al niño tener una vida productiva y físicamente activa.</br>Para lograr este objetivo es imprescindible:</p>
						<ul>
							<li><span>&#8226;</span>Tomar correctamente los medicamentos.</li>
							<li><span>&#8226;</span>Entender la importancia del cumplimiento del tratamiento, conociendo la diferencia entre los medicamentos de rescate y los preventivos.</li>
							<li><span>&#8226;</span>Reconocer los signos de empeoramiento del asma y tomar acción precoz y adecuada.</li>
							<li><span>&#8226;</span>Requerir ayuda médica cuando sea necesaria.</li>
							<li><span>&#8226;</span>Evitar los factores de riesgo conocidos que puedan generar o empeorar las crisis.</li>
							<li><span>&#8226;</span>Realizar la técnica inhalatoria correcta con el dispositivo adecuado.</li>
						</ul>
						<p>¿Qué deben tener en cuenta los padres para realizar correctamente la técnica inhalatoria?</br>Hay que adecuarla sobre todo a la edad del niño y al tipo de dispositivo que tenga en el hogar.</br>Debe ser monitoreado por su médico en cada consulta, evaluando la calidad del material, la válvula, la máscara, o la pieza bucal, y luego de estar seguros que el dispositivo está en buenas condiciones.</br>Monitoreo de la técnica en cada consulta, frente al profesional para evaluar si la misma es correcta o tiene defectos. Es una maniobra sencilla, pero si se realiza en forma incorrecta es una de las causas más frecuentes de falta de respuesta de estos niños.</p>
						<p><strong>¿Cómo se les enseña a los padres y al niño a utilizar los broncodilatadores?</strong></br>Primero se explica a los padres los signos de crisis y se les entrega de un informe escrito sobre los pasos a seguir frente a los distintos signos de crisis y a reconocer los indicadores de mejoría y los de alarma.</br>Es los consultorios se dispone de los diferentes dispositivos a utilizar, y se recomienda los más adecuados para cada niño; luego se hace una demostración práctica frente al niño y familia. </br>Al finalizar la consulta habitualmente se dispone de instrucciones con ilustraciones hecha para padres sobre el uso e higiene de la misma. </p>
