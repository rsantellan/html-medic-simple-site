				    <h1>Fruta: una buena opción para el verano</h1>	
					  <p>En verano las frutas y verduras se convierten en protagonistas de las comidas ya que tienen un alto contenido acuoso en relación a su peso y son alimentos ricos en vitaminas, minerales y fibra.</p>
					  <p><strong>Compartimos con ustedes los aportes nutricionales de las principales frutas de esta estación:</strong></p>
					  <p><strong>Frutilla:</strong> es rica en fibra, vitamina C y tiene una gran capacidad antioxidante. Además, posee muy pocas calorías, por lo que se recomienda en dietas para bajar de peso.</p>
					  <p><strong>Durazno:</strong> es rico en potasio, carotinoides y vitamina C. Tiene alto contenido en hierro y es muy buen diurético y antioxidante. Es de fácil digestión ayudando al hígado a realizar los procesos digestivos.</p>
					  <p><strong>Ciruela:</strong> contiene vitamina E y mucha vitamina A. Tiene alto contenido en hidratos de carbono aportando energía al cuerpo. Es baja en calorías por lo que se integra bien a las dietas de adelgazamiento. Ayuda a regularizar el tránsito intestinal. </p>
					  <p><strong>Pera:</strong> es la fruta más tolerada por el organismo y la menos alergénica. Es muy digestiva cuando está madura. Tiene un alto contenido en hierro y calcio.</p>
					  <p><strong>Sandía:</strong> contiene 91% de agua, 4,5% de hidratos de carbono, 0,5% de proteínas, 0,3% de lípidos y 0,3% de fibra. Es una buena fuente de antioxidantes y vitamina C. </p>
					  <p><strong>Melón:</strong> es rico en vitaminas A, C y E. Además es rico en potasio, magnesio y calcio. Se utiliza como laxante diurético natural por su contenido de agua y fibra.</p>
					  <p>Es importante recordar que el calor produce deterioro microbiano en los alimentos, por lo que aquellos perecederos como lácteos, carnes, huevos, vegetales y frutas deben conservarse bien refrigerados hasta el momento previo al consumo.<br>
