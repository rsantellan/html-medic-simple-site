						<h1>Cuidados del recién nacido al llegar al hogar</h1>
					  <p><strong>Dra. Ana Claudia Lain | Pediatra Neonatóloga</strong></p>
					  <p>Llegar a casa luego del alta con un recién nacido es todo un desafío, por lo que es importante comentarles sobre medidas básicas del cuidado.					  </p>
					  <ul>
					    <li><span>&#8226;</span>La alimentación con pecho materno es lo mejor para el bebé y su madre, por múltiples motivos: su bajo costo, es fácilmente digerible, además de fomentar el apego y proporcionar defensas naturales que le protegen de ciertas enfermedades y alergias.</li>
							<li><span>&#8226;</span>La alimentación a pecho no debe tener un horario estricto, debe ser a demanda, sabiendo que requiere un promedio de 7 a 8 tomas diarias administradas en un lapso aproximado cada 3 horas con una duración variable de 10 a 15 minutos por toma. Es preferible no utilizar suplementos (fórmulas lácteas) que no sean estrictamente necesarios, salvo indicación médica.</li>
							<li><span>&#8226;</span>El número de deposiciones es variable y cambiante en el correr de los días. Al inicio expulsará meconio, luego las materias se hacen más grumosas hasta hacerse líquidas y amarillentas. No se asusten si regurgitan aisladamente.</li>
							<li><span>&#8226;</span>La ropa del recién nacido debe ser cómoda evitando el exceso de abrigo.</li>
							<li><span>&#8226;</span>El aseo diario incluye lavado de genitales con agua y baño parcial con jabón neutro, hasta que se produce la caída del cordón. La caída del mismo se da en un tiempo variable entre los 5 y 15 días de vida. Mientras no se caiga, se debe limpiar con alcohol evitando el contacto con la piel.</li>
                            <li><span>&#8226;</span>El primer baño de inmersión se realizará a las 48 horas de la caída del cordón.</li>
                            <li><span>&#8226;</span>Es aconsejable no usar perfumes ni lociones anti mosquitos en la piel, la prevención de las picadura de mosquitos se realiza mediante uso de tul en la cuna y cochecito.</li>
                            <li><span>&#8226;</span>No podemos cortar las uñas del recién nacido.</li>
                            <li><span>&#8226;</span>El llanto es la forma que tiene el recién nacido para manifestar sus necesidades. Poco a poco aprenderás a conocer sus motivos: hambre, cólicos, exceso de abrigo o necesidad de contacto físico. El mejor consuelo son los brazos de sus padres.</li>
                            <li><span>&#8226;</span>La temperatura normal del recién nacido oscila entre los 36-37 grados centígrados y debe controlarse con termómetro en su axila. La fiebre (temperatura mayor a 38 grados) es un signo de alarma en todo menor de un mes.</li>
                            <li><span>&#8226;</span>La posición al dormir debe ser boca arriba, sin exceso de abrigo. La cuna debe estar libre de almohadas y juguetes, con colchón firme. El cobertor debe ser fino, dejando que los brazos queden fuera del mismo para que los pueda movilizar libremente.</li>
                            <li><span>&#8226;</span>Aunque sea difícil, es aconsejable restringir las visitas y tener una adecuada higiene al manipular al recién nacido y sus objetos.</li>
                            <li><span>&#8226;</span>Es importante evitar la exposición de los niños a un ambiente con humo, y es preciso tener la habitación ventilada y a temperatura agradable debido al aumento y riesgo de las infecciones respiratorias durante el invierno, sabiendo que los recién nacidos son la población más vulnerable y con mayor morbimortalidad.</li>
                            <li><span>&#8226;</span>La mejor manera de prevenir la tos ferina (Pertussis o Tos convulsa) y gripe en recién nacidos es a través de la vacunación de sus contactos, mantenerlos además alejados de toda persona que tenga tos o se encuentre resfriado. Todas las embarazadas y sus parejas deben recibir de rutina la vacuna contra Pertussis y gripe en el tercer trimestre de gestación.</li>
                            <li><span>&#8226;</span>La mejor manera de prevenir accidentes es nunca dejarlo solo sin protección y transportarlo en silla adecuada.</li>
