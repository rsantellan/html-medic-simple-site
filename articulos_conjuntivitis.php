						<h1>Conjuntivitis: qué es, cómo prevenirla y cómo curarla</h1>	
						<p>La conjuntivitis es una inflamación del tejido fino y transparente que cubre el interior del párpado y la parte blanca del ojo. Este tejido es llamado conjuntiva. Provoca que los vasos sanguíneos sean más visibles, por eso el ojo queda de color rojizo.</p>
						<p>También causa hinchazón, picazón, ardor, lagrimeo, mayor sensibilidad a la luz, sensación de tener arena en los ojos, pestañas con lagañas y enrojecimiento de la conjuntiva.</p>
						<p>Las causas pueden ser:</p>
						<ul>
							<li><span>&#8226;</span>Infección bacteriana o viral</li>
							<li><span>&#8226;</span>Alergias</li>
							<li><span>&#8226;</span>Sustancias que causan irritación</li>
							<li><span>&#8226;</span>Productos que se usan para los lentes de contacto, gotas para los ojos o ungüentos</li>
						</ul>
						<p>La conjuntivitis alérgica es muy común en esta época del año ya que es provocada por sustancias alérgenas, como el polen.</p>
						<p>Es difícil predecir quién desarrollará este tipo de conjuntivitis. Las personas con mayor predisposición son aquellos que tengan familiares directos con conjuntivitis o quienes presenten otro tipo de alergia, especialmente rinitis.</p>
						<p>La conjuntivitis viral y bacteriana se contagia fácilmente. Para evitar el contagio es importante que la persona infectada se lave las manos frecuentemente con agua tibia y jabón, evite tocarse los ojos, limpie toda la secreción alrededor del ojo varias veces al día, no comparta el envase de las gotas de ojos, lave y no comparta las fundas de almohadas, sábanas y toallas así como el maquillaje para los ojos. Las piscinas son un lugar común de contagio, por lo tanto deberá evitar meterse en ellas.</p>
						<p>La infección desaparece por si sola en la mayoría de los casos. Sin embargo, en la conjuntivitis bacteriana se necesita de tratamiento con gotas o ungüentos antibióticos.</p>
