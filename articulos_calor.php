						<h1>Recomendaciones ante el calor</h1>	
						<p>El Ministerio de Salud Pública, a través de la Dirección General de la Salud, ha efectuado una serie de recomendaciones a tener en cuenta frente al calor, dado que una elevada temperatura ambiente puede afectar nuestra salud.</p>
						<h2>Las personas más vulnerables son:</h2>
						<ul>
							<li><span>&#8226;</span>Niños, sobre todo los menores de 6 años.</li>
							<li><span>&#8226;</span>Adultos mayores de 65 años.</li>
							<li><span>&#8226;</span>Personas con enfermedades crónicas como hipertensión arterial, diabetes, obesidad, cardiopatías, consumidores de alcohol.</li>
							<li><span>&#8226;</span>Personas con discapacidades.</li>
							<li><span>&#8226;</span>Quienes realizan trabajo de esfuerzo físico intenso al aire libre.</li>
							<li><span>&#8226;</span>Personas que practican deportes al aire libre.</li>
						</ul>
						<h2>Algunos de los síntomas y signos de exceso de calor son:</h2>
						<ul>
							<li><span>&#8226;</span>Calambres</li>
							<li><span>&#8226;</span>Agotamiento</li>
							<li><span>&#8226;</span>Dolor de cabeza</li>
							<li><span>&#8226;</span>Nauseas y/o vómitos</li>
							<li><span>&#8226;</span>Deshidratación: decaimiento intenso, postración, piel seca, boca y/o lengua seca, ojos hundidos, en los niños pequeños llanto sin lágrimas y fontanela (mollera) hundida, sed intensa. Puede presentar presión arterial baja y pulso rápido.</li>
						</ul>
						<h2>Signos y síntomas de golpe de calor</h2>
						<p>En casos extremos se puede llegar al golpe de calor, cuadro grave que se caracteriza por temperatura corporal elevada mayor de 40 grados, piel caliente y roja, inestabilidad  al caminar o estar de pie, mareos, cambios de conducta o nivel de conciencia como delirio, somnolencia, confusión, pudiendo llegar a presentar convulsiones y coma.</p>
						<p>Para protegernos del calor debemos:</p>
						<ul>
							<li><span>&#8226;</span>Evitar actividad física fuera de su casa entre las 11:00 y las 18:00 hs., así como exposición a los rayos solares en la playa.</li>
							<li><span>&#8226;</span>Beba líquidos siempre que sienta sed.</li>
							<li><span>&#8226;</span>Consuma más líquidos de lo habitual aunque no tenga sed.</li>
							<li><span>&#8226;</span>Ofrecer a los niños más líquidos de lo habitual y a los bebés lactancia a demanda.</li>
							<li><span>&#8226;</span>Permanecer en el espacio más fresco de la casa.</li>
							<li><span>&#8226;</span>Evitar comidas copiosas y calientes.</li>
							<li><span>&#8226;</span>Prefiera comidas ligeras en base a verduras y frutas, optando por porciones chicas aunque coma más veces al día.</li>
							<li><span>&#8226;</span>No abusar de las bebidas alcohólicas.</li>
							<li><span>&#8226;</span>Tomar dos o tres duchas refrescantes por día o refrescarse con paños húmedos.</li>
							<li><span>&#8226;</span>Usar ropa holgada, ligera, de colores claros, sombrero, lentes de sol y protector solar.</li>
							<li><span>&#8226;</span>Mantener los alimentos en la heladera y extremar las medidas de higiene.</li>
							<li><span>&#8226;</span>En horarios de mucho calor, caminar por lugares sombreados.</li>
							<li><span>&#8226;</span>Evitar la permanencia de niños, personas mayores y animales dentro del auto, cuando esté estacionado.</li>
							<li><span>&#8226;</span>Si tiene familiares, amigos o vecinos ancianos, con discapacidades o enfermedades crónicas que viven solos o permanecen solos durante varias horas en el hogar, llámelos frecuentemente.</li>
						</ul>
						<h2>Conducta ante síntomas y/o signos de exceso de calor</h2>
						<p>Si usted u otra persona, presenta síntomas de exceso de calor, tome las siguientes medidas:</p>
						<ul>
							<li><span>&#8226;</span>Permanecer en un lugar fresco y sombreado.</li>
							<li><span>&#8226;</span>Refrescarse con paños húmedos y fríos sobre todo en cabeza, cuello, axilas y regiones inguinales o tomar una ducha refrescante.</li>
							<li><span>&#8226;</span>Beber abundante líquido frío. </li>
							<li><span>&#8226;</span>Si con estas medidas no mejora, consultar con SEMM (159) a la brevedad posible.</li>
						</ul>
						<p>En el caso de que usted se encuentre ayudando a una persona con un golpe de calor debe ofrecerle líquidos en el caso de que esté consciente y poner la cabeza más elevada que el cuerpo.  Si está inconsciente, no debe darle líquidos. Colóquela de costado con las piernas flexionadas y refresque con paños húmedos hasta que llegue SEMM.</p>
