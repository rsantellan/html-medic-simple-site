						<h1>Tos convulsa, qué es y cómo se previene</h1>
						<ul class="notas_mama">
							<li>Revista Tu bebé </li>
							<li>Martes 14 de febrero de 2012</li>
							<li>Dr. Fernando Prego</li>
						</ul>
						<p>La tos convulsa o tos ferina es una enfermedad infectocontagiosa producida por una bacteria, <I>Bordetella pertussis</I>, que afecta a personas de todas las edades. Es más grave en los primeros meses de la vida con mayor posibilidad de complicaciones severas e incluso la muerte.</br>Es una antigua enfermedad que en los últimos años tiende a ser más frecuente.</p>
						<p>El paciente comienza con catarro y tos progresiva; alrededor de 10 días después aparecen los accesos característicos de tos llamados quintas, consisten en cinco golpes aproximadamente de tos, de intensidad creciente y que le ocasionan al niño una angustia considerable con imposibilidad de respirar durante el acceso, finalizando con una inhalación desesperada y ruidosa de aire y frecuentemente vómitos. Esta situación puede repetirse 20 a 30 veces en las 24 horas.</p>
						<p>En los bebés menores de tres meses, en lugar de los accesos de tos pueden presentar episodios de ahogo con apneas o interrupción de la respiración y coloración azulada de la piel -cianosis-.</p>
						<p>Las quintas se prolongan alrededor de 6 a 8 semanas y la convalecencia puede ser de varias semanas con remisión gradual de los síntomas.</br>La tos convulsa es muy contagiosa especialmente en los primeros síntomas de tos y catarro antes de la aparición de las quintas, la contagiosidad puede extenderse hasta tres semanas del comienzo.</br>El tratamiento con antibióticos apropiados acorta sensiblemente su contagiosidad a cinco días.</p>
						<p>Para conocer un poco más de esta enfermedad de la que se habla desde hace meses conversamos con el pediatra Fernando Prego.</p>
						<p><strong>¿De qué se trata la tos convulsa?</strong></br>Antes que nada, quisiera aclarar que por más que ahora está como en el tema del día, esta enfermedad es bastante vieja, no es algo que haya surgido ahora. Es una enfermedad infecciosa provocada por una bacteria y es endémica a nivel mundial, o sea, que siempre está presente y hace brotes una vez cada tanto, o sea que cada cierto tiempo hay más casos de lo habitual en determinado lugar.</p>
						<p><strong>¿Qué tan efectiva es la vacuna contra la tos convulsa?</strong></br>La vacuna contra esta enfermedad existe desde el 1940 y es bastante eficaz; aunque tiene la contra de que genera la inmunización total a partir de la tercera dosis; por eso se da una a los 2 meses, a los 4, a los 6, al año y a los 5 años.</p>
						<p>Como la inmunidad que otorga la vacuna es mala en el tiempo y se va perdiendo; por eso se agregar una nueva dosis a los 12 años. De todas maneras en la edad adulta pasa que se puede perder la inmunidad por eso es que a los mayores que trabajan en sectores de riesgo también se los inmuniza.</p>
						<p>Por lo que se debe maximizar los cuidados cuando se está en contacto con los menores de 6 meses, porque el adulto tiene menos riesgo de enfermarse de tos convulsa pero sí puede trasmitirla.</p>
						<p><strong>¿Cuáles son los síntomas de esta enfermedad?</strong></br>La tos convulsa tiene tres etapas bien definidas. La primera que es exactamente igual a un resfrío como el que todos conocen, con mocos, tos, algo de fiebre y dura unos 10 días aproximadamente.</p>
						<p>Luego se pasa a lo que se denomina como periodo de estado, que es el característico, donde se diagnostica y se da una tos característica que viene en accesos, seca, con mucho esfuerzo y termina con una inspiración muy profunda.</p>
						<p>En los bebés es frecuente que hagan una apnea e incluso pueden llegar a convulsivar en el acceso a la tos, por lo que es una tos aparatosa, no como la que se ve comúnmente.  </p>
						<p>La tercera etapa es la convalecencia que es cuando empieza a decaer y va disminuyendo la tos.</p>
						<p><strong>¿Sólo se puede prevenir con la vacuna?</strong></br>En realidad si, la inmunización es lo único que previene realmente de esta enfermedad.</p>
						<p>Como en otras enfermedades infecciosas se pueden tomar otras medidas como pueden ser evitar el hacinamiento, no estar en contacto con gente a la que se sospecha que pueda padecer la convulsa y tratar de identificarla rápidamente, no se puede considerar que todo niño que tosa tiene esta enfermedad, pero sí estar atentos ante los síntomas que pueden aparecer y si el niño pasa varios días con ese resfrío y la tos no se va, consultar al médico para que pueda guiar al padre sobre las medidas a tomar.</p>
