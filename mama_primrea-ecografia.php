						<ul class="notas_mama">
							<li>Revista Tu Bebé</li>
							<li>Martes 28 de febrero de 2012</li>
							<li>Prof. Agdo. Dr. Marcelo De Agostini</li>
						</ul>
						<p>La primera imagen que tendrás de tu bebé será a través de la ecografía. Esta imagen, que por sí misma suele ser poco expresiva, si no fuera por las explicaciones del ecografista, quedará en tu memoria para siempre. Esto no es vivido igual  por todas las futuras mamás. Para unas, la ecografía no hará más que aumentar la angustia del embarazo. Para la mayoría de las mamás, sin embargo, significa la certeza de que el bebé existe: a partir de este momento se intensificará mucho más la comunicación madre-hijo que se había iniciado tenuemente en los primeros días de retraso de la regla.</p>
						<p>Hasta la aparición de la ecografía, la futura mamá iba a la consulta prenatal y ella era el centro de todas las atenciones. El bebé permanecía en el secreto de su cuerpo hasta el día del parto. La ecografía ha modificado por completo nuestra percepción del bebé que va a nacer: la ecografía coloca el bebé en el centro de la escena, exponiéndole a nuestra mirada, a nuestra palabras, a nuestras proyecciones inconscientes positivas o negativas.</p>
						<p>La ecografía nos dejará ver por primera vez a nuestro bebé: su silueta, sus movimientos, cómo succiona su pulgar, cómo abre la boca y deglute, cómo respira.</p>
						<p>Pero la ecografía no lo puede saber todo. Es una exploración extremadamente fiable, sobre todo hecha a expensas de un buen equipo material y en manos expertas. Pero puede dar lugar a todo tipo de angustias y preguntas en los futuros padres.</br>Las ecografías en el primer trimestre del embarazo se realizan a través de ka vagina: por eso las llamamos ecografías transvaginales. Te colocarás en posición ginecológica, como para proceder a una revisión anual, y el ecografista introducirá dentro de tu vagina el objeto que emite ultrasonidos. Tú no percibirás ninguna sensación extraña. Al momento verás aparecer en la pantalla las primeras imágenes de tu bebé: el ecografista te ayudará a interpretarlas mediante sus explicaciones.</p>
						<p><strong>¿Cómo es este primer encuentro visual entre los padres y su bebé</strong></br>La primera ecografía es lo que hace real el embarazo. La pareja pasa de tener un examen de laboratorio o un test con dos rayas que dice positivo a ver directamente el saco gestacional y la idea es ver el embrión.</br>Si ante la falta de menstruación, la embarazada no tiene los síntomas del primer embarazo, mareos, nauseas, aconsejamos hacer la primera ecografía entre la semana seis y la semana siete.</p>
						<p><strong>¿Por qué se hace en ese momento?</strong></br>Es el momento en que no solamente diagnosticamos que si hay un embarazo, que está bien ubicado en el útero, que no es un embarazo ectópico. Confirmamos el número de embriones, si es uno, dos o tres. Confirmamos además la vitalidad de ese embrión y determinamos la edad gestacional del embarazo, que va a servir sobre todo al final si hay dudas del crecimiento del feto, del bebé.    </p>
						<p><strong>¿Siempre se realiza en esas semanas?</strong></br>Generalmente se hace en estas semanas, pero puede hacerse antes. Nosotros, desde la cuarta semana podemos ver el embarazo y confirmar que hay un saco gestacional, pero no vemos el embrión. En ese momento empiezan los temores, las preguntas, las dudas y la incertidumbre de si estará o no el embrión. Por eso, si no hay sangrado, si no hay dolor, ni nada que obligue a hacerlo antes, esperamos a la semana seis. Ahí vemos el embrión, confirmamos que son seis semanas de embarazo y ya vemos la actividad cardiaca.</p>
						<p><strong>¿El momento de sentir los latidos es muy importante?</strong></br>Es un momento muy emotivo para los padres, que no se olvida jamás.</p>
						<p><strong>¿Cuál es la técnica que se utiliza en esta ecografía?</strong></br>En sí la tecnología avanza y nos arrolla. La ecografía transvaginal es un aparatito que se coloca en la vagina con la finalidad de estar directamente contra el útero. Nos evita planos superficiales y nos permite observar mejor lo que adentro del útero. Por eso se trata de hacerla transvaginal en el primer trimestre, si no hay contratiempos. El ginecólogo solicita el estudio que se llama ecografía obstétrica en el primer trimestre y muchas veces aclara, transvaginal. En otros momentos no. Queda de nuestra parte, en conjunto con la embarazada, si quedan el estudio por vía transvaginal. A veces por la vía abdominal no se ve la actividad cardiaca y por la vía vaginal si. Es muy importante determinar que hay actividad cardiaca en esta instancia. </p>
						<p><strong>¿Los papás llegan con mucha ansiedad?</strong></br>Si, pero con el tiempo ya se ha ido superando ese poder y ese problema. Todas las embarazadas saben que en el primer trimestre del embarazo es importante la ecografía transvaginal.</p>
						<p><strong>¿Qué se diagnostica en esta primera ecografía?</strong></br>Primero confirmamos el embarazo, segundo confirmamos que esté en el útero, descartamos el embarazo utópico. Tercero el número de embriones, cuarto la edad gestacional y quinto que esté vivo. Con todos esos elementos nos quedamos muy satisfechos y es el cometido de este estudio, que es el primer estudio, la primera foto del bebé.</p>
