						<p><a name="asiento-auto" class="p_name">Asiento del auto</a></br>Una de las piezas más importantes del equipo de los bebés es el asiento del auto. Nunca es seguro sostener a un bebé o niño en su regazo en un vehículo en movimiento.</p>
						<p>El lugar más seguro para que viajen todos los niños es el asiento trasero.</br>Asegúrese de instalarlo viendo hacia atrás, la cual es la única posición segura para los lactantes en los autos.</p>
						<p>Cuando el bebé alcance el año o pese por lo menos 9 kilos puede cambiar por un asiento más grande y darlo vuelta para que el bebé quede mirando hacia el frente.</p>					
						<p><a name="seguridad-en-la-cuna" class="p_name">Seguridad en la cuna y al dormir</a></br><strong>Al momento de elegir una cuna es bueno tener en cuenta los siguientes puntos:</strong></p>
						<ul>
							<li><span>&#8226;</span>Barrotes laterales separados entre sí por una distancia menor de 6 cm.</li>
							<li><span>&#8226;</span>Paneles de cabecera y piecera deben ser sólidos, sin recortes decorativos.</li>
							<li><span>&#8226;</span>El colchón debe adaptarse perfectamente a la cuna.</li>
							<li><span>&#8226;</span>Revise la cuna en forma periódica para asegurarse que no tenga bordes rasposos, astillas, grietas en la madera.</li>
							<li><span>&#8226;</span>Coloque protectores acolchados en toda la cuna para evitar que el bebé se lastime la cabeza.  Asegúrese de amarrar todos los cordones de los protectores y de que los cordones midan menos de 15 cm. para evitar la estrangulación.</li>
							<li><span>&#8226;</span>Nunca coloque la cuna cerca de cordones colgantes.</li>
							<li><span>&#8226;</span>Cuando elija la ropa de cama para la cuna del bebé, no use almohadas.</li>
							<li><span>&#8226;</span>No ponga animales de peluche en la cuna del bebé.</li>
						</ul>
						<p><a name="dormir" class="p_name">Dormir</a></br>Siempre ponga al bebé boca arriba para que duerma, incluso para las siestas. Ésta es la posición más segura para dormir.</p>
						<p>Pueden existir bebes que por problemas de salud como lo es el Reflujo Gastroesofagico tengan que dormir en una posición “Antireflujo”, levantando cabecera de la cuna o elevando el colchón del bebe aproximadamente a 45° para evitar el regurgite del alimento.</p>
						<p><strong>Para reducir el riesgo del Síndrome de la Muerte Súbita Infantil (SMSI) es bueno tener en cuenta:</strong></p>
						<ul>
							<li><span>&#8226;</span>Amamantar a su bebé.</li>
							<li><span>&#8226;</span>Colocar al bebé hacia los pies de la cuna, remeter el cobertor alrededor del colchón y taparlo sólo hasta la altura de su pecho.</li>
							<li><span>&#8226;</span>No fumar ni exponer al bebé a ambientes con humo.</li>
							<li><span>&#8226;</span>Mantener la temperatura del cuarto a un nivel que sea cómodo, no más caliente de lo normal.</li>
						</ul>
						<p>Algunos bebés que duermen boca arriba pueden presentar una zona plana en la parte posterior de la cabeza.  Esta zona plana desaparecerá cuando el bebé aprenda a sentarse.</p>
						<p><a name="ropa" class="p_name">Ropa</a></br>Cuando compre ropa para el recién nacido evite los botones que puedan ser tragados con facilidad, moños o cordones que puedan ahorcar al bebé.</p>
						<p>Dado que tendrá que cambiar al bebé varias veces al día asegúrese que las prendas sean sencillas y que las pueda quitar con facilidad.</p>
						<p>Busque ropa con broches de presión.</p>
						<p>Para saber qué cantidad de ropa se le debe poner póngale la misma capas de ropa con las cuales usted estaría cómodo.</p>
						<p>Recuerde que la piel de su bebé se quema con facilidad bajo el sol.  Manténgalo en la sombra, coloque bloqueador solar después de que su bebé cumpla los seis meses.</p>
						<p><a name="cambio-panial" class="p_name">Cambio de pañal</a></br><strong>Para que el cambio de pañal sea lo más cómodo para usted y su bebé le recomendamos tener a mano todo lo básico:</strong></p>
						<ul>
							<li><span>&#8226;</span>Pañales: Asegúrese de contar con una provisión adecuada de pañales</li>
							<li><span>&#8226;</span>Toallitas húmedas para bebé o algodones en agua</li>
							<li><span>&#8226;</span>Un lugar donde tirar los pañales</li>
							<li><span>&#8226;</span>Crema para bebé en caso de que existan paspaduras</li>
							<li><span>&#8226;</span>Mesa para cambiar y cambiador</li>
						</ul>
						<p>Cuando vaya a cambiar el pañal use una superficie plana. Para evitar que orine mientras lo cambia le recomendamos poner una gaza cubriendo la parte genital mientras limpia el resto</p>
						<p>Después de quitar el pañal sucio tómese el tiempo para limpiar con cuidado la parte inferior del cuerpo del bebé.</p>
						<p>Sostenga las piernas por los tobillos con una mano mientras lo limpia.</p>
						<p>Utilice algodón con agua tibia o toallitas húmedas para limpiar el área del pañal del bebé.</p>
						<p>Cuando el bebé tenga movimiento intestinal utilice la parte frontal limpia del pañal para quitar la mayor parte de heces.</br>Termine de limpiar con cuidado mediante un trapo o una toallita utilizando un jabón suave si requiere.</p>
						<p>Levante la parte inferior del cuerpo del bebé sujetándolo por los tobillos y deslice el pañal limpio debajo de él.</p>
						<p><a name="banio" class="p_name">El baño</a></br>Hasta que caiga el cordón umbilical se le da al recién nacido baños parciales, tal como nos enseñaron en el sanatorio, con algodón y agua tibia con un poquito de jabón liquido.</br>Luego de su caída tu Pediatra te dirá cuando podrás comenzar baños de inmersión.</p>
						<p>Los primeros baños deben ser suaves, tranquilos.</p>
						<p>Mucha gente le da el baño al bebé antes de la hora de dormir, como ritual relajante y promotor del sueño, otra gente prefiere un horario en que el bebé esté despierto por completo.</br>Primero ten presente que la temperatura del ambiente sea la adecuada, sin corrientes de aire
Coloca todos los implementos necesarios para que en el momento de bañarlo no te falte nada ya sea jabón , toalla, pañales, vestimenta para tu bebe.</br>Antes de llenar la tina o recipiente pruebe la temperatura del agua con su codo o muñeca. El agua debe sentirse tibia, no caliente.  </p>
						<p>Desvista al bebé, quitando el pañal al final y limpiando la parte de abajo del cuerpo si está sucio antes de meterlo al agua.</br>Utilice una mano para sostener la cabeza del bebé y la otra para introducirlo al agua.</p>
						<p>Cuando coloque jabón en la cabeza coloque su mano sobre su frente de manera que la espuma corra por los lados y no sobre los ojos.</p>
						<p>Tenga cuidado al manejar a su infante mientras está mojado y resbaloso.</p>
						<p><a name="cuidado-unias" class="p_name">Cuidado de las uñas</a></br>Corte las uñas después del baño.  Espere que su bebé este dormido o pida a otra persona que sujete a su bebé mientras usted le corta las uñas. Corte las uñas en forma recta y manténgalas siempre cortas.</p>
