						<h1>Sigamos siendo un país libre de Dengue</h1>	
						<p>El dengue es un tema repetido en nuestros veranos, pero nunca está de más el recordar qué es y cómo podemos prevenirlo.</p>
						<p>El dengue se transmite a través de una picadura de un mosquito de la especie Aedes infectado por alguno de los cuatro virus del dengue. Para transmitir la enfermedad, el Aedes Aegypti tiene que haber picado antes a una persona infectada.<br>
						  Para prevenirlo se deben eliminar lugares donde los mosquitos ponen sus huevos y se reproducen; los recipientes donde se acumula agua. Los huevos del mosquito son colocados en lugares con agua, envases, cubiertas, baldes, floreros, tanques de agua, bebederos de animales, etc. Todos los recipientes deben ser vaciados, tapados o rellenados con arena.</p>
						<p>Se recomienda el uso de insecticidas de uso doméstico, de la familia de los PIRETROIDES. Como repelente ambiental utilice preferentemente productos termoevaporables así como espirales. Coloque estos repelentes en los horarios de actividad del mosquito y, por lo tanto, de mayor riesgo de picadura.</p>
						<p>Aplique insecticidas sólo cuando haya visto mosquitos adultos en lugares como cortinas, detrás de los muebles, zonas oscuras de la casa o con vegetación. Evite la presencia de niños y mascotas mientras lo aplica.</p>
						<p>En cuanto a la fiebre chikungunya es una enfermedad vírica transmitida al ser humano por mosquitos infectados. Causa fiebre, fuertes dolores articulares, musculares, de cabeza, náuseas, cansancio y erupciones cutáneas. “Chikungunya” significa “doblarse”, en alusión al aspecto encorvado de los pacientes debido a los dolores articulares.</p>
					  <p>La mayoría de los pacientes se recuperan completamente. No existe ningún antivírico específico para tratarla. El tratamiento consiste principalmente en aliviar los síntomas, entre ellos el dolor articular, con antipiréticos, analgésicos óptimos y líquidos.</p>
