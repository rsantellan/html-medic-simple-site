				    <h1>Que la cocina no sea un lugar prohibido</h1>	
					  <p>Terminan las clases, tenemos a nuestros hijos más tiempo en casa y pensamos el modo de ocuparles el tiempo para escapar del &quot;estoy aburrido&quot;. </p>
					  <p>Una actividad que seguro los va a entusiasmar es la de cocinar juntos algo para la merienda, la cena o el almuerzo.</p>
					  <p>Cocinar en familia implica realizar una pausa en el día, la cual sirve para compartir y conocerse mejor, sirve para crear en conjunto y promover las tradiciones culturales y mantener la historia de la familia.</p>
					  <p>Muchas veces cuando nuestros hijos se nos acercan a ver qué estamos haciendo o desean colaborar nosotros pensamos que estamos justos de tiempo, que no es el momento adecuado y que seguro la cocina va a terminar hecha un lío si ellos intervienen.<br>
					    ¡Esto puede ser así al comienzo ya que es parte del aprendizaje, pero en algún momento hay que empezar! Sin darnos cuenta, a través de la cocina fortalecemos la relación con nuestros pequeños y desarrollamos otras capacidades en ellos.</p>
					  <p>Cocinar ayuda a saber planificar, ver qué vamos a utilizar, si debemos ir antes al supermercado, hacer una lista con lo que se necesita, conocer en qué orden debemos ir poniendo los ingredientes.</p>
					  <p>Nos va a facilitar el que nuestros hijos deseen probar nuevos sabores, que estén más propensos a probar el resultado final, ellos se apropian del proceso y se animan a probar sus propias creaciones.</p>
					  <p>En este lugar de la casa se puede aprender química y matemática, se cuestionan sobre qué pasa cuando el agua y el aceite se juntan, o cuánto es 1/3 de taza. </p>
					  <p>También son momentos donde podemos irle contando el origen de los alimentos elegidos y hablarles de las verduras o frutas de estación. Podemos recordar lo que nos gustaba comer a nosotros de niño y contarles tradiciones familiares: &quot;así lo hacía mi abuela&quot;.</p>
					  <p>Una vez finalizado y ya listo para ser comido y probado por los otros integrantes de la familia va a ser reconfortante para ellos escuchar &quot;te quedó riquísimo&quot;, sentirán una sensación de logro y superación inolvidable.</p>
					  <p>Decidir cocinar juntos va a colaborar a que ellos valoren el trabajo de lo que es la comida casera y agradezcan por ese &quot;mimo&quot; que les hacemos a diario.</p>
					  <p>Aprender a cocinar de niños ayuda a que sean más independientes y a resolver el tema de su alimentación.</p>
					  <p><em>Diego Ruete, educador preescolar y cocinero, aconseja a los padres que usen la cocina como tiempo de calidad. Por eso, no hagamos de la cocina un lugar prohibido, que sea un espacio familiar donde juntos la pasamos bien.</em></p>
