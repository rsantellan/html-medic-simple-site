				    <h1>Cambiando nuestros paradigmas </h1>	
				    <p>El 18 de agosto se llevó a cabo MP Update, un evento focalizado en cómo las revoluciones tecnológicas han cambiado nuestra forma de ver el mundo. En esta edición contamos con speakers que, aunque venían de áreas muy distintas, evidenciaron que siempre es posible encontrar soluciones innovadoras. </p>
				    <p>Desde el comienzo se planteó una dinámica distinta cuando Daniel Goldman, con su presentación totalmente en silencio, con mímicas y juegos, nos mostró como la mayoría sufrimos de fijaciones mentales. Ese era, justamente, el motivo que nos reunía: romper con viejos esquemas, repensando y potenciando la capacidad creativa que todos llevamos dentro.</p>
				    <p>Juan Grunwaldt, cofundador de Projects of Humanity, habló sobre esta plataforma que busca ser un canal de contenidos de marcas que quieren no solo llegar al público con un mensaje sino que también desean que quienes eligen verlas y oírlas donen su “atención” a una ONG que deseen. </p>
				    <p>Valentina Arraigada nos enseñó cómo desde un problema familiar nace una oportunidad y presentó Connectus Medical, una plataforma web que permite a pacientes que necesitan diálisis encontrar centros médicos en el extranjero que se adapten a su enfermedad.</p>
				    <p>Victoria Corti reflexionó sobre la educación y la necesidad de pensar nuevas estructuras y disposición de las clases, y estableció que “la educación debe ser una aventura”. En la búsqueda de cambios, junto a un grupo multidisciplinario crearon LAB4U, una aplicación que permite realizar experimentos de laboratorio de física y química sin la necesidad de tener equipamiento especializado. </p>
				    <p>Gino Tubaro, con apenas 20 años, nos hizo admirarlo y sensibilizarnos al presentar sus inventos pensados para mejorar la calidad de vida de las personas con discapacidades. Expuso las prótesis de manos y brazos realizados en impresoras 3D, que son livianos, tienen movimiento y son prácticamente sin costo. También contó sobre su nuevo proyecto “Braille Dinámico”, un sistema que traduce texto a braille. </p>
				    <p>Eduardo Mangarelli cerró el evento con una exposición dinámica en la que nos mostró cómo el futuro ya está entre nosotros y cada vez llega más rápido, en relación a la importancia de innovar, reinventarse y cambiar las reglas.</p>
				    <p>Al terminar las presentaciones, un ambiente distendido esperaba a los asistentes para disfrutar de un catering, juegos y la música de Patricia Wolf.</p>
				    <p>Si te quedaste con ganas de ir o te gustaría volver a escuchar las charlas te invitamos a seguirnos en nuestro canal de Youtube MPersonalizada o en nuestras redes sociales. </p>
