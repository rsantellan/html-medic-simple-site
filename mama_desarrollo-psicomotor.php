						<h1>Desarrollo psicomotor: comportamiento y actitudes</h1>
						<h2>0 a 3 meses </h2>
						<p><strong>¿Cómo es el niño? </strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación:</strong>El amamantamiento suave y placentero como encuentro madre-hijo y como fuente de nutrición</li>
							<li><span>&#8226;</span><strong>Higiene:</strong>Necesita moverse, conocerse y que lo conozcan</li>
							<li><span>&#8226;</span><strong>Sueño: </strong>duerme la mayor parte del tiempo con su propio ritmo.</li>
							<li><span>&#8226;</span><strong>Lenguaje:</strong> emite gritos y llantos, sonidos para expresar hambre, soledad, dolor, cansancio. Comprende cuando le hablan</li>
							<li><span>&#8226;</span><strong>Motricidad: </strong> conoce el mundo a través de la boca. Se mete cosas en la boca, chupa, muerde; esto lo ayuda a relajarse.</li>
							<li><span>&#8226;</span><strong>Relacionamiento:</strong>la comunicación entre la madre y el bebé se basa en demandas y respuestas.</li>
						</ul>
						<p><strong>Actitud materna y de la familia</strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación:</strong> acariciarlo, mirarlo, hablarle durante la alimentación.</li>
							<li><span>&#8226;</span><strong>Higiene:</strong> al cambiarle la ropa, al bañarlo, jugar, cantar, tocarlo.</li>
							<li><span>&#8226;</span><strong>Sueño:</strong> que duerma en su propia cuna, en su habitación. Las canciones de cuna le dan seguridad.</li>
							<li><span>&#8226;</span><strong>Lenguaje:</strong> hay que aprender a distinguir los llantos, llamarlo por su nombre, imitar sus sonidos, explicarle lo que pasa.</li>
							<li><span>&#8226;</span><strong>Motricidad:</strong> vestirlo con ropa cómoda, colocarlo en posiciones diferentes, colgar objetos de colores al alcance de su vista.</li>
							<li><span>&#8226;</span><strong>Relacionamiento:</strong> brindar cuidados adecuados (ni excesivos ni insuficientes). Buscar la adaptación.</li>
						</ul>
						<h2>3 a 6 meses</h2>
						<p><strong>¿Cómo es el niño? </strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación:</strong> es capaz de regular la cantidad de comida que necesita y de expresar saciedad o disgusto.</li>
							<li><span>&#8226;</span><strong>Higiene:</strong> juega con su cuerpo, quiere mirarse, tocarse, moverse. Disfruta en el agua.</li>
							<li><span>&#8226;</span><strong>Sueño:</strong> el llanto nocturno puede significar angustia por la separación; no tiene conciencia de que es momentánea.</li>
							<li><span>&#8226;</span><strong>Lenguaje:</strong> responde al juego con una variedad de sonidos (laleos, gritos), aprende el nombre de objetos y personas.</li>
							<li><span>&#8226;</span><strong>Motricidad:</strong> necesita moverse libremente para desarrollar el equilibrio, la coordinación y confianza en sí mismo para canalizar su energía.</li>
							<li><span>&#8226;</span><strong>Relacionamiento:</strong> expresa sus sentimientos mediante el llanto.</li>
						</ul>
						<p><strong>Actitud materna y de la familia </strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación:</strong> ser paciente en la introducción de alimentos nuevos (semisólidos) y en el uso de la cuchara.</li>
							<li><span>&#8226;</span><strong>Higiene: </strong> dejarlo un rato sin ropa antes de bañarlo para que haga ejercicio. En el baño dejarlo disfrutar con el agua.</li>
							<li><span>&#8226;</span><strong>Sueño:</strong>  los ruidos familiares, la música suave y la luz tenue ayudan a tranquilizarlo. Llevarlo a la cama paterna no favorece el desarrollo.</li>
							<li><span>&#8226;</span><strong> Lenguaje: </strong> escuchar e imitar sus sonidos, nombrarle y mostrarle objetos y personas. Contarle las actividades a medida que se realizan.</li>
							<li><span>&#8226;</span><strong>Motricidad: </strong> cambiarlo de posición (sentarlo con almohadones, acostarlo boca abajo) efectuar juegos corporales (balanceo, caballito, etc.). Permitirle chupar, tocar o agarrar objetos de la casa.</li>
							<li><span>&#8226;</span><strong> Relacionamiento:</strong> hay que encontrar un punto de equilibrio al atenderlo. No siempre que llore hay que levantarlo en brazos, y además no levantarlo nunca, no ayuda a evitarle mañas. Despedirse y dar explicaciones cuando la madre va a trabajar.</li>
						</ul>	
						
						<h2>6 a 9 meses</h2>
						<p><strong>¿Cómo es el niño? </strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación: </strong> es más independiente; disfruta al tomar la comida con sus manos, intenta manejar la cuchara. Le atrae más tocar que comer.</li>
							<li><span>&#8226;</span><strong>Higiene:</strong> necesita conocer los límites de su cuerpo y quererlo.</li>
							<li><span>&#8226;</span><strong>Sueño:</strong> necesita seguridad a la hora de dormir y sentirse acompañado por personas u objetos de preferencia.</li>
							<li><span>&#8226;</span><strong>Lenguaje:</strong> emite sonidos y sílabas simulando conversación.</li>
							<li><span>&#8226;</span><strong>Motricidad:</strong> se arrastra, gatea, tiende a sentarse y a incorporarse. Aprende a pararse sin ayuda.</li>
							<li><span>&#8226;</span><strong>Relacionamiento:</strong> distingue los rostros, es sensible a los desconocidos y llora porque extraña.</li>
						</ul>
						<p><strong>Actitud materna y de la familia</strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación: </strong>  fomentar la independencia aunque se ensucie, dejarlo tocar, mirar y oler la comida.</li>
							<li><span>&#8226;</span><strong>Higiene:</strong> jugar al cambiarlo y bañarlo, tocando y nombrando partes de su cuerpo. Permitirle jugar en el agua con objetos no muy pequeños.</li>
							<li><span>&#8226;</span><strong>Sueño:</strong> permitirle dormir con su juguete u objeto preferido; no cambiarlo ni tirarlo.</li>
							<li><span>&#8226;</span><strong>Lenguaje:</strong> escucharlo y responderle con distintos sonidos y tonos de voz. Cantar y leerle cosas sencillas; jugar con él frente al espejo.</li>
							<li><span>&#8226;</span><strong>Motricidad:</strong> ayudarlo y tratar que los objetos en que se apoye sean firmes y sin puntas. Enseñarle a gesticular (aplaudir, fruncir la nariz, etc.)</li>
							<li><span>&#8226;</span><strong>Relacionamiento:</strong> enseñarle los límites de lo permitido, sin emplear castigos.</li>
						</ul>

						<h2>9 a 12 meses</h2>
						<p><strong>¿Cómo es el niño? </strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación: </strong> ha desarrollado sus gustos y preferencias. Quiere participar en las comidas.</li>
							<li><span>&#8226;</span><strong>Higiene:</strong> sigue el proceso de individualización. El niño disfruta jugando con su cuerpo. Quiere desplazarse por la casa.</li>
							<li><span>&#8226;</span><strong>Sueño:</strong> necesita seguridad a la hora de dormir. Se mueve mucho.</li>
							<li><span>&#8226;</span><strong>Lenguaje:</strong> ocaliza tratando de imitar lo que le hablan. Entiende gestos y palabras. Imita voluntariamente acciones simples. Expresa sentimientos mediante besitos y abrazos.</li>
							<li><span>&#8226;</span><strong>Motricidad:</strong> fortalece los músculos de la deambulación. Tirar y soltar cosas, es su pasatiempo favorito. Aprende cosas con los juegos de imitación.</li>
							<li><span>&#8226;</span><strong>Relacionamiento:</strong> el niño está en una etapa muy inquieta. Toca todo, es curioso, se desplaza, y hay que tener paciencia para enseñarle y evitar situación enojosas o peligrosas.</li>
						</ul>
						<p><strong>Actitud materna y de la familia</strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación: </strong> tener en cuenta sus gustos a la hora de preparar la comida, sin que esto signifique desbalancear la dieta. Dejarlo tomar la comida con las manos, cuchara y vaso; servir porciones pequeñas para evitar volcar y ensuciar. Que participe de la mesa familiar.</li>
							<li><span>&#8226;</span><strong>Higiene:</strong> continuar las recomendaciones anteriores. Aún no es tiempo de sacarle los pañales. Intentar hacerlo, puede dañarlo emocionalmente y retrasar el aprendizaje.</li>
							<li><span>&#8226;</span><strong>Sueño:</strong> continuar con las recomendaciones anteriores. Observar si intenta pasar por encima de los barrotes de la cuna. En este caso, bajarlos y colocar un colchón en el piso o usar una cama normal. Establecer un horario.</li>
							<li><span>&#8226;</span><strong>Lenguaje:</strong> nombrar los objetos siempre con la misma palabra; no hablarle en forma aniñada y darle órdenes sencillas (toma, dame) lo ayudan a formar y asociar el lenguaje con objetos y situaciones.</li>
							<li><span>&#8226;</span><strong>Motricidad:</strong> dejarlo explorar; no dejar objetos valiosos o peligrosos a su alcance. Enseñarle a dar pasitos. No es conveniente el andador. Usar el corral con moderación. Dejarlo explorar.</li>
							<li><span>&#8226;</span><strong>Relacionamiento:</strong> estar presente cuando juega. Los tonos de voz, las miradas y los abrazos son “herramientas” adecuadas para guiarle en su comportamiento.</li>
						</ul>			

						<h2>12 a 18 meses</h2>
						<p><strong>¿Cómo es el niño? </strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación: </strong> el niño está más interesado en lo que lo rodea, que en la alimentación</li>
							<li><span>&#8226;</span><strong>Higiene:</strong> empieza a hacer algunas cosas solo. Experimenta juegos durante el baño. Salpica, chapotea.</li>
							<li><span>&#8226;</span><strong>Sueño:</strong> permanece más tiempo despierto. Necesita una rutina con horarios de siesta y descanso nocturno. Los descubrimientos pueden generar ansiedad.</li>
							<li><span>&#8226;</span><strong>Lenguaje:</strong> emite sonidos, repite palabras y señala con gestos lo que desea.</li>
							<li><span>&#8226;</span><strong>Motricidad:</strong> comienza la marcha autónoma, tomándose de los muebles y luego camina sin apoyo.</li>
							<li><span>&#8226;</span><strong>Relacionamiento:</strong> esta etapa es una verdadera explosión motriz. El niño es capaz de moverse libremente y alcanzar todos los objetos de la casa. Se refuerza la autoestima.</li>
						</ul>
						<p><strong>Actitud materna y de la familia</strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación: </strong> no preocuparse si disminuye el apetito del niño. Compartir la mesa familiar con él sin enseñarle “buenos modales”. Ofrecerle comidas nuevas y variadas y fomentar el uso de la cuchara. Evitar caramelos, pan y galletitas antes de las comidas. Tolerar sus enojos al negárselos y no ofrecerlos para que se calle durante una “rabieta”.</li>
							<li><span>&#8226;</span><strong>Higiene:</strong> durante el baño, dejarlo chapotear, salpicar, llenar y vaciar recipientes. Permitirle colaborar al vestirlo o desvestirlo.</li>
							<li><span>&#8226;</span><strong>Sueño:</strong> establecer claramente los horarios de siesta y sueño nocturno. De este modo, se evitaran disturbios en el clima familiar y se favorece el sueño del niño.</li>
							<li><span>&#8226;</span><strong>Lenguaje:</strong> aunque no entienda todo, hablarle mucho. Reforzar sus primeras palabras, nombrarle los sentimientos que los padres perciben en él (alegría, dolor, etc.). Continuar con las órdenes sencillas y fomentar su “colaboración” en las tareas domésticas.</li>
							<li><span>&#8226;</span><strong>Motricidad:</strong> vigilar sus primeros pasos, para evitar accidentes. Animarlo a trepar, subir, bajar, esconderse, etc., para ejercitar su equilibrio y coordinación. Puede jugar solo, aunque prefiera tener alguien al lado. Darle cubos y recipientes para apilar, llenar y vaciar; se fomenta el desarrollo de estructuras lógicas.</li>
							<li><span>&#8226;</span><strong>Relacionamiento:</strong> se deben extremar las precauciones para evitar accidentes. No exteriorizar angustias ante cada caída del niño, pues esto puede disminuir su interés por explorar.</li>
						</ul>	

						<h2>18 a 24 meses</h2>
						<p><strong>¿Cómo es el niño? </strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación: </strong> adquiere destrezas manuales más complejas. La alimentación es un buen momento para la integración familiar..</li>
							<li><span>&#8226;</span><strong>Higiene:</strong> se llega al nivel madurativo para comenzar a autorregularse (controlar esfínteres) y responder pedidos de los padres. Le gusta jugar con barro y arena.</li>
							<li><span>&#8226;</span><strong>Sueño:</strong>  a partir de esta edad comienzan las pesadillas, que pueden persistir hasta los seis años.</li>
							<li><span>&#8226;</span><strong>Lenguaje:</strong> emite frases simples y cumple órdenes sencillas.</li>
							<li><span>&#8226;</span><strong>Motricidad:</strong> mejora el equilibrio y la coordinación. Adquiere destrezas manuales más complejas.</li>
							<li><span>&#8226;</span><strong>Relacionamiento:</strong> son frecuentes las rabietas sin razón aparente; se trata de necesidades que el niño no domina y las expresa en forma impulsiva.</li>
						</ul>
						<p><strong>Actitud materna y de la familia</strong></p>
						<ul>
							<li><span>&#8226;</span><strong>Alimentación: </strong> no insistir con buenos modales o con que coma todo; lo importante es que intente comer solo. Atención: comer en exceso o no comer nada puede reflejar carencias afectivas. Dejarlo participar en la elaboración de la comida (pelar huevos duros, mezclar la ensalada, etc.).</li>
							<li><span>&#8226;</span><strong>Higiene:</strong> aprovechar para inducir hábitos de higiene. Tener paciencia en el aprendizaje del control de esfínteres. Apurar y castigar disminuye su autoconfianza y retrasan el proceso de adquisición.</li>
							<li><span>&#8226;</span><strong>Sueño:</strong> dejar una veladora encendida y acompañarlos al ir a la cama, contándoles un cuento. Ya no son necesarias las barandas en la cama.</li>
							<li><span>&#8226;</span><strong>Lenguaje:</strong> escuchar cuando habla sin corregirlo permanentemente. Hablarle en forma correcta y fomentar la expresión de sus sentimientos positivos y negativos. Jugar con él, cantar, bailar, imitar animales, mostrar cosas y decir el nombre, color, forma, etc.</li>
							<li><span>&#8226;</span><strong>Motricidad:</strong> realizar juegos corporales. Darle papel para que pinte, dibuje, rasgue; revistas para que mire y pase hojas. Ofrecerle juguetes para tirar y arrastrar, llevarlo a lugares donde pueda trepar, bajar, etc.</li>
							<li><span>&#8226;</span><strong>Relacionamiento:</strong> es norma que esto ocurra, y no se debe perder la paciencia. Es necesario poner límites firmes, pero no violentos. El niño se angustia si se lo deja solo, con su rabieta, tanto como si se lo reprende violentamente.</li>
						</ul>																
