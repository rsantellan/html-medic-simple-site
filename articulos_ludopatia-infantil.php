				    <h1>¿Ludopatía infantil?</h1>	
					  <p>La ludopatía implica realizar una actividad con una intensidad que sobrepasa lo normal y sano, atenta contra la propia salud y lleva a una pérdida del control del tiempo.</p>
					  <p>Hay niños que pasan una, tres o cinco horas frente a una consola sin poder dejar de jugar, destinando más tiempo que el adecuado.</p>
					  <p>Son niños que se involucran con el juego al extremo de no hacer otras actividades útiles y placenteras.</p>
					  <p>La ludopatía puede desarrollarse a cualquier edad y los niños sin el apoyo adecuado de padres u otros adultos están más propensos a un mal uso y posteriormente a una posible dependencia y adicción.</p>
					  <p>Se recomienda fortalecer otras actividades en la que el niño sienta retribución emocional y afectiva así como aquellas que colaboren en las habilidades sociales evitando que el niño se encierre en este tipo de juegos.</p>
					  <p><strong>Pueden ser factores de riesgo para esta enfermedad:</strong></p>
					  <ul>
					    <li><span>&#8226;</span>Mal control de parte de los adultos a cargo 
					      sobre el uso de juegos.</li>
					    <li><span>&#8226;</span>Carencia de actividades que retribuyan 
					      emocional y afectivamente.</li>
					    <li><span>&#8226;</span>Inseguridades dentro del desarrollo de la 
					      personalidad y la predisposición hereditaria a la adicción.</li>
				      </ul>
<p>Hay niños que llegan a desarrollar síntomas de abstinencia, es decir que cuando no están jugando tienen molestias físicas y psíquicas que lo hacen pensar en ello y querer volver a jugar.</p>
					  <p>La ludopatía tiene tratamiento, comenzando por la patología de base como posibles trastornos depresivos y alteraciones en el desarrollo de la personalidad del menor. Se debe trabajar en el control sobre el uso y reforzar la confianza en si mismo para la realización de otras actividades.</p>
					  <p>El continuar jugando de un modo obsesivo traerá aparejado el fracaso escolar y la dificultad por conciliar el sueño.</p>
