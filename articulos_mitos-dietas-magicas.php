						<h1>Mitos sobre las dietas mágicas</h1>	
						<p><strong>Mito 1: Ciertos alimentos como el pomelo, la sopa de apio o de repollo, queman calorías y ayudan a bajar de peso.</strong><br>
					    No hay ningún alimento que queme calorías. Hay algunos que contienen cafeína que ayudan a acelerar el metabolismo por un corto tiempo, pero no producen pérdida de peso.</p>
						<p><strong>Mito 2: Saltarse comidas es una buena forma de bajar de peso.</strong><br>
					  Si se salta una comida, lo más probable es que recuperará esas calorías en bocaditos a deshoras y comiendo a destajo en la próxima comida. Lo ideal es comer cada tres o cuatro horas, porque así el metabolismo se mantiene en constante funcionamiento. Cuando se generan largos períodos de hambruna, el metabolismo disminuye su ritmo y produce menos gasto energético, como un método de defensa ante la falta de alimento.</p>
						<p><strong>Mito 3: Los frutos secos engordan. No deberían ingerirse si se desea bajar de peso.</strong><br>
					  Son altos en calorías y grasas pero contienen bajas cantidades de grasas saturadas y son una fuente importante de proteína y fibra. Además no tienen colesterol. En un plan de baja de peso se restringe o modera su ingesta. Aportan muchas calorías en un volumen pequeño.</p>
						<p><strong>Mito 4: Los hidratos de carbono son engordadores y deberían ser suspendidos para bajar de peso.</strong><br>
						  Los hidratos de carbono o almidones son una fuente importante de energía para el organismo. Por ello es que deben ser controlados para bajar de peso, pero no suspendidos.<br>
					  El azúcar y derivados sí deberían ser eliminados en una dieta para bajar de peso.</p>
						<p><strong>Mito 5: Puedo bajar de peso comiendo mucha fruta.</strong><br>
					  La fruta seca tiene calorías, ya que es rica en azúcar pero también es una buena fuente de vitaminas y fibras. Debe consumirse en cantidades controladas y acompañada de otros alimentos para que la dieta sea equilibrada.</p>
						<p><strong>Mito 6: Comer carnes rojas es dañino para la salud y dificulta la pérdida de peso.</strong><br>
					  Las carnes rojas contienen grasas saturadas y colesterol, pero también son una fuente importante de proteínas, hierro, vitamina B12 y zinc. Se eligen las variedades menos grasas y, en caso de colesterol alto, se controla su frecuencia de consumo.</p>
						<p><strong>Mito 7: Dietas altas en proteínas y bajas en carbohidratos son una forma saludable de bajar de peso.</strong><br>
						  Las dietas altas en proteínas y bajas en carbohidratos pueden producir una rápida bajada de peso pero la mayoría proviene de la pérdida de agua y masa muscular, y no de las grasas. Esto ocurre al aportar exceso de proteínas sin una ingesta calórica adecuada, que lleva a usarlas como fuente de energía en lugar de guardarlas y formarlas.<br>
					  Otra desventaja es la constipación, el aumento de algunas sustancias (ácido úrico) y otros síntomas como náuseas o debilidad.</p>
						<p><strong>Mito 8: Transformarse en vegetariano es asegurarse una baja de peso y ser más saludable.</strong><br>
					  Las dietas vegetarianas por lo general son saludables, porque aportan pocas grasas saturadas y colesterol. Pero para ser igual de nutritiva que una dieta normal, debe controlarse por un especialista. Para adquirir la proteína necesaria se debe ingerir una cantidad extra de lentejas, tofu, frutos secos, etc. También se debe vigilar el aporte de hierro, vitamina B12 y zinc.</p>
