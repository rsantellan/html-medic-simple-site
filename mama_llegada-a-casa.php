						<h1>LLegada a casa</h1>
						<p>Una vez pasada la experiencia del parto, mamás y papás tienen que asumir la responsabilidad de cuidar del bebé cuando apenas se tiene experiencia, con las lógicas incomodidades de un traslado. El día de la vuelta a casa no es nada fácil. Los siguientes tampoco lo son, pero las cosas se van simplificando a medida que se organizan. Es importante procurar descansar, alimentarse bien, contar con ayuda y afrontar el posible desánimo con calma y paciencia.</p>
						<p><strong>Los horarios que un bebé exige, día y noche, pueden resultar física y mentalmente agotadores, especialmente para la madre que amamanta; por eso, es preciso que trate de descansar: </strong></p>
						<ul>
							<li><span>&#8226;</span>Reduciendo sus responsabilidades. Lo único que solo la madre puede y debe hacer es alimentar a su hijo y cuidar de ella misma. En ese período ha de ser sustituida en las tareas domésticas y saber dejar para más adelante todo lo que admita espera.</li>
							<li><span>&#8226;</span>Durmiendo a la vez que el bebé. De hecho, las hormonas que se producen cuando el niño succiona el pezón dan sueño a la madre. Si sigue ese impulso natural, puede acumular muchos minutos de descanso a lo largo del día.</li>
							<li><span>&#8226;</span>Limitando las visitas. Puede ser muy agradable recibir a familiares y amigos, pero ante la fatiga no hay que dudar en pedir disculpas y retirarse; los demás lo entenderán.</li>
							<li><span>&#8226;</span>Distrayéndose y paseando. Puede hacerlo con el bebé si lo desea, pues no hay el menor inconveniente en sacarlo a la calle desde el primer día.</li>
							<li><span>&#8226;</span>Una buena nutrición mejorará el estado físico. Es más fácil digerir varias pequeñas raciones repartidas a lo largo del día que las tres clásicas grandes comidas. Aunque no conviene perder peso rápidamente, las frutas y verduras son alimentos sanos que apenas engordan.</li>
							<p>Una opción es que el padre adelante las vacaciones o que comparta el permiso por maternidad, pues si bien las primeras seis semanas son de descanso obligatorio para la madre, el padre puede consumir alguna de las diez restantes durante ese período. Por lo demás, aunque también puede tratarse de una amistad, lo más habitual es recurrir a un miembro de la familia, como una abuela o tía del bebé. Si la situación económica lo permite, también se puede contratar personal de servicio doméstico o de enfermería.</p>
							<p>En cualquier caso, es importante que la persona que va a ayudar sepa claramente lo que se espera de ella. Y en el caso de la pareja, tanto o más importante que su colaboración material, es su apoyo moral.</p>
							<p>Hasta un 80% de las madres se sienten tristes o irascibles en los quince días que siguen al parto, probablemente debido a los cambios hormonales que están sufriendo, aunque también al cansancio, los nervios y la falta de sueño que acarrea la nueva situación. Esta tristeza posparto, que los anglosajones denominan "baby-blues" o "postpartum-blues" (aludiendo con la palabra "blues" a un estado de ánimo melancólico), se inicia a los tres o cuatro días y no dura más de un par de semanas, durante los que la madre puede presentar bruscos cambios de humor con irritabilidad y llanto fácil, pérdida de apetito, dificultad para concentrarse, falta de energía, ansiedad y, a veces, insomnio.</p>
							<p>Si no ha sido advertida, puede sorprenderse de no sentirse feliz o tan contenta como esperaba con el deseado nacimiento de su hijo y llegar incluso a pensar que no es una buena madre; pero un poco de reposo y apoyo suele bastar para superar sin problemas ese período, especialmente si se tiene en cuenta que es algo normal y transitorio.</p>
