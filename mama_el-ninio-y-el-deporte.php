						<h1>El niño y el deporte</h1>
						<ul class="notas_mama">
							<li>Dr. Guillermo Pose Trujillo</li>
							<li>Pediatra-Cardiólogo infantil</li>
						</ul>							
						<p>El ejercicio físico es uno de los principales pilares de una vida saludable, es fundamental para contribuir a los procesos de desarrollo y maduración de su potencial genético. La actividad deportiva, el juego o actividad lúdica que implique movimiento, mejora significativamente las funciones cardiovasculares y contribuye a una adecuada maduración del sistema músculo-esquelético y de sus habilidades psicomotoras. El ejercicio físico rutinario, acompañado de una dieta equilibrada, va a contribuir al mantenimiento de un adecuado peso corporal, evitando la aparición de obesidad, tanto en la infancia como en la vida adulta, puesto que los hábitos que se aprenden de niño, se mantienen con los años, (el 80% de los adultos obesos han sido niños obesos). También va a ayudar a la prevención de las enfermedades degenerativas como la arteriosclerosis, la hipertensión arterial y la diabetes. </p>
						<p>En el niño un entrenamiento regular produce beneficios en las habilidades motoras y cognitivas, siendo beneficioso también para sus relaciones personales y en el grupo social que le rodea, aprendiendo a integrarse y obteniendo bienestar físico y psicológico. Todos los beneficios que se obtienen de una vida activa son especialmente significativos a partir de la pubertad.</p>
						<ul>
							<li><span>&#8226;</span>Es el medio más eficaz e integrador para transmitir a todos los niños las habilidades, modelos de pensamiento, valores, conocimientos y comprensión necesarios para que practiquen actividades físicas y deporte a lo largo de su vida. </li>
							<li><span>&#8226;</span>Contribuye a un desarrollo integral y completo tanto físico como mental.</li>
							<li><span>&#8226;</span>Es la única asignatura escolar que tiene por objeto el cuerpo, la actividad, el desarrollo físico y la salud.</li>
							<li><span>&#8226;</span>Ayuda a los niños a familiarizarse con las actividades corporales y les permite desarrollar ante ellas el interés necesario para cuidar su salud, algo que es fundamental para llevar una vida sana en la edad adulta, al mismo tiempo que comprenden la importancia que la realización de ejercicio físico tiene como prevención del desarrollo de algunas enfermedades.</li>
							<li><span>&#8226;</span>Ayuda a los niños a respetar su cuerpo y el de los demás.</li>
							<li><span>&#8226;</span>Contribuye a fortalecer en los niños la autoestima y el respeto por sí mismos.</li>
							<li><span>&#8226;</span>Desarrolla en los niños la conciencia social al prepararlos en situaciones de competición para enfrentarse a victorias y derrotas, así como para la colaboración y el espíritu de compañerismo.</li>
							<li><span>&#8226;</span>Proporciona habilidades y conocimientos que podrán utilizarse más adelante en la vida laboral dentro del campo de los deportes, las actividades físicas, de recuperación y tiempo libre que están cada vez más en auge. </li>
							<p>Los padres deben intentar <strong>introducir a sus hijos en alguna actividad deportiva</strong> y tratar de aficionarlos, preocupándose por encontrar el deporte que más le guste a su hijo de acuerdo con las aptitudes físicas del niño.</p>
							<p>Para que haya una continuidad, <strong>deben buscar un deporte que les divierta y con el que disfruten</strong>, para ello los padres deben mostrar interés, preocupándose por el equipo, asistiendo a los partidos y comentándolos después con ellos, observando su comportamiento y tratando de animarlo cuando sea preciso o calmarlo cuando esté sobreexcitado. </p>
							<p>Tanto los entrenadores como <strong>los padres deben evitar que los niños sean excesivamente competitivos</strong>, no se trata de preparar a futuros profesionales si no de que hagan ejercicio físico y se diviertan, por ello hay que enseñarles que lo importante es el deporte, que no consiste en ser los mejores ni en ser unos campeones, sino de practicar deporte.</p>
