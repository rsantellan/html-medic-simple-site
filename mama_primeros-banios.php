						<h1>Consejos para los primeros baños</h1>
						<p>Para los padres, es un momento en el que deben prestar especial atención. Lo ideal es que la primera vez, lo hagan los dos juntos. </br>Un bebé enjabonado es muy escurridizo; si resulta difícil bañarle en el lavabo sin hacerle daño con los grifos y hacerlo en la bañera grande es incómodo, resulta imprescindible disponer de una bañera especial para bebé. Las que más habitualmente se eligen son las plegables. Es importante vigilar que lleven un buen sistema para evitar su cierre accidental y dañar al bebé. </p>
						<p>No es preciso esperar a que cicatrice el ombligo para bañar al bebé. La humedad mantenida es perjudicial para el ombligo, pero si después del baño se seca y cuida debidamente, no hay ningún peligro. </p>
						<p>El baño diario es muy conveniente en niños más mayores, pero que no es imprescindible en un bebé que apenas se ensucia en la zona del pañal o cuando devuelve un poco de leche. La piel tiene sus propios mecanismos de limpieza y la excesiva frecuencia o duración de los baños pueden irritarla, especialmente en bebés que la tienen muy sensible y cuando se emplea agua muy caliente. Un breve baño diario en agua tibia es bueno y a muchos bebés les relaja, pero al principio, cuando no se tiene demasiado tiempo, basta con bañarles dos o tres veces por semana. Hay que lavarles tantas veces como sea necesario la zona del pañal, la cara, las manos, las axilas y los pliegues de la piel, o cualquier zona que se haya ensuciado.</p>
						<p>Los bebés agradecen la seguridad de lo rutinario. Lo más habitual es bañarlos antes de la última toma del día, pero se puede hacer a cualquier otra hora si a los padres les va mejor o al bebé no le gusta el agua y, en vez de relajarle, le desvela. </p>
						<p>No es recomendable intentar bañar a un bebé que está llorando de hambre, pero en general, es mejor que el baño no sea después de las tomas, no por el riesgo de un corte de digestión, que sólo podría producirse si pasasen frío, sino porque suelen preferir dormir y también es fácil que los movimientos provoquen alguna regurgitación.</p>
						<p><strong>Al momento de bañarlos se recomienda:</strong></p>
						<ul>
							<li><span>&#8226;</span>Caldear el cuarto de baño por encima de veinte grados. </li>
							<li><span>&#8226;</span>Preparar y tener cerca todo lo que se necesita para bañarle y cambiarle, pues no se le podrá dejar solo ni un momento. </li>
							<li><span>&#8226;</span>Llenar la bañera hasta unos 5 ó 10 centímetros de profundidad con agua tibia. </li>
							<li><span>&#8226;</span>Comprobar la temperatura del agua con la piel del dorso de la mano o el codo, más sensible y parecida a la del bebé, cuidando de que sólo este tibia. Si se usa termómetro debe estar entre 32ºCy 38ºC. </li>
							<li><span>&#8226;</span>Coger al bebé pasando un brazo por detrás de su cabeza y espalda, sujetándole por la axila y usando la mano libre para enjabonarle y aclararle con una esponja suave o con la mano. </li>
							<li><span>&#8226;</span>Nunca lo dejes sólo. Un bebé puede ahogarse en dos dedos de agua mientras se ha ido a abrir la puerta o a atender el teléfono. </li>
							<li><span>&#8226;</span>No lo entretengas demasiado, pues los baños largos maceran la piel y el agua puede enfriarse. El baño debe ser agradable, pero no es el momento más adecuado para jugar, porque no conviene que dure más de cinco minutos. </li>
							<li><span>&#8226;</span>Secarle enseguida y bien, en una superficie amplia y estable, poniendo especial cuidado en los pliegues (axilas, ingles, cuello) y más aún en el ombligo si aún no ha cicatrizado. </li>
							<li><span>&#8226;</span>Los jabones y shampoo para bebés deben ser suaves, neutros o muy poco ácidos, para no modificar la acidez natural de su piel. </li>
							<li><span>&#8226;</span>Las cremas o leches hidratantes aplicadas después del baño son muy útiles para mantener la humedad de la piel, pero un bebé con una piel sana no las necesita.</li>
							<li><span>&#8226;</span>Es mejor prescindir de los polvos de talco porque pueden complicar la cicatrización de cualquier pequeña herida o erosión que haya en la piel y nunca se deben emplear para curar el ombligo. Si se usan, deben verterse en la mano y aplicarse con ella y no espolvorearlos directamente, pues el bebé podría inhalarlos.</li>
							<li><span>&#8226;</span>Las colonias ocasionan problemas con cierta frecuencia, por lo que si se desea utilizar alguna, es mejor aplicarla sobre la ropa del bebé y no sobre su piel.</li>
						</ul>
