						</ul>	
						<p>Una de las etapas en la vida de la mujer en la que ocurren los cambios más significativos es el embarazo. No solo desde el momento de la concepción, sino también antes y después de ocurrido el nacimiento del bebé.</p>
						<p>Cuando hablamos de nutrición, la gestación es un período en el cual aumentan las demandas de energía y nutrientes con el fin de atender el crecimiento y desarrollo fetal, así como también responder a los cambios tanto estructurales como metabólicos que acompañan dicho proceso: aumento del volumen sanguíneo, formación de la placenta, aumento en el tamaño del útero y mamas, formación de depósitos energéticos, entre otros.</p>
						<p><strong>Estado nutricional previo</strong></br>Desde el momento que pensamos en la idea de concebir el embarazo, debemos prestar especial atención a la alimentación de la futura mamá, ya que el estado nutricional previo de la embarazada condicionara el incremento de peso necesario para dicha etapa.</p>
						<p>Es por eso que el período previo a la concepción, es el momento ideal para plantearse y poner en práctica modificaciones en el estilo de vida, como por ejemplo mejorar los hábitos de alimentación, abandono del tabaquismo, cesación del consumo de alcohol, incremento de actividad física. De esta manera lograremos recibir el embarazo con un adecuado estado nutricional, evitando las complicaciones que derivan tanto del bajo peso materno como del sobrepeso u obesidad. </p>
						<p>Debemos recordar, que tanto para el embarazo como para todas las etapas del ciclo vital, los extremos no son buenos, es así que un aumento escaso o exagerado de peso durante este período se asocia con diversas complicaciones materno-fetales. Retardo en el crecimiento intrauterino y parto prematuro son algunas de las complicaciones del escaso aumento de peso; por otro lado la obesidad puede ocasionar dificultades en el momento del parto como, pre eclampsia (hipertensión), bebés macrosómicos, entre otras. </p>
						<p>El <I>Institute of Medicine de EUA</I> establece las recomendaciones de incremento de peso para el embarazo basándose en el Índice de Masa Corporal (IMC). </p>
						<p>Cabe destacar que aunque exista sobrepeso u obesidad pregestacional, el embarazo no es el momento indicado para realizar ningún tipo de dieta restrictiva, por lo tanto en estos casos, se deberá seguir más de cerca el incremento ponderal.</p>
