						<p><strong>Nutrientes que importan</strong></br>Obviamente que todos los nutrientes son importantes durante el embarazo en pos de que las necesidades de la mayoría de ellos aumenta, pero hay algunos en los cuales debemos poner mayor énfasis en el cuidado de su consumo. </p>
						<p>El <strong>hierro</strong> es uno de ellos. Es un mineral cuyas necesidades aumentan de manera considerable en esta etapa en función de la formación de la placenta, la transferencia al feto, la expansión de la masa eritrocitaria y la pérdida de sangre durante el parto. Se estima que la mayoría de las mujeres en edad fértil tienen reservas insuficientes de hierro y en los países subdesarrollados la mitad de las mujeres embarazadas padecen anemia. Es por esto que en nuestro país el MSP recomienda la suplementación medicamentosa de este mineral desde el primer control de la embarazada y se extienda hasta los primeros seis meses de la lactancia como mínimo. Además del suplemento, se recomienda la incorporación de alimentos fortificados con hierro, y ricos naturalmente en este mineral, como son; las carnes y las leguminosas. Es importante  la ingesta en conjunto de facilitadores de la absorción, como son los alimentos con alto contenido de vitamina C, es decir, cuando comes carnes puedes acompañarlas con jugo de naranja y/o seleccionar una naranja o mandarina como postre.</p>
						<p>Otro nutriente importante en la embarazada, es el <strong>ácido fólico</strong>. Existe suficiente evidencia científica para aconsejar el uso de suplemento de ácido fólico desde antes del embarazo, cuando este planificad y extender la ingesta hasta las doce semana de gravidez. Esta recomendación se realiza a efectos de prevenir defectos del tubo neural, del cual se va a formar el sistema nervioso central el bebé (médula espinal y cerebro).</p>
						<p>Si bien con una alimentación balanceada cubrimos la mayoría de las necesidades nutricionales, el hierro y el ácido fólico son los únicos nutrientes para los cuales se recomienda su suplementación medicamentosa.</p>
						<p>El <strong>calcio</strong> constituye otro mineral indispensable en la alimentación. Su adecuado consumo es necesario durante el embarazo, ya que si su requerimiento no es cubierto a través de la dieta, el mismo es movilizado desde el esqueleto materno, ampliando el riesgo de osteoporosis en etapas futuras de la vida de la mujer. Mas allá de que su requerimiento no varía para la mujer adulta de acuerdo con el previo al embarazo, debemos asegurarnos el consumo diario de alimentos fuente de este mineral.</p>
						<p><strong>Buscar el equilibrio: ni dejar de comer ni comer por dos</strong></p>
						<p>Hemos escuchado muchas veces que la embarazada tiene que “comer por dos”… importa destacar que más que a la cantidad de alimentos, tenemos que prestar atención a la calidad de los mismos.</p>
						<p>El requerimiento energético aumenta sin lugar a dudas, ya que dentro de esa mamá se esta formando un ser vivo.</br>La cantidad de calorías adicionales varía según la etapa del embarazo, pero la recomendación es sumar a las necesidades energéticas normales de la madre, entre 300 y 500kal/día, dependiendo del estado nutricional previo. </br>Este aumento calórico, debería traducirse en alimentos de buena calidad, como pueden ser los lácteos, las frutas y  los vegetales; con solo agregar alguna porción más de ellos a nuestra alimentación diaria (2 vasos de leche o yogur, 2 frutas, 1 plato de ensalada colorida), estaremos cubriendo el plus energético necesario para responder a las demandas maternas y fetales. </p>
						<p><strong>Recomendaciones</strong></p>
						<ul>
							<li><span>&#8226;</span>Intenta consumir al menos 3 porciones de <strong>lácteos</strong> al día, como leche, queso y yogur, además de incorporar preparaciones como salsa blanca y postres de leche</li>
							<li><span>&#8226;</span>Procura que todos los días haya <strong>vegetales</strong> crudos y cocidos y al menos 3 <strong>frutas</strong>. Recuerda que son alimentos ricos en vitaminas y minerales, agua y fibra, la cual contribuye a mantener un tránsito intestinal normal, siendo el estreñimiento un problema frecuente en la embarazada.</li>
							<li><span>&#8226;</span>Consume una porción de carnes al día, sin olvidar incluir el <strong>pescado</strong> una o dos veces por semana, es un alimento rico en grasas omega 3 las cuales benefician la formación del sistema nervioso y retina del bebé.</li>
							<li><span>&#8226;</span>Reduce el consumo de <strong>grasas de mala calidad</strong> como son las grasas saturadas (manteca, cortes grasos de carne vacuna, embutidos) y las grasas trans (también llamadas aceite vegetal hidrogenado) que están presentes en muchos panificados, galletas, productos de repostería y margarina entre otros.</li>
							<li><span>&#8226;</span>Agrega <strong>sal</strong> con moderación, el organismo no necesita más que el sodio contenido en los alimentos.</li>
							<li><span>&#8226;</span>Debes aumentar el consumo de <strong>líquidos</strong>, preferentemente agua, limitando bebidas que contienen cafeína como el café, bebidas cola, mate y té, sobre todo beberlas luego de una comida rica en hierro, ya que inhiben su absorción. A su vez debes suprimir las bebidas alcohólicas, el alcohol atraviesa la placenta pudiendo provocar en el feto síndrome alcohólico fetal, el cual podría derivar en malformaciones, problemas cardíacos y del sistema nervioso. </li>
							<li><span>&#8226;</span><strong>4 comidas diarias</strong> (desayuno, almuerzo, merienda y cena) más 2 colaciones (media mañana y media tarde) constituyen el fraccionamiento ideal para la mujer embarazada, brindando la posibilidad de distribuir todos los grupos de alimentos en estas seis comidas, además de contribuir a evitar ayunos prolongados, que no benefician ni a la madre ni al feto. Al mismo tiempo estas colaciones ayudan a disminuir síntomas como náuseas, vómitos y/o acidez, que habitualmente surgen durante la gestación.</li>
						</ul>
