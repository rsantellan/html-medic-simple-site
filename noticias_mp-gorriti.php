				    <h1>Armemos nuestra aventura en la Isla Gorriti</h1>
                        <p>Podemos armar un plan divertido para nuestros niños y llevarlos a conocer la Isla Gorriti, a 2,5 kilómetros de la costa de Punta del Este, frente a la playa Mansa. </p>
                        <p>Se puede ir en una embarcación privada o abordar alguna de las lanchas que parten desde el puerto de Punta del Este y brindan servicio de traslado. <br>
                          El viaje dura unos 15 minutos aproximadamente, y en temporada alta sale cada media hora.</p>
                        <p>La isla ha sido declarada Patrimonio Histórico Nacional por lo que no está permitido que se edifique en ella. Esto permite, que conserve su entorno natural de flora y fauna autóctona.</p>
                        <p>Mide más de dos kilómetros de largo y tiene una superficie de 21 hectáreas. Posee paradores e infraestructura para ir a pasar el día haciendo playa o paseando, teniendo en cuenta los horarios recomendados de exposición al sol.</p>
                        <p>Su nombre le rinde honores al Comandante Militar de Montevideo, el español Francisco Gorriti, arrestado en 1752 por Joaquín de Viana y encarcelado en la isla hasta el año siguiente. El arresto se debió a que se negó a colaborar con una expedición militar contra charrúas y minuanes.</p>
                        <p>Desde la salida del puerto se van a ver tentados a sacar muchas fotos con la península y el mar de fondo. La llegada es al Puerto Cañón, un muelle pequeño cerca de los restos coloniales y la batería de artillería. <br>
                          Frente al mismo se encuentra un bosque cerrado. Atravesando sus caminos se llega a las playas: conduciendo el camino transversal, a Puerto Jardín, de aguas cristalinas y rocas y en el lado opuesto, hacia el mar abierto, se encuentra Playa Honda. <br>
                          Ambas son playas tranquilas, sin olas ni viento.<br>
                        </p>
					  <p><strong>Datos interesantes</strong></p>
                      <p>• En 1869 hubo en la Isla Gorriti corridas de toros, organizadas por marineros de fragatas españolas.<br>
                        • Fue usada como cementerio de ingleses no católicos y como tiro al blanco por argentinos e ingleses.<br>
                      • Ha sido una isla con mucho protagonismo en la historia nacional y mundial, ya que fue usada tanto para evacuaciones de la población de Maldonado como para ocupaciones artiguistas, portuguesas e inglesas y cuarentena de cólera. </p>
