						<p>A lo largo del embarazo de Bajo riesgo, se recomiendan por lo menos entre tres y cinco  ecografías, siempre a criterio del médico tratante. En el embarazo de Alto riesgo, las necesarias de acuerdo a cada patología. Generalmente, el primer estudio se realiza ante la falta menstrual, o entre la <strong>semana 6 y 7 de retraso</strong>. Confirma la gravidez, la edad del embarazo,  determina su ubicación, la vitalidad y el número de embriones. Este estudio conviene realizarlo por vía transvaginal, no nesecita preparación, es mas pre ciso y no tiene contraindicaciones para el embarazo normal. </p>
						<p>La ecografía transvaginal puede detectar no solo el embarazo, sino también la actividad cardíaca (los latidos del corazón), a partir de la sexta semana, aproximadamente, siempre contando a partir del primer día de la última menstruación. </p>
						<p>La segunda ecografía se recomienda <strong>entre la semana 11 y 14</strong>. Es aquí donde se estudia la translucencia nucal, la presencia del hueso nasal y se pueden descartar las malformaciones fetales mas importantes. Se puede determinar el riesgo de enfermedades genéticas, trisomías, fundamentalmente trisomía 21 (síndrome de Down), trisomía 13 y 18, entre otras. Puede en ciertos casos, desaconsejar la realización de estudios invasivos, que conllevarían riesgos innecesarios.</p>
						<p>La tercer ecografía se recomendaría <strong>entre la semana 20 y 24</strong>. Es a esta edad gestacional donde se estudia en profundidad la anatomía fetal. Se pueden diagnosticar las alteraciones anatómicas fetales, malformaciones, más importantes y frecuentes. Pueden diagnosticarse aproximadamente un 75 % de las malformaciones fetales más comunes.</p>
						<p>La última ecografía se recomienda realizar <strong>entre la semana 30 y la 34</strong>. Aquí se evalúa el crecimiento fetal, la altura de la placenta en cuanto a su inserción y su grado de maduración. Además puede valorarse la ubicación del feto, la cantidad de líquido amniótico y la topografía del cordón umbilical</p>
						<p>Podría realizarse además, de ser necesaria, una <strong>Ecografía próxima al parto</strong>, para confirmar presentación en casos dudosos, estimación de peso y presencia de circulares de cordón umbilical en cuello, entre otras.</br>Si bien se suponen estudios inocuos para el feto y su madre, ya que su fundamento biofísico es el ultrasonido, sonido de alta frecuencia, inocuo e imperceptible para el oído humano, sin haberse descrito efectos secundarios desde los comienzos de su utilización (hace más de 30 años), su utilidad estará dada por la necesidad del médico de verificar el estado del embarazo. </p>
						<p>Es importante que los padres sepan el alcance de cada ecografía y que elementos nos  puede brindar, para el control y seguimiento del desarrollo fetal. No hay que exigirle al método mas allá de lo que éste nos puede ofrecer.</p>
						<p>Si bien hasta hace pocos años, las ecografías eran solo en dos dimensiones, el avance de la tecnología nos acerca hoy a la tridimensión, agregando relieves y volúmen a las imágenes obtenidas (Ecografías 3-4 D). Esta nueva tecnología, logra una mayor precisión en el estudio y un diagnóstico y confirmación más precisa de ciertas patologías, además de poder valorar y conocer los rasgos faciales del futuro recién nacido (“Estudio social”), lo que facilita el vínculo entre la pareja, su familia y el nuevo integrante del hogar.</p>
