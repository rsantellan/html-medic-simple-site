				    <h1><strong>Infecciones urinarias en niños</strong></h1>
                        <p>La infección urinaria es una enfermedad que corresponde a la colonización, invasión y multiplicación de microorganismos patógenos en las vías urinarias. Puede presentarse a cualquier edad, siendo más frecuente en niñas mayores de dos meses. Sus síntomas son fiebre, dolor al orinar, irritabilidad y urgencia para acudir al baño.</p>
                        <p>El tracto urinario está compuesto por los riñones, los uréteres, la vejiga y la uretra. Cada órgano tiene una función específica en el proceso de eliminación de los desechos líquidos del cuerpo. Los riñones son los encargados de filtrar la sangre y producir la orina, los uréteres llevan la orina de los riñones a la vejiga y ésta almacena la orina hasta que es eliminada del cuerpo a través de la uretra.</p>
                        <p>La infección se denomina cistitis cuando lo comprometido es la parte baja, compuesta por la uretra y la vejiga. Pasa a ser pielonefritis cuando se extiende hacia partes más altas del tracto urinario, ascendiendo por los uréteres hasta los riñones. <br>
                          En este último caso es más grave, ya que compromete órganos que pueden llevar al daño renal.</p>
                        <p>Cuando los niños dejan los pañales y empiezan a limpiarse solos, debemos enseñarles buenos hábitos higiénicos. <br>
                          Las niñas deben asearse con el papel higiénico desde adelante hacia atrás, para impedir que los gérmenes procedentes del recto entren en la uretra. Además, se les debe enseñar a todos los niños que no aguanten sus ganas de orinar, porque el mantener la orina en la vejiga proporciona a las bacterias un campo de cultivo idóneo para proliferar.</p>
                        <p>Además, se recomienda el uso de ropa interior de algodón.</p>
                        <p>El tratamiento de esta enfermedad es con antibióticos y una vez finalizado es necesario realizar un nuevo examen de orina para confirmar que la infección ha terminado, ya que si queda mal curada puede volver a presentarse. </p>
                        <p><strong>Debe consultar con un médico si su hijo:</strong></p>
                        <p>- Tiene fiebre acompañada de escalofríos, sobre todo si se asocia a dolor de espalda.<br>
                          - Orina más de lo habitual.<br>
                          - Se moja de día o de noche en una edad en la que ya tiene el control miccional.<br>
                          - Tiene orina maloliente, sanguinolenta o 
                          descolorida.<br>
                          - No logra una buena curva de peso y talla.</p>
